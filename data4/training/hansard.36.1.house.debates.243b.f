résidence de le gouverneur général 
le 14 septembre 1999 
monsieur le Président : 
je ai le honneur de vous aviser que le honorable John Major , juge puîné de la Cour suprême de le Canada , en sa qualité de Gouverneur général suppléant , se rendra à la Chambre de le Sénat , aujourd' hui , le 14 septembre 1999 à 14 heures , afin de donner la sanction royale à certains projets de loi . 
