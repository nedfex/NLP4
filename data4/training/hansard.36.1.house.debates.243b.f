* * * 
message DU SÉNAT 
le président suppléant (M. McClelland ) : 
je ai le honneur de annoncer à la Chambre que le Sénat lui a adressé un message pour le informer que il a adopté certains projets de loi que il la prie de approuver . 
le huissier de le bâton noir apporte le message suivant : 
monsieur le Président , ce est le désir de le honorable suppléant de le Gouverneur général que cette honorable Chambre se rende immédiatement auprès de lui dans la salle de le honorable Sénat . 
en conséquence , le Président et les députés se rendent à le Sénat . 
et de retour : 
le président suppléant (M. McClelland ) : 
je ai le honneur de faire savoir à la Chambre que , lorsque elle se est rendue à le Sénat , il a plu à le suppléant de le Gouverneur général de donner , à le nom de Sa Majesté , la Sanction royale à les projets de loi suivants : 
projet de loi C - 32 , Loi visant la prévention de la pollution et la protection de le environnement et de la santé humaine en vue de contribuer à le développement durable - Chapitre 33 . 
projet de loi C - 78 , Loi constituant le Office de investissement de les régimes de pensions de le secteur public et modifiant la Loi sur la pension de la fonction publique , la Loi sur la pension de retraite de les Forces canadiennes , la Loi sur la pension de retraite de la Gendarmerie royale de le Canada , la Loi sur la continuation de la pension de les services de défense , la Loi sur la continuation de les pensions de la Gendarmerie royale de le Canada , la Loi sur les allocations de retraite de les parlementaires , la Loi sur la Société canadienne de les postes et une autre loi en conséquence - Chapitre 34 . 
comme il est 14 h 19 , la Chambre se ajourne à 11 heures le lundi 20 septembre 1999 , conformément à les paragraphes 28 ( 2 ) et 24 ( 1 ) de le Règlement . 
( la séance est levée à 14 h 19 . ) 
