la santé 
le engagement de un ancien candidat de le Parti québécois à le poste de président - directeur général de le hôpital de Ottawa 
le honorable Philippe Deane Gigantès : 
honorables sénateurs , une chose scandaleuse se produit à Ottawa et on doit y mettre un terme . 
je parle de la nomination deM. David Levine à le poste de président - directeur général de le nouveau centre hospitalier de Ottawa . 
de les gens à Ottawa se opposent ouvertement à cette nomination parce que il a un passé péquiste . 
les péquistes font la même chose , mais nous ne sommes pas comme les péquistes . 
nous ne devons pas imiter les mauvaises pratiques de un gouvernement séparatiste qui demande quelles sont les convictions politiques de un candidat avant de le embaucher . 
je ai déjà dit à une personne que elle avait remporté un concours . 
elle me a répondu : « Mais , sénateur , saviez - vous que je suis séparatiste ? » 
je lui ai dit : « Je vous embauche . » 
elle a été loyale envers moi . 
ce est cela , la réalité canadienne , le idéal canadien . 
se attaquer à quelque un à cause de ses convictions politiques est indigne . 
