la dette de les pays en développement 
le honorable Nicholas W . Taylor : 
en 1996 , la dette de les pays en développement était de 1,5 billion de dollars américains . 
plusieurs pays consacrent plus de 20p. 100 de leur PIB rien que à le service de leur part de cette dette . 
le Fonds monétaire international a répertorié 41 pays « fortement endettés » . 
sur ces 41 pays , 32 sont en Afrique subsaharienne . 
( 1410 ) 
dans certains pays de le monde , on ne peut pas , de façon réaliste , espérer que les habitants se serrent la ceinture pour résoudre le problème de la dette . 
À le contraire , la croissance requise pour commencer à rembourser ces dettes ne peut venir que de investissements massifs dans le capital humain , physique et social . 
il serait possible de trouver une solution profitable à tous , le prêteur et le créancier , en se souvenant que le Canada est à la fois un exportateur de matériel et aussi de capitaux . 
les pays industrialisés exportent un pourcentage toujours plus grand de leurs produits vers les marchés de le tiers monde . 
il se ensuit que de les économies prospères dans ces parties de le monde profiteraient à les entreprises canadiennes . 
comme pour beaucoup de autres choses dans la vie , nous pouvons bien faire en faisant de petits sacrifices maintenant , ou nous pouvons attendre les conséquences de le inaction et en payer le prix plus tard . 
deuxièmement , nous devons nous assurer que les pays en développement empruntent dans les meilleures conditions possibles . 
les gouvernements de ces pays en développement devraient pouvoir appeler de les experts qui soient indépendants de le processus politique et de les organismes internationaux de prêt . 
autrement dit , honorables sénateurs , ils ont besoin de experts financiers qui leur diront quels trucs employer , quelle devise rembourser en premier et , cela va sans dire , quel système utiliser pour contrôler la dette . 
