Mme Angela Vautour ( Beauséjour - Petitcodiac , NPD ) : 
monsieur le Président , la Loi sur le Bureau canadien de la sécurité de les transports a été adoptée pour la première fois en 1989 , sous le gouvernement Mulroney . 
il réunissait sous le même toit un certain nombre de organismes fédéraux responsables de la sécurité de les transports . 
le NPD se était opposé à la loi originale pour trois raisons . 
premièrement , un Bureau de la sécurité de les transports nouvellement créé était sous - financé et insuffisamment équipé pour se acquitter de son vaste mandat , qui comprend les enquêtes relatives à tous les accidents aériens , maritimes et ferroviaires ainsi que ceux concernant les pipelines . 
deuxièmement , la loi permet à les ministres et à les sociétés privées intéressées de examiner les rapports provisoires de le bureau et de les commenter . 
