troisièmement , favoriser la collaboration entre les Inuvialuit , le gouvernement de le Canada et le gouvernement de les Territoires de le Nord - Ouest en ce qui a trait à la planification , à le exploitation et à la gestion de le parc . 
quatrièmement , susciter et appuyer la création et le maintien de emplois et de entreprises dans la région , en favorisant le usage de la chasse à le intérieur de le parc uniquement à de les fins de subsistance . 
cinquièmement , favoriser une plus grande compréhension et un plus grand respect de le patrimoine culturel de les Inuvialuit , ainsi que de le milieu naturel dans lequel cette nation a évolué . 
sixièmement , créer un environnement propice à de les recherches à long terme sur le patrimoine écologique et culturel de le parc . 
et septièmement , préserver le intégrité écologique de le parc . 
le parc sera géré conjointement avec la communauté inuvialuit . 
le conseil de gestion de le parc sera composé de cinq membres : deux nommés par les Inuvialuit ; deux par le gouvernement fédéral , dont un sur la recommandation de le gouvernement de les Territoires de le Nord - Ouest ; et un président qui sera nommé après entente de toutes les parties . 
le Conseil de gestion de le parc sera responsable de concilier les différents objectifs de préservation de la nature , de développement économique et touristique et de respect de les traditions autochtones . 
le entente prévoit le développement de un plan de formation et de aide communautaire pour aider les résidants de Paulatuk à mettre en valeur les ressources touristiques et économiques liées à le parc , le embauche prioritaire de employés inuvialuit lorsque ils possèdent les compétences nécessaires , une priorité à les entreprises inuvialuit lors de le octroi de contrats , si elles respectent les modalités de le contrat quant à la fourniture de produits et services de qualité . 
