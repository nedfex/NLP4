je reconnais que le projet de loi S - 2 ne est pas très différent de le projet de loi C - 86 qui le avait précédé durant la trente - cinquième législature . 
mais en théorie , les sénateurs auraient pu faire de ce projet de loi ce que ils voulaient et nous le renvoyer . 
