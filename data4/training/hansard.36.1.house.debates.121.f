monsieur le Président , je interviens aujourd' hui à le étape de la troisième lecture sur le projet de loi C - 38 présenté par la ministre de le Patrimoine canadien et dont le titre est , à la suite de le adoption de notre amendement à le étape de le rapport , Loi modifiant la Loi sur les parcs nationaux ( création de le parc Tuktut Nogait ) . 
ce projet de loi vise à créer un parc national dans les Territoires de le Nord - Ouest , plus précisément dans la région désignée de le Règlement de la revendication foncière de les Inuvialuit . 
en contrepartie de la cession à la Couronne de leurs intérêts sur de autres terres que ils revendiquaient , le gouvernement de le Canada a contracté , à le terme de la Convention , certaines obligations en faveur de les Inuvialuit qui habitent cette région . 
cette convention a été mise en vigueur par le adoption , en 1984 , de la Loi sur le règlement de les revendications de les Inuvialuit de la région ouest de le Arctique . 
ce est donc en partie pour respecter ses obligations envers les Inuvialuit que le gouvernement fédéral a entrepris , en 1989 , de les négociations en vue de la création de un parc national dans cette région . 
les six parties impliquées dans cette négociation étaient le gouvernement fédéral , le gouvernement de les Territoires de le Nord - Ouest , la Corporation régionale inuvialuit , le Conseil inuvialuit de gestion de le gibier , la Corporation communautaire de Paulatuk et le Comité de chasseurs et de trappeurs de Paulatuk . 
comme chacun et chacune le sait , on crée un parc pour autant que il représente un aspect original à protéger . 
alors , ce parc de Tuktut Nogait , de une superficie de 16 340 kilomètres carrés , représentera , dans le réseau de les parcs nationaux , la région naturelle de les collines de la toundra . 
le parc possède une riche biodiversité , car les collines et les vallées , où croît une végétation luxuriante , offrent un excellent habitat pour le caribou et le boeuf musqué . 
un grand nombre de falaises et de remparts en font un lieu de nidification idéal pour les oiseaux de proie . 
le parc offrira à les visiteurs le occasion de découvrir de les paysages arctiques intacts et de en observer la faune et la flore . 
entre autres , le parc offrira de les activités où on pourra pratiquer la randonnée pédestre , le camping , le observation de les oiseaux et de la nature et la photographie . 
