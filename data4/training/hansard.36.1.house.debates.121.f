quoi que il en soit , le projet de loi S - 2 est une mesure de ordre administratif , foncièrement valable . 
il comporte toutefois quelques lacunes , que je aimerais porter à le attention de la Chambre . 
le article 4 , qui fixe les modalités de emploi de les membres de le bureau , dont il limite le nombre à cinq , dont trois à temps plein , ne prévoit aucun système de nomination transparent fondé sur le mérite . 
les membres de le bureau continueront de être choisis , à la discrétion de le ministre , comme ce est déjà le cas pour de nombreux autres organismes gouvernementaux . 
la seconde lacune tient à le fait que le bureau conservera le pouvoir discrétionnaire de ne pas enquêter à le sujet de accidents mortels se il estime que une enquête ne résulterait probablement pas en une réduction de les risques pour les personnes , les biens ou le environnement . 
si aucune enquête ne a lieu , comment peut - on aboutir à de telles conclusions ? 
le organisme dispose actuellement de seulement 135 enquêteurs et de un budget de seulement 22 millions de dollars pour 1998 - 1998 . 
il convient de se interroger à le sujet de les priorités de un gouvernement qui accorde de les centaines de millions de dollars en subventions et prêts - subventions à de les entreprises qui construisent de les aéronefs , mais seulement une pitance pour tirer à le clair pourquoi un avion se écrase . 
je rappelle que bien que les écrasements de avion attirent beaucoup le attention , le bureau doit aussi se occuper de les trains , navires et pipelines réduits en morceaux à la suite de accidents . 
les 135 enquêteurs sont trop éparpillés . 
je exhorte le gouvernement à remédier à la situation . 
le projet de loi S - 2 ne remédie pas à les problèmes intrinsèques de le bureau , mais les lacunes de le organisme , que je ai porté à le attention de la Chambre , sont dues à de les omissions beaucoup plus que à de les actions . 
