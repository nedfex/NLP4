la crise agricole dans les provinces de les Prairies - La possibilité de une aide gouvernementale 
le honorable Leonard J . Gustafson : 
honorables sénateurs , ma question se adresse à le leader de le gouvernement à le Sénat et concerne la crise dans le secteur agricole . 
si les sénateurs se étaient trouvés à Regina samedi , ils y auraient vu de la colère , de la désillusion et de le désespoir . 
la situation y est très grave . 
ma question ne concerne pas directement le Programme de aide en cas de catastrophe liée à le revenu agricole , ou ACRA , parce que nous avons déjà dit que il ne fonctionne pas . 
le ministre de le Agriculture de la Saskatchewan a dit que il faudrait remplacer ce programme parce que il ne permet pas de résoudre efficacement les problèmes de les agriculteurs , en particulier en Saskatchewan , mais également dans certaines parties de le Alberta et de le Manitoba . 
compte tenu de le nombre très peu élevé de contribuables en Saskatchewan et de le fait que cette province compte pour 65p. 100 de le industrie canadienne de le grain , la province ne peut supporter la situation actuelle . 
il appartient à le gouvernement fédéral de trouver une solution . 
lorsque le comité sénatorial permanent de le agriculture et de les forêts se est rendu en Europe , nous avons appris , à le cours des25 séances que nous avons tenues dans quatre pays , que les Européens sont déterminés à épauler solidement leurs agriculteurs . 
cela ne fait aucun doute . 
le Union européenne versera 79 milliards de dollars à ses agriculteurs . 
les États - Unis ont indiqué clairement leur intention de soutenir leurs propres agriculteurs . 
en octobre dernier , le gouvernement avait déjà versé jusque à 80 000 $ par agriculteur . 
les agriculteurs canadiens ont déjà perdu 41p. 100 de leurs revenus à cause de la baisse de les prix de les produits agricoles . 
les agriculteurs de la Saskatchewan , comme ceux de certaines régions de le Manitoba et de le Alberta , ne peuvent supporter cette situation . 
