le agriculture et le agroalimentaire 
le honorable Eugene Whelan : 
honorables sénateurs , je aimerais soulever de nouveau la question de la décision prise par le gouvernement de le Canada de faire disparaître les stations de recherche de Agriculture Canada et de laisser à le secteur privé le soin de diriger et de financer la recherche . 
il semble que je aie eu raison de craindre que les multinationales se intéressent à le plus haut point à le financement de la recherche pour ce qui est de certains produits que ils pourraient ensuite contrôler grâce à leurs brevets ou en effectuant de les manipulations génétiques dans le but de empêcher les fermiers de reproduire eux - mêmes leurs semences , les forçant ainsi à en acheter de nouvelles chaque année . 
Barry Wilson a publié récemment dans le Western Producer un article qui prouve bien que mes craintes et mes avertissements répétés relativement à les dangers de la perte de notre potentiel de recherche sont maintenant partagés par bon nombre de personnes . 
je espère toutefois que il ne est pas trop tard . 
le auteur souligne que à le cours de les dix dernières années , la situation se est empirée à le fur et à mesure que les multinationales ont commencé à se occuper de le financement de la recherche . 
il semble maintenant que les scientifiques passent plus de le tiers de leur temps à ramasser de les fonds plutôt que à se occuper de leurs travaux de recherche . 
les fonds sont plus accessibles pour les projets à court terme qui permettront de réaliser rapidement de les profits que pour les travaux de recherche fondamentale qui visent le bien public en général . 
le équipement de les laboratoires gouvernementaux sert davantage à les projets à court terme et on ne le remplace pas lorsque il devient désuet . 
les sociétés privées ne sont pas prêtes à financer de les laboratoires qui ne leur appartiennent pas . 
nous avons été témoins , ici , dans la capitale nationale , de certaines décisions coûteuses et très peu prévoyantes . 
la station de recherche agricole de Greenbank était une de les plus modernes à le monde . 
elle était dotée de les terrains et de les installations nécessaires pour mener de la recherche sur les grains , les produits laitiers , le boeuf , le porc et la volaille . 
avec la fermeture de nos installations à Ottawa , nous avons perdu plus de 300 scientifiques de la région de la capitale . 
honorables sénateurs , je souligne que nous avions le plus vaste programme de production laitière à le monde . 
nous comptions plus de 1 200 vaches laitières , dont 500 à Ottawa seulement , qui servaient à la recherche dans deux stations différentes . 
nous disposions de un matériel remarquable . 
nous avions également la possibilité de montrer à les visiteurs de le étranger nos compétences en matière de recherche et nos pratiques de production modernes . 
lorsque je étais ministre , rien ne me faisait autant plaisir que de visiter ces installations de recherche en compagnie de dignitaires de le étranger qui venaient à le Canada pour observer les raisons de notre grande productivité à le Canada , ce pays de grands froids appelé « pays de glace et de neige » . 
honorables sénateurs , nous ne saurions tolérer ces pertes . 
je exhorte fortement le gouvernement fédéral à agir immédiatement pour rétablir le financement de la station de recherche agricole et , surtout , pour rouvrir la station agricole de Greenbank . 
bientôt , il sera trop tard , et nos compétences scientifiques et nos installations de recherche de niveau mondial seront perdues à jamais . 
