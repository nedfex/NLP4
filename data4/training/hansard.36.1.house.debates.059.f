il a appris le grec , le latin , le français , le anglais et le cri . 
il retourne en 1866 dans la région de la rivière Rouge et arrive à Saint - Boniface en juillet 1868 . 
À son retour , il découvre une forte antipathie entre colons anglais protestants nouvellement arrivés qui veulent contrôler la colonie avec le aide de le pouvoir central et les Métis , les francophones catholiques et les anglophones déjà établis sur le territoire . 
