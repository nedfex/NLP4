alors , les Métis décident de se défendre et demandent à le jeune Louis David Riel , âgé de 21 ans , et en qui ils ont confiance , de se placer à leur tête . 
Riel épouse la cause de ses compatriotes , proteste contre les opérations de les arpenteurs et envoie de les réclamations à le Parlement fédéral . 
en décembre 1869 , la Compagnie de la Baie de Hudson cesse de administrer le pays . 
le gouvernement fédéral promettra une amnistie complète et le respect de les revendications de les Métis , promesses qui ne seront jamais respectées . 
