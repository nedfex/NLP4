Lehon. Lorne Nystrom ( Que Appelle , NPD ) propose : 
- monsieur le Président , nous avons saisi aujourd' hui la Chambre de une question très importante , qui prend de plus en plus de ampleur dans tout le pays , ce est - à - dire la question de les inégalités croissantes . 
pendant de nombreuses années , dans les années 1960 et 1970 , le écart entre les riches et les pauvres se amenuisait à le Canada et nous étions fiers de ce progrès . 
de nouveaux programmes sociaux , comme le Régime de pensions de le Canada et les soins de santé universels , avaient réduit le écart entre les nantis et les pauvres . 
ce était un héritage dont nous étions tous fiers en tant que Canadiens . 
je me rappelle très bien que en 1968 , le chef de le époque de le Parti libéral , le premier ministre de le pays , Pierre Elliot Trudeau , avait fait campagne dans tout le pays à le sujet de une société juste et de une réduction de ces disparités . 
ce est ce qui se est produit pendant un certain temps , mais à le cours de les dix dernières années environ , et en particulier depuis que le gouvernement libéral actuel est arrivé à le pouvoir , nous constatons la création de ce que je appelle une société injuste où le écart se élargit entre les plus riches et les plus pauvres . 
Étant donné que nous ne sommes que à deux semaines de la présentation de le budget , il est important que nous commencions à tenir un débat public sur ces questions et le gros problème qui se pose , selon moi , à le Canada , à savoir que il y a deux classes de citoyens , les nantis , les très riches , et les autres , surtout les plus démunis . 
ce que je veux faire ce matin , très brièvement , ce est transmettre certains renseignements à la Chambre et donner , je le espère , certains paramètres à le débat que nous tenons aujourd' hui . 
malgré tout cela et malgré le fait que la conjoncture se améliore , comme le dit le ministre , la question reste à savoir pour qui elle se améliore . 
elle ne est certainement pas meilleure pour la famille canadienne moyenne , dont le revenu a diminué de 5p. 100 à le cours de les sept ou huit dernières années . 
depuis 1989 , 538 000 enfants se sont ajoutés à ceux qui vivent dans la pauvreté à le Canada , le un de les pays les plus riches de le monde . 
le nombre de banques alimentaires a triplé à le Canada depuis 1989 . 
le nombre de personnes qui ont déclaré faillite a aussi triplé à le cours de cette même période . 
nous constatons également que , en 1996 , le nombre de personnes à faible revenu est supérieur de 40p. 100 à ce que il était en 1989 . 
je maintiens que nous assistons actuellement à la création de une société injuste et que ce sera le héritage que aura laissé notre ministre de les Finances , à moins que il ne change ses priorités et que il ne commence à redistribuer le revenu et les possibilités à le Canada . 
ce est de cela que il est question dans le débat de ce matin . 
quelles sont les raisons de ce problème ? 
je dénombre pour ma part quatre ou cinq raisons évidentes . 
de abord , à le cours de les dernières années , la Banque de le Canada se est montrée déterminée , et avec le appui de le ministre de les Finances et de ses prédécesseurs , à combattre le inflation et à donner la priorité à cet objectif par rapport à les objectifs de création de emplois et de croissance de le économie . 
le économie a donc ralenti , le chômage a augmenté , les emprunts sont devenus plus dispendieux et le expansion de les petites entreprises et de le industrie agricole ont ralenti sur toute la ligne . 
deuxièmement , notre pays ne considère pas la création de emplois comme un objectif urgent . 
il ne y a pas de cible établie pour la réduction de le chômage ou de la pauvreté , comme ce est le cas pour la réduction de le inflation à le Canada . 
le plus grand coup a été porté quand le ministre de les Finances a réduit les transferts à les provinces pour les programmes de santé et de éducation . 
ce changement a fait grandir le écart entre les plus riches et les plus pauvres , de un bout à le autre de le pays . 
de autre part , le ministre de les Finances est allé puiser dans la caisse de le assurance - chômage pour tenter de économiser de le argent et de équilibrer le budget . 
enfin , le exemple qui illustre le mieux le fossé qui se creuse de plus en plus entre les riches et les pauvres est certes le projet de fusion de la Banque de Montréal et de la Banque Royale annoncé il y a environ deux semaines . 
À moins que les Canadiens et les députés libéraux de arrière - ban ne contestent , je prédis que le ministre de les Finances acquiescera à la demande de ses bons amis de la Banque de Montréal et de la Banque Royale le an prochain , à les mois de octobre ou de novembre . 
voilà donc certaines raisons qui expliquent le écart qui se accentue . 
la disparité de les revenus est un autre problème qui devrait tous nous inquiéter . 
les écarts deviennent énormes . 
les PDG de entreprises cotées à la bourse de Toronto qui ont exercé leurs options de titres en 1996 ont vu leurs gains augmenter de 653 700 $ durant la dernière année comparativement à 238 000 $ le année précédente . 
en comparaison , leurs employés ont obtenu de les hausses salariales de 2,1p. 100 en 1996 , ce qui dénote encore une fois un écart considérable qui devrait préoccuper tous les Canadiens . Comme je le disais , nous assistons à la création de une société injuste . 
la rémunération de les principaux PDG à le Canada soulève également de les questions . 
je pense notamment à le président de la Banque de Montréal , Matthew Barrett , et à celui de la Banque royale de le Canada , John Cleghorn . 
si le on tient compte de leurs options de titres , de leurs traitements et de les primes que ils reçoivent , tous deux touchent plusieurs millions de dollars chacun par année . 
prenons aussi le exemple de notre vieil ami Frank Stronach , qui est président et PDG de Magna International . 
laissez - moi vous parler un peu de Frank Stronach , le ami de le ministre de les Institutions financières . 
Frank Stronach a reçu le an dernier une rémunération de 43,2 millions de dollars . 
m . Stronach a dit ceci : « Si je additionne toutes les heures de travail que je ai effectuées à le cours de les 40 dernières années , je ne ai probablement pas touché plus que le salaire minimum . » 
il a pourtant reçu 43,2 millions . 
en travaillant à le salaire minimum , soit 7 $ le heure , 24 heures par jour et 365 jours par année , M. Stronach aurait mis 688 ans à accumuler 43,2 millions de dollars . 
en effet , ces gens contribuent à leur parti politique . 
les disparités grandissantes que nous constatons à le Canada devraient tous nous inquiéter beaucoup . 
nous ne laissons pas un héritage prometteur à la prochaine génération . 
comme je partage mon temps de parole avec la députée de Vancouver - Est , je terminerai sur de les statistiques internationales intéressantes concernant les écarts entre riches et pauvres . 
les données portent sur le revenu familial . 
on constate que le Canada arrive à le deuxième rang de les sept ou huit pays de le OCDE pour ce qui est de le écart de revenu . 
À les États - Unis , le écart entre les riches et les pauvres est supérieur à 54 000 $ , et ce est là le écart le plus considérable de les neuf pays . 
le Canada est donc à le deuxième rang pour ce qui est de le pire écart entre les riches et les pauvres . 
je demande à le ministre qui répondra aujourd' hui à le nom de son collègue le Ministre de les Finances , soit le secrétaire de État chargé de les Institutions financières , de prendre la parole et de nous expliquer comment le gouvernement entend créer plus de égalité et de équité entre les riches et les pauvres à le Canada . 
ce est une question extrêmement importante . 
dans un pays de grande prospérité et à le potentiel énorme , il est triste de constater que , sur les neuf principales puissances comme la France , la Grande - Bretagne , le Allemagne et les États - Unis , le Canada se classe à le deuxième rang de les pays où le écart est le plus important entre les riches et les pauvres . 
nous assistons actuellement à le édification de une société injuste , héritage de le ministre de les Finances et de le premier ministre . 
