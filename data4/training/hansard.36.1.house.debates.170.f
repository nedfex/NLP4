monsieur le Président , à le nom de les membres de mon parti et à le nom de les députés de la Chambre de les communes , je veux rendre hommage à la députée de Windsor -St. 
ce est une merveilleuse famille à laquelle nous appartenons . 
la députée de Windsor -St. 
clair était la meilleure parmi nous . 
ce était une personne extrêmement dévouée et engagée dans toutes les causes sociales dans sa ville , Windsor . 
elle est venue ici avec les mêmes préoccupations . 
elle était présidente de le comité de la justice et a passé toute sa vie à veiller à ce que les plus démunis de notre société puissent avoir une vie un peu plus belle . 
ce était sa préoccupation quotidienne , à les comités , à la Chambre de les communes et à le caucus . 
comme tout le monde le sait , ce était une personne qui avait son franc - parler . 
elle ne avait absolument pas peur de dire les choses comme elles étaient , comme elles les voyaient . 
elle avait un grand sens de le humour . 
elle était devenue le amie de tout le monde . 
ce est incroyable la grande tristesse qui me a envahi hier soir lorsque je ai appris son décès et lorsque je ai vu mes collègues pleurer . 
nous avons perdu une soeur . 
ce que les gens ne comprennent pas , ce est que nous pouvons avoir nos divergences politiques , mais nous voulons tous faire de notre mieux pour réaliser une société meilleure . 
ce était le cas de Shaughnessy . 
elle débordait de énergie . 
elle faisait avancer les choses ; ce était une pousseuse . 
elle était très partisane de bien de les façons . 
elle croyait dans le parti et dans les valeurs de notre parti . 
