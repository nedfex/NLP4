ce doit être un choc terrible et une perte incroyable pour eux . 
mais ils savent que son séjour à le Parlement a été remarqué . 
elle avait de les convictions profondes et elle savait aussi se faire de les amis . 
la vie , pour elle , était de travailler le jour , et aussi être capable de profiter de la vie , avoir de le plaisir de temps en temps , de parfois nous créer de les problèmes , mais de me faire rire tout de suite après . 
À le nom de les membres de mon parti , à le nom de les citoyens de le Canada , je désire la remercier pour ce que elle a fait pour ses électeurs et pour tous les citoyens de le Canada . 
