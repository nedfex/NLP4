les MALADIES IMMUNOLOGIQUES ET NEUROLOGIQUES CHRONIQUES 
m . Janko Peric ( Cambridge , Lib . ) : 
monsieur le Président , le 12 mai est la Journée internationale de sensibilisation à les maladies immunologiques et neurologiques chroniques comme le syndrome de fatigue chronique , la fibromyalgie , le syndrome de la guerre de le Golfe et la polysensibilité chimique . 
ces terribles maladies frappent un nombre croissant de Canadiens qui souffrent de problèmes cognitifs , de douleurs musculaires et articulaires chroniques , de une baisse extrêmement marquée de le endurance et de nombreux autres symptômes qui rendent les gens malades et grabataires pendant de les années . 
la plupart de les gens atteints sont incapables de travailler à cause de leur douleur . 
cependant , leur souffrance ne est pas simplement physique . 
le manque de connaissances de la population à le sujet de ces troubles cause souvent à ces gens la perte de leur emploi et de leurs prestations de invalidité . 
une électrice , une mère de famille qui est une amie très proche , Leni Spooner , souffre de fibromyalgie . 
elle devra vivre avec cette maladie pour le reste de sa vie . 
on a également diagnostiqué récemment le même trouble chez sa fille de 16 ans . 
je exhorte tous les députés à faire connaître cette terrible maladie pour que de les gens comme Leni et sa fille souffrent un peu moins . 
