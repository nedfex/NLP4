la JOURNÉE DU SYNDROME DE FATIGUE CHRONIQUE 
Lehon. Andy Scott ( Fredericton , Lib . ) : 
monsieur le Président , ce est aujourd' hui à le Nouveau - Brunswick la Journée de le syndrome de fatigue chronique . 
le syndrome de fatigue chronique , ou SFC , est un trouble débilitant et complexe qui se caractérise par une profonde fatigue que le repos ne chasse pas et que une activité physique ou mentale risque de aggraver . 
on ne connaît pas la ou les causes de le SFC et il ne existe aucun test de diagnostic spécifique . 
il se agit de une véritable maladie qui mérite que on mène de vraies recherches pour en trouver la cause et pour mettre à le point un traitement . 
les symptômes en sont empirés de le fait que elle ne est habituellement pas reconnue dans le cadre de les programmes déjà disponibles . 
dans Fredericton , ma circonscription , je ai participé par les années passées à la marche annuelle de les éléphants qui vise à recueillir de les fonds pour le SFC et à conscientiser la population à cette maladie . 
