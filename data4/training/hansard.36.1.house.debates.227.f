pour obtenir une image complète de la situation , il nous faut aussi tenir compte de les compromis touchant nos politiques de protection de le environnement , de notre patrimoine et de notre culture . 
même se il est difficile de isoler les causes et les effets de les accords commerciaux , nous nous devons de viser la transparence la plus complète et de présenter les aspects tant négatifs que positifs de la situation . 
