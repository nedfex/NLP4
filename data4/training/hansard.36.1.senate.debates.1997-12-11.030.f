DÉCLARATION SOUS SERMENT De YVAN ROY 
je , soussigné , Yvan Roy , membre de le Barreau de le Québec et avocat , de la ville de Ottawa , dans la province de Ontario , déclare sous serment ce qui suit : 
je suis avocat auprès de le ministère de Justice et je suis actuellement avocat général principal à la section de la politique - droit pénal , poste que je occupe depuis 1990 . 
2 . Que la section de la politique - droit pénal est responsable de le élaboration de les initiatives de politique de même que de la préparation de les mesures législatives en matière de droit criminel . 
en conséquence , les fonctionnaires de la section de la politique - droit pénal - identifient les questions juridiques de actualité en matière de droit criminel et préparent les modifications à le Code criminel et à les autres lois pénales . 
la section de droit pénal est aussi chargée de fournir de les avis juridiques à de autres ministères fédéraux , à les provinces et à les territoires de même que à les autorités de police sur de les questions de droit criminel . 
4 . Le 22 mai 1997 , cette honorable Cour a rendu son jugement dans le affaire R . c. Feeney . 
À première vue , le Code criminel ne comporte de mécanisme législatif qui permettrait de obtenir une telle autorisation . 
5 . Le 10 juin 1997 , M. Daniel Bellemare , sous - procureur général adjoint ( droit pénal ) à le ministère de la Justice a réuni un conférence téléphonique à laquelle je ai participé , avec ses homologues provinciaux et territoriaux relativement à la question de interprétation et a donné de les avis à la police relativement à les pouvoirs de arrestation . 
puisque que aucun consensus ne se dégageait de cette conférence sur les termes que il faudrait présumer que le Code criminel comporte et à quel article ils seraient insérés , il a été évident que chaque ressort adopterait une attitude différente de celle de les autres quant à le obligation établie par le arrêt Feeney de avoir un mandat . 
de fait , il y a eu de les différences importantes quant à le attitude que les différents ressorts canadiens ont adoptée immédiatement après le arrêt Feeney , et vu les différentes considérations pratiques auxquelles les forces de police et les substituts de chaque ressort devaient faire face . 
6 . À mon avis les questions graves soulevées par le arrêt Feeney créaient de le incertitude en droit pénal et soulevaient la possibilité que le Code criminel ne soit pas appliqué de façon uniforme de un bout à le autre de le pays . 
je ai été informé par mes homologues provinciaux que ils étaient assaillis de demandes de renseignements et de avis juridiques relativement à le effet de le arrêt Feeney et à les procédures que il fallait suivre pour se assurer que les arrestation soient menées conformément à la Charte . 
ces demandes venaient principalement de les forces de police et de les substituts de différents ressorts . 
le arrêt Feeney a eu pour conséquence de exiger que les pratiques de routine de la police subissent une modification importante et immédiate . 
7 . Par avis de requête en date de le 17 juin 1997 , le procureur général de la Colombie - Britannique , avec intervention subséquente de les procureurs généraux de le Alberta , de le Canada , de le Ontario et de le Québec , a déposé devant cette honorable Cour , une motion demandant soit une ordonnance établissant une période de transition soit une ordonnance accordant une nouvelle audition afin de clarifier la nature de la disposition relative à le mandat présumément présente à le Code criminel . 
le 27 juin 1997 , cette honorable Cour a accordé une suspension de six mois de le application de le arrêt R .. c. Feeney 
quant à le obligation de avoir un mandat pour procéder à une arrestation dans une maison de habitation . 
cette suspension expire le 22 novembre 1997 . 
8 . Immédiatement après la décision de cette honorable Cour de accorder la suspension de le application de le arrêt Feeney , le sous - procureur général adjoint ( droit pénal ) et moi avons commencé de recevoir de les réponses à une lettre que nous avions fait parvenir à nos homologues provinciaux , le 20 juin 1997 , à le sujet de les procédures intérimaires appliquées dans chaque ressort avant la suspension de le arrêt Feeney , lettre par laquelle nous demandions le avis de les provinces quant à le régime législatif en vertu duquel les agents de la paix pourraient obtenir le mandat exigé par la Constitution pour entrer dans une maison de habitation pour y procéder à une arrestation . 
9 . Le 4 juillet 1997 , je ai transmis une lettre à les services juridiques de les autres ministères fédéraux afin de déterminer les dispositions législatives touchées par le arrêt Feeney et de obtenir leurs opinions sur les réformes législatives que il serait possible de adopter en réponse à cet arrêt . 
nous avons reçu de les réponses pendant tout le été . 
10 . Pendant le mois de juillet 1997 , de les collègues chargés de cette question et moi - même avons consulté de les collègues de le ministère de la Justice afin de préparer différentes options et recommandations de réforme législative . 
de les recherches ont été menées à le sujet de les lois adoptées à les États - Unis et ans de autres ressorts de common law . 
je ai alors transmis un document de discussion dans ce sens à mes collègues provinciaux afin de discuter de les possibilités raisonnables pendant la Conférence sur le uniformisation de les lois tenue de le 17 à le 22 août 1997 . 
je ai demandé à mes collègues de réagir à ces propositions concrètes dans un délai de deux semaines . 
11 . Vers la fin de août 1997 , je ai eu le occasion de soulever la question de une possible solution législative à le arrêt de cette honorable Cour dans le affaire Feeney avec la nouvelle ministre de la Justice le honorable Anne McLellan . 
avec le assentiment de la nouvelle ministre , je ai immédiatement commencé à prendre les mesures nécessaires pour présenter un projet de loi de le gouvernement , pour pourvoir à un mécanisme en vertu duquel un agent de la paix pourrait obtenir le autorisation judiciaire de entrer dans une maison de habitation pour y procéder à une arrestation . 
12 . Le 9 septembre 1997 , je ai rencontré , avec de les collègues , de les représentants de le Association canadienne de la police , de le Association canadienne de les chefs de police , de la GRC et de le Solliciteur général afin de discuter les options retenues par la ministre en vue de obtenir leurs vues sur les mesures à prendre selon le point de vue de les autorités de police . 
13 . Le 12 septembre 1997 , je ai rencontré , avec de les collègues , de les représentants de les ministères fédéraux touchés par le arrêt Feeney afin de discuter de les conséquences de les options retenues par la ministre sur le exécution de les arrestations ou les dispositions relatives à les appréhensions de les lois fédérales autres que le Code criminel et de déterminer se il fallait prendre de les mesures dans le texte législatif pour tenir compte de ces dispositions . 
la rédaction préliminaire a commencé par la suite . 
le 26 septembre 1997 , je ai tenu une conférence téléphonique avec de les représentants de le Association de le Barreau canadien afin de discuter les options retenues par la ministre et de demander leurs points de vue sur une solution législative qui respecterait un équilibre entre les intérêts de le public à une application efficace de les lois et les intérêts de les citoyens canadiens à le caractère privé de leur maison de habitation . 
15 . Le 26 septembre 1997 , mes collègues de le ministère de la Justice ont tenu une conférence téléphonique avec de les fonctionnaires de les procureurs généraux de les provinces afin de discuter les propositions à la lumière de le document de discussion qui leur avait été remis à la Conférence sur le uniformisation de les lois . 
le 29 septembre 1997 , le premier avant - projet de loi a été distribué à différentes sections de le ministère de la Justice pour fins de consultations internes . 
le 9 octobre , une version préliminaire de le projet de loi a été remise à les fonctionnaires de les procureurs généraux provinciaux et à les ministères fédéraux intéressés . 
le 14 octobre , mes collègues de le ministère de la Justice ont tenu une téléconférence avec de les fonctionnaires de les procureurs généraux provinciaux pour discuter de le texte et de toute question non résolue dans leur champ de compétence . 
de même , le 14 octobre , mes collègues de le ministère de la Justice se sont réunis avec de les responsables chargés de le application de la loi pour discuter de le texte et de toute question non résolue que ils pourraient souhaiter soulever . 
les 15 et 17 octobre , deux autres téléconférences ont été tenues entre mes collègues de le ministère de la Justice et de les fonctionnaires de les procureurs généraux provinciaux . 
les 21 et 22 octobre , mes collègues de le ministère de la Justice ont de nouveau rencontré de les responsables chargés de le application de la loi pour discuter de une version révisée de le avant - projet de loi . 
22 . Le 23 octobre , mes collègues de le ministère de la Justice se sont de nouveau réunis avec de les fonctionnaires de Immigration Canada et de le Service correctionnel pour discuter plus à fond de certains aspects de le texte qui concernaient leurs ministères respectifs . 
le 24 octobre , je ai rencontré de les représentants de le Association de le Barreau canadien et les ai mis à le courant de le contenu de la version préliminaire de le texte . 
