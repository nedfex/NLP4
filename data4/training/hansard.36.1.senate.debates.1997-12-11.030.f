lettres de dépôt et billets de dépôt 
deuxième lecture - Ajournement de le débat 
- honorables sénateurs , je suis heureuse de proposer la deuxième lecture de le projet de loi sur les lettres de dépôt et les billets de dépôt et de en appuyer le adoption rapide . 
je appuie le adoption rapide parce que , comme bon nombre de entre vous le savent , ce projet de loi est de nature essentiellement technique . 
il jouit de le plein appui de le secteur bancaire ainsi que de le industrie de les services financiers . 
comme vous êtes sans doute à le courant , le secteur de les services financiers canadiens remanie en profondeur son mode de fonctionnement . 
ce changement procède évidemment de une concurrence internationale accrue , qui résulte elle - même de énormes progrès technologiques . 
en fait , le secteur de les services financiers est sans doute celui qui a été le plus touché par le évolution de la technologie de le information . 
ce est pourquoi de les secteurs de le industrie qui ne avaient pas évolué depuis de les années sont maintenant obligés de se moderniser et de accroître leur efficience . 
la Loi sur les lettres de dépôt et les billets de dépôt constitue une autre mesure prise par le gouvernement pour faciliter la modernisation de notre secteur de les services financiers . 
ce projet de loi porte sur le transfert de propriété de certains instruments négociables de le marché monétaire , comme les acceptations bancaires et les effets commerciaux . 
À une certaine époque , les institutions détenaient et échangeaient directement ces instruments . 
de les documents changeaient effectivement de mains . 
toutefois de nos jours , les banques et de autres intervenants détiennent de plus en plus couramment ces instruments , ce est - à - dire de les obligations , de les bons de le Trésor et de autres instruments négociables par le biais de dépositaires . 
lorsque il y a cession de propriété de ces instruments , le transfert se effectue à le moyen de une inscription dans les registres de le dépositaire . 
on assiste donc à le inscription de le nouveau propriétaire dans le registre de la chambre de compensation , et non à la remise de le effet proprement dit . 
or , la législation régissant les institutions financières ne reflète pas cette pratique établie . 
le problème tient à le fait que la loi fédérale pertinente , la Loi sur les lettres de change , continue de faire allusion à la possession effective de un instrument négociable lorsque il est question de les droits de les parties à une opération . 
ainsi , le terme « porteur » désigne la personne en possession de une lettre ou de un billet payable à le porteur . 
par conséquent , lorsque un instrument négociable est détenu par un dépositaire et que le transfert se effectue à le moyen de une inscription dans un registre , les droits mentionnés dans la Loi sur les lettres de change sont impossibles à interpréter puisque le instrument lui - même demeure auprès de le dépositaire . 
jusque ici , ces exigences empêchaient de faire appel à un dépositaire ou à un système de transfert de propriété par inscription . 
le projet de loi que nous étudions aujourd' hui corrige le problème en créant deux nouvelles catégories de instruments : les lettres et les billets de dépôt . 
le projet de loi assure que le acquéreur de une lettre ou de un billet de dépôt jouit de les mêmes droits légaux que le acquéreur de une lettre ou de un billet en vertu de la Loi sur les lettres de change , sans que il y ait livraison de le instrument proprement dit . 
toutefois , puisque ils sont conçus pour être diffusés et négociés relativement à grande échelle , les droits et les responsabilités qui en découlent seront définis en faisant clairement référence à la fonction de la chambre de compensation et à le transfert par inscription . 
pour les distinguer de autres titres semblables , ces nouveaux instruments arboreront à le recto une mention attestant que il se agit de lettres et de billets de dépôt assujettis à la Loi sur les lettres de dépôt et les billets de dépôt . 
la Caisse canadienne de dépôt de valeurs limitée , honorables sénateurs , souhaite pouvoir détenir de les instruments négociables de le marché monétaire , comme de les acceptations bancaires et de les effets commerciaux , et consigner le transfert de ces instruments dans ses registres dès que possible . 
le adoption de ce projet de loi le y autorisera . 
cette nouvelle pratique sera beaucoup plus efficiente que le régime actuel , qui requiert la prise de possession effective de le instrument en cause . 
je ajoute , honorables sénateurs , que ce projet de loi ne empêche nullement de les particuliers ou de les institutions de acquérir et de détenir de autres lettres et billets négociables , lesquels demeurent assujettis à la Loi sur les lettres de change . 
honorables sénateurs , le dépôt de ce projet de loi donne suite à les recommandations de le groupe de le secteur privé qui examine le fonctionnement de le système financier international , communément appelé le G30 . 
ce groupe réclame la mise en place à grande échelle de systèmes de dépôt de valeurs et de consignation de les opérations par voie de registre , à le motif que cela améliorera le efficience de les marchés monétaires . 
cette initiative jouit également de le appui de tous les intervenants de les milieux financiers . 
je vous invite à adopter sans tarder ce projet de loi technique constructif . 
le projet de loi S - 9 ne est pas controversé et , comme je le disais plus tôt , il mérite de être adopté rapidement . 
je invite les honorables sénateurs à donner leur approbation pour que nous puissions passer à de autres projets de loi . 
