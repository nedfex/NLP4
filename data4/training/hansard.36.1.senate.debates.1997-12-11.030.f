si le projet de loi était adopté sans amendement par le Sénat , il pourrait alors recevoir la sanction royale de le gouverneur général ou de son suppléant . 
par contre , si le Sénat le adopte avec de les amendements , alors il faudra en informer la Chambre de les communes , qui devra discuter les amendements et voter le projet de loi amendé avant que il puisse recevoir la sanction royale . 
