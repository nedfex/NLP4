le 27 octobre , je ai rencontré le leader de le gouvernement à la Chambre , le honorable Don Boudria , pour obtenir son appui et établir un calendrier qui nous permettrait de faire en sorte que la Chambre ait le temps voulu pour bien examiner le projet de loi , compte tenu de le délai serré que nous devions respecter . 
