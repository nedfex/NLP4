les ressources humaines 
le âge limite pour la conversion de les régimes enregistrés de épargne - retraite par les personnes âgées - La possibilité de concessions - La position de le gouvernement 
( réponse à une question posée par le honorable sénateur Donald H . Oliver le 18 novembre 1997 ) 
le âge auquel les REER doivent être convertis en FERR a été ramené de 71 ans à 69 ans dans le budget de 1996 . 
la nouvelle limite de âge entre en vigueur cette année , ce qui veut dire que le REER de les personnes qui auront 69 , 70 ou 71 ans en 1997 doit venir à échéance de ici le 31 décembre 1997 . 
on a exprimé de les inquiétudes à le effet que certaines personnes de 69 à 70 ans ne soient pas à le courant de la date limite et pourraient , par inadvertance , ne pas accomplir les formalités voulues pour que leur REER vienne à échéance de ici le 31 décembre , les conséquences de cela étant que les régimes seraient désenregistrés , et les intéressés seraient alors assujettis à le impôt sur le plein montant de les fonds investis . 
toutefois , cette préoccupation ne est pas justifiée . 
en effet , les émetteurs de REER ont averti , en juin , tous leurs clients qui sont visés par la date limite de la fin de le année . 
le budget de 1996 exigeait que ils le fassent . 
de plus , les banques , les sociétés de fiducie et de autres émetteurs de REER ont communiqué la date limite dans de les documents promotionnels et de les documents de information publics . 
et surtout , les contrats relatifs à un REER que la plupart de les émetteurs utilisent comprennent une disposition qui stipule que le régime doit parvenir à échéance selon les instructions de le émetteur afin de respecter la date limite , lorsque le émetteur ne parvient pas à communiquer avec le détenteur de le régime . 
cela signifie que dans la quasi - totalité de les cas , les REER de les clients qui pourraient ne pas être à le courant de le changement de la date limite seront automatiquement convertis en FERR de ici le 31 décembre - évitant ainsi que ils soient désenregistrés et assujettis à le impôt . 
lorsque cette clause de inexécution est appliquée , le REER est converti en un FERR par le émetteur . 
le client reste libre de convertir le FERR en une rente ou de la transférer dans une autre institution financière , à une date ultérieure . 
de plus , à le cours de les dernières années , Revenu Canada a constaté très peu de cas de non - respect de le échéance à 71 ans , et le ministère a pour principe de faire preuve de souplesse à le égard de ce genre de situation . 
en fait , les cas qui se sont produits se rattachaient à de les situations inhabituelles , par exemple à le fait que le émetteur de le REER avait mal pris en note le âge de le client . 
en pareil cas de erreur commise par inadvertance , Revenu Canada a pu lever les pénalités qui dans de autres cas se seraient appliquées . 
Étant donné les mécanismes de protection en place , changer la date limite maintenant ne ferait que invalider tous les avis et tous les documents publics qui ont été diffusés à de les fins de information , ce qui sèmerait la confusion et le incertitude chez les personnes âgées qui sont touchées par le changement . 
