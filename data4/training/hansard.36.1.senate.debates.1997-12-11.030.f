le 31 octobre , le projet de loi C - 16 a reçu la deuxième lecture à la Chambre de les communes et a été renvoyé à le Comité permanent de la justice et de les droits de la personne . 
le 4 novembre , le Comité de la justice et de les droits de la personne a entendu deux témoins , soit la ministre de la Justice , le honorable Ann McLellan , que je accompagnais , et le Association canadienne de les policiers . 
le 5 novembre , le Comité de la justice et de les droits de la personne a entendu trois témoins , soit le Centre canadien de ressources pour les victimes de crimes , le Association canadienne de les criminalistes et le Association canadienne de les chefs de police . 
le Barreau de le Québec a soumis un mémoire à le Comité , mais aucun représentant ne est venu témoigner . 
je ai assisté à les audiences de le Comité de la justice et de les droits de la personne . 
À ces audiences , je ai entendu de les membres de le Comité de tous les partis indiquer que ils craignaient que sans nouveau délai pour examiner le projet de loi , les parlementaires ne seraient pas en mesure de jouer leur rôle de représentants bien informés chargés par le gouvernement de le Canada de adopter cette mesure législative . 
30 . Pour préparer cette mesure , les fonctionnaires de le ministère de la Justice ont tenu un grand nombre de consultations , jusque à le dépôt de le projet de loi , avec de nombreux groupes que la question intéresse , ainsi que avec de les fonctionnaires de les ministères fédéraux intéressés et de les ministères provinciaux de les procureurs généraux . 
ces consultations étaient essentielles à le élaboration de un projet de loi qui tente de trouver la meilleure solution de compromis sur les nombreuses questions découlant de le arrêt Feeney . 
toutefois , à cause de ces nécessaires consultations , le Parlement se est retrouvé à court de temps pour étudier ces questions importantes et les témoins ont manqué de temps pour bien préparer leurs exposés ou mémoires . 
néanmoins , la Chambre a procédé à la troisième lecture de le projet de loi le 7 novembre , compte tenu de le délai . 
