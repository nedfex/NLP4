le honorable Richard J . Doyle : 
honorables sénateurs , je prends la parole à le sujet de une question urgente de intérêt public . 
plus de une semaine se est écoulée depuis que trois sénateurs ont posé de les questions à le leader de le gouvernement à le Sénat sur la réponse que le gouvernement devrait donner à le rapport final de le juge Horace Krever sur la Commission de enquête sur le approvisionnement en sang de le Canada . 
maintenant , grâce à les médias , les Canadiens sont conscients de le ampleur de les problèmes qui doivent être réglés . 
dès le tout début de son rapport , le juge Krever souligne le étendue de les difficultés . 
il dit : 
une catastrophe nationale en santé publique a frappé le ensemble de le Canada à la fin de les années 1970 et pendant les années 1980 . 
À le début de les années 1990 , plusieurs centaines de Canadiens infectés par le VIH avaient contracté le sida . 
bon nombre de entre eux étaient déjà décédés . 
de autres , qui avaient reçu une transfusion à le début de les années 1980 , apprenaient que ils étaient infectés par le VIH . 
on reconnaissait de plus en plus le importance et la gravité de la contamination de les réserves de sang ... 
honorables sénateurs , faites bien attention à le langage très direct que utilise le juge . 
le cinquième mot de le rapport est « catastrophe » . 
puis arrivent en cascade les mots « infectieux » , « contaminé » et « décès » . 
le histoire de la progression de cette calamité est racontée sur un ton sombre , dans trois volumes , révélant la lenteur avec laquelle ce pays a réagi à la propagation de le infection et le insouciance de les gardiens de notre santé face à les avertissements . 
tout indique que nous avons négligé les précautions prises à les États - Unis . 
le juge Krever a fourni à les gardiens de la santé publique le plus solide de les arguments en faveur de une transformation globale et immédiate de le système de protection de le approvisionnement en sang . 
je ai constaté , dans les réponses données à le Sénat la semaine dernière , une sympathie authentique à le égard de les victimes de une négligence nationale . 
le ministre a dit que « le gouvernement présenterait une réponse très exhaustive » . 
on peut seulement espérer que les communiqués de presse décrivant ce que il a fait jusque à maintenant ne font pas partie de cette « réponse exhaustive » . 
il ne faudrait pas que il reporte à plus tard cette étape de les correctifs . 
la population , et surtout les organismes représentant les personnes à risque , devraient pouvoir présenter publiquement leurs arguments en faveur de le changement . 
on ne doit pas les faire attendre jusque en février , car les gouvernements auront alors établi de les programmes qui ne pourront plus être modifiés . 
seul un débat global et permanent , lancé en cette Chambre , incitera les gens à se intéresser à un domaine où le échec a fait loi pendant trop longtemps . 
