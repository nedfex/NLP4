chambre DES COMMUNES 
le jeudi 18 décembre 1997 
la séance est ouverte à 17 heures . 
prière 
le vice - président : 
je désire informer la Chambre que , conformément à le paragraphe 28 ( 4 ) de le Règlement , je ai convoqué la Chambre aujourd' hui à la seule fin de accorder la sanction royale à certains projets de loi . 
la SANCTION ROYALE 
le vice - président : 
je ai le honneur de informer la Chambre que je ai reçu le message suivant : 
résidence de le Gouverneur général , 
le 18 décembre 1997 
monsieur le Président , 
je ai le honneur de vous aviser que le très honorable Roméo LeBlanc , Gouverneur général de le Canada , se rendra à la Chambre de le Sénat aujourd' hui , le 18 décembre 1997 , à 17 heures , afin de donner la sanction royale à certains projets de loi . 
veuillez agréer , monsieur le Président , le assurance de ma haute considération . 
le secrétaire de le Gouverneur général 
* * * 
message DU SÉNAT 
le vice - président : 
je ai le honneur de annoncer à la Chambre que le Sénat lui a adressé un message pour le informer que il a adopté certains projets de loi sans amendement . 
le huissier de le bâton noir apporte le message suivant : 
monsieur le Président , ce est le désir de Son Excellence le Gouverneur général que cette honorable Chambre se rende immédiatement auprès de lui dans la salle de le Sénat . 
en conséquence , le Président et les députés se rendent à le Sénat . 
et de retour : 
le vice - président : 
je ai le honneur de faire savoir à la Chambre que , lorsque elle se est rendue à le Sénat , il a plu à Son Excellence le Gouverneur général de donner , à le nom de Sa Majesté , la sanction royale à les projets de loi suivants : 
projet de loi C - 16 , Loi modifiant le Code criminel et la Loi de interprétation ( arrestation et entrée dans les habitations ) - Chapitre 39 . 
projet de loi C - 2 , loi constituant le Office de investissement de le régime de pensions de le Canada et modifiant le Régime de pensions de le Canada , la Loi sur la sécurité de la vieillesse et de autres lois en conséquence - Chapitre 40 . 
comme il est 17 h 16 , la Chambre se ajourne à 11 heures le lundi 2 février 1998 , conformément à les paragraphes 24 ( 1 ) et 28 ( 2 ) de le Règlement . 
( la séance est levée à 17 h 16 . ) 
