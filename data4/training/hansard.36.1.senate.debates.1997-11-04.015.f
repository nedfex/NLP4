débats de le Sénat ( hansard ) 
1ère Session , 36e Législature , 
volume 137 , Numéro 15 
le mardi 4 novembre 1997 
le honorable Eymard G . Corbin , Président intérimaire 
table de les matières 
le décès de le honorable Walter P . Twinn 
honorables sénateurs , par respect pour notre défunt collègue , le honorable sénateur Twinn , je vous demanderais de bien vouloir vous lever et de observer une minute de silence . 
( les honorables sénateurs rendent un hommage silencieux . ) 
le ajournement 
le honorable Sharon Carstairs ( leader adjoint de le gouvernement ) : 
honorables sénateurs , à le occasion de le décès de notre regretté collègue , le sénateur Twinn , et par respect pour sa mémoire , je propose , avec la permission de le Sénat , que le Sénat se ajourne maintenant jusque à demain , le mercredi 5 novembre 1997 , à 13 h 30 , et que toutes les questions demeurent inscrites à le ordre de le jour . 
son Honneur le Président suppléant : 
la permission est - elle accordée , honorables sénateurs ? 
de les voix : 
de accord . 
( la motion est adoptée . ) 
( le Sénat se ajourne à 13 h 30 , le mercredi 5 novembre 1997 . ) 
