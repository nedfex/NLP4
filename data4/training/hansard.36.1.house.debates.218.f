le CENTRE De EMPLOI DE BROCK 
m . John O' Reilly ( Haliburton - Victoria - Brock , Lib . ) : 
monsieur le Président , les résidents de le canton de Brock , près de la circonscription de Haliburton - Victoria - Brock , ont reçu de le ministère fédéral de le Développement de les ressources humaines une subvention de 168 000 $ , qui leur a donné un bon coup de pouce . 
cette subvention a permis de établir le nouveau centre communautaire de ressources en emploi , situé à le hôtel de ville de Beaverton . 
le centre ouvrira officiellement ses portes à le public demain , le vendredi 30 avril . 
il peut se avérer très difficile de trouver un emploi . 
le centre aidera les résidents à chercher de le emploi par le truchement de un certain nombre de services . 
les gens auront notamment accès à de les services de orientation en emploi , à de les listes de emplois , à de les services de photocopie gratuits , à de les télécopieurs et ordinateurs ainsi que la liste de les cours de recyclage offerts par les collèges et universités . 
le centre a été créé grâce à le partenariat de le gouvernement fédéral , de le canton de Brock , de le collège Durham et de autres organismes . 
je félicite les personnes qui sont à le origine de ce projet . 
le centre aidera non seulement les résidents mais également toute la communauté de le canton de Brock . 
