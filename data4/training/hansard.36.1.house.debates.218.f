le PRIX DE Le ESSENCE 
m . David Chatters ( Athabasca , Réf . ) : 
monsieur le Président , le prix de le essence attire beaucoup le attention ces derniers jours . 
le fait est que les Canadiens peuvent compter sur les prix de le essence parmi les plus bas à le monde . 
sans les taxes gouvernementales , le essence est aussi bon marché que il y a 20 ans . 
la seule chose qui a changé beaucoup , ce est le montant de les taxes que les Canadiens doivent payer à la pompe . 
lorsque je me rends à le épicerie ou à le restaurant , je vois le prix de un produit avant le imposition de les taxes . 
pourquoi les vendeurs de essence à le détail ne offrent - ils pas la même chose à leurs clients ? 
pourquoi ne annoncent - ils pas leurs prix avant le imposition de les taxes pour que les Canadiens puissent bien voir combien de argent ils donnent à le gouvernement chaque fois que ils font le plein ? 
pourquoi les détaillants sont - ils prêts à porter le odieux de les taxes gouvernementales ? 
les Canadiens ont besoin de un allégement de leur fardeau fiscal maintenant pour pouvoir mieux se en sortir . 
