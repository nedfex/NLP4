la VIOLENCE CHEZ LES JEUNES 
m . Reg Alcock ( Winnipeg - Sud , Lib . ) : 
monsieur le Président , nous avons tous été bouleversés et attristés par la tragédie insensée qui a eu lieu hier après - midi à Taber , en Alberta . 
nous adressons nos condoléances à la famille et à les amis de Jason Lang qui a perdu la vie hier durant cette tuerie . 
nos prières accompagnent la famille et les amis de Shane Christmas qui demeure dans un état critique à le hôpital . 
en tant que parent , je ne peux imaginer la terreur que on éprouve lorsque on entend parler de une tuerie à le école de son enfant . 
je peux comprendre la douleur de les gens de Taber aujourd' hui . 
en tant que parent , je insiste pour que nos écoles soient ce que elles sont censées être , un lieu de apprentissage , un lieu où nous pouvons envoyer nos enfants en sachant que ils sont en sécurité . 
À titre de député , cela me rappelle que nous devons tous unir nos efforts pour veiller à faire disparaître de nos collectivités les conditions qui conduisent à de les actes de violence aussi horribles . 
comme le père de Jason Lang le a dit aujourd' hui : « Beaucoup de choses doivent changer et beaucoup de plaies restent à cicatriser » . 
À le cours de les jours et de les mois à venir , je sais que tous les députés veilleront à faire tout ce qui peut être fait . 
