président Havel , Madame Havlovà , Monsieur le Premier ministre et Madame Chrétien , sénateurs , collègues de la Chambre de les communes , distingués invités , mesdames et messieurs . 
monsieur le Président , je vous remercie de avoir honoré la Chambre et nous tous de votre présence et de votre éloquence . 
comme le a dit le premier ministre , nos deux Chambres ne se rassemblent pas souvent . 
si nous le faisons aujourd' hui , monsieur le Président , ce est un signe de les liens étroits qui existent entre la République tchèque et le Canada , et de notre grande amitié . 
ce est le témoignage de un attachement indéfectible à les principes politiques les plus nobles . 
notre pays a la bonne fortune de connaître la démocratie depuis sa création . 
pourtant , il peut arriver de oublier ce que ce est que de vivre dans une société démocratique . 
de autre part , monsieur le Président , vous avez dû lutter pour garantir les droits politiques de votre peuple , en courant personnellement de grands risques . 
vous avez agi selon vos convictions , et vous le avez souligné aujourd' hui , voulant que tous ont droit à la liberté et à la dignité . 
et , en tant que parlementaires canadiens , nous savons quels efforts vous avez déployés pour rebâtir les institutions parlementaires de votre pays , qui sont le expression de ces droits . 
votre présence à la Chambre est un puissant symbole pour nous tous , un symbole qui nous rappelle que nous devons toujours chérir , nourrir et renouveler les idées démocratiques fondamentales dont la Chambre est le incarnation . 
vous nous avez donné une perspective plus large de les défis qui attendent notre pays se il veut jouer un rôle utile dans un monde tourmenté . 
nous sommes conscients que il y a de les valeurs tellement fondamentales que elles méritent de être défendues , parfois à ne importe prix . 
car , en dernière analyse , ces valeurs ne sont pas uniquement tchèques ou canadiennes , voire occidentales , mais elles appartiennent à le humanité tout entière . 
comme vous , nous sommes fiers de notre diversité et nous nous efforçons de tirer profit de nos différences . 
monsieur le Président , vous nous avez montré comment une seule personne peut infléchir le cours de le histoire , même dans le adversité . 
le monde peut se féliciter de avoir en vous un porte - parole aussi éloquent de ses plus hautes aspirations . 
il y a quelques années , monsieur le Président , à le instar de une foule de Canadiens , sinon de la totalité de entre eux , je ai applaudi à le printemps de Prague et je ai pleuré avec vous quand votre rêve a pris fin . 
maintenant , ces dernières années , quand vous avez accédé à la présidence de votre pays , ce fut le début de un nouveau printemps de Prague . 
vous avez parlé non seulement de droits individuels , mais vous avez aussi parlé , monsieur le Président , de le humanité . 
je ai dit une fois à la Chambre à une occasion semblable à celle - ci que , pour connaître la force de un pays , il faut regarder ses lois et ses soldats . 
quant à le âme de un pays , il faut , pour la saisir , se tourner vers ses poètes , ses écrivains et ses artistes . 
aujourd' hui , vous êtes devenu pour nous tous le poète , le écrivain et le interprète de le âme de le humanité . 
merci de être avec nous aujourd' hui . 
