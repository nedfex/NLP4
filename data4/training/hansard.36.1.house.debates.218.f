la LOI DE Le IMPÔT SUR LE REVENU 
le adoption est un geste noble consistant à donner à un enfant de les parents qui le aimeront . 
les parents adoptifs doivent souvent assumer de lourdes dépenses lors de le adoption et pourtant les frais relatifs à le adoption ne sont même pas déductibles de impôt , alors que le adoption constitue une importante contribution sociale . 
un environnement où il y a une maman et un papa est un environnement propice à le épanouissement de un enfant . 
le adoption est également une mesure très rentable et utile à une époque où il y a tant de parents seuls et de grossesses chez les adolescentes . 
le projet de loi entend reconnaître la contribution sociale que les parents adoptifs apportent et corriger les iniquités à le égard de les parents adoptifs . 
