cette détermination est le un de les fondements qui nous ont permis de investir dans une économie et dans une société plus fortes , de alléger la fiscalité de le ensemble de les Canadiens tout en évitant le déficit budgétaire . 
comme il y a beaucoup de choses à voir dans le projet de loi , je passe immédiatement à les mesures de le projet de loi comme telles , en commençant par la question cruciale de les soins de santé . 
