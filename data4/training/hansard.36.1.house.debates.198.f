la FONCTION PUBLIQUE DU CANADA 
m . Keith Martin ( Esquimalt - Juan de Fuca , Réf . ) : 
monsieur le Président , les employés de le AFPC se font rouler . 
ils sont en grève parce que on les a privés de la possibilité de recourir à le arbitrage obligatoire . 
tout ce que les employés de le AFPC souhaitent , ce est être traités équitablement et retourner à le travail . 
pendant ce temps , ce est le public et notre économie qui en souffrent . 
que veulent les employés de le AFPC ? 
ils veulent toucher la même paie pour le même travail exigeant les mêmes compétences que les autres membres de les autres syndicats à le sein de le appareil gouvernemental . 
pour le instant , les employés de le AFPC sont beaucoup moins payés . 
dans la circonscription de Esquimalt - Juan de Fuca que je représente , les personnels de la base de les Forces canadiennes ont toujours été à la hauteur de les attentes . 
on a licencié plus de 40p. 100 de entre eux , on a exigé de eux que ils soient encore plus efficaces et on a doublé leurs charges de travail . 
voilà comment le gouvernement les a récompensés . 
nous devons traiter les employés de le AFPC équitablement , sur un pied de égalité avec les autres travailleurs à le sein de le gouvernement fédéral , leur accorder les mêmes avantages pécuniaires et autres afin que ils retournent à le travail . 
si le gouvernement ne en fait rien , ce sera la preuve que il a abdiqué ses responsabilités . 
