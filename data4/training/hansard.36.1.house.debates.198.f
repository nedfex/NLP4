la partie 1 modifie la Loi sur les arrangements fiscaux entre le gouvernement fédéral et les provinces afin de augmenter de 11,5 milliards de dollars la contribution à le titre de le Transfert canadien en matière de santé et de programmes sociaux . 
cette contribution constitue le plus important investissement jamais fait par le gouvernement . 
cet investissement aidera les provinces à répondre à les préoccupations immédiates de les Canadiens à le sujet de les soins de santé . 
leurs préoccupations portent , de toute évidence , sur les aspects suivants : les listes de attente , le encombrement de les salles de urgence et les services de diagnostic . 
par ailleurs , cette contribution financière assurera à les Canadiens une stabilité et une prévisibilité à plus long terme de les services de santé . 
le premier ministre Romanow a reconnu le importance de le budget lorsque il a déclaré : « Ce budget assurera une injection de fonds attendue depuis longtemps dans le système de soins de santé , non seulement en Saskatchewan mais à la grandeur de le pays . » 
le projet de loi autorise le ministre de les Finances à payer 3,5 milliards de dollars de ces fonds à une fiducie qui les distribuera à les provinces selon les modalités que elles établiront , sur une période de trois ans commençant le 1 er avril 1999 . Les 8 milliards de dollars restants seront distribués sur une période de quatre ans commençant le 1 er avril 2000 . 
ces montants sont , en soi , imposants mais on me permettra de les situer dans un contexte plus général , à le intention de la Chambre et de les téléspectateurs . 
quand le augmentation de le financement atteindra 2,5 milliards de dollars en 2001 - 2002 , le soutien financier direct de le gouvernement fédéral dans le cadre de le TCSPS se élèvera à 15 milliards de dollars par année . 
autrement dit , le montant destiné à la santé dans le cadre de le TCSPS sera aussi élevé que avant la période de réduction de les dépenses . 
ces réductions avaient pour but de éponger le déficit à le milieu de les années 90 . 
mais ce ne est pas tout . 
si on ajoute la valeur croissante de les transferts de points de impôt à le financement en espèces , on obtient une aide totale de 30 milliards de dollars à les provinces en 2001 - 2002 . 
ce est un nouveau sommet . 
ce projet de loi prévoit la disparition complète de les disparités par habitant dans la répartition de les fonds de le TCSPS entre les provinces . 
en 2001 - 2002 , toutes les provinces recevront un montant par habitant identique pour les services de santé et de services sociaux . 
en plus de le augmentation de 11,5 milliards de dollars de les transferts provinciaux à la santé , il est également annoncé dans le budget que le gouvernement investira un montant supplémentaire de 1,4 milliard de dollars pour le reste de le année et à le cours de les trois années à venir dans les systèmes de information de la santé , la recherche , les services de santé de les Inuit et de les premières nations ainsi que dans la prévention de la maladie . 
grâce à de les augmentations non négligeables de la péréquation , les provinces moins riches disposeront également de plus de ressources pour les services publics , dont la santé . 
