grâce à la création de un régime national de prestations pour enfants , le gouvernement fédéral et les provinces se sont lancés dans un effort important de coopération pour aider les familles et réduire la pauvreté chez les enfants . 
nous voulons nous assurer que les enfants se en sortent toujours mieux lorsque leurs parents entrent sur le marché de le travail . 
en 1997 , nous avons annoncé la première contribution fédérale à ce projet national , 850 millions de dollars , à compter de juillet de le an dernier , ce qui a permis de accroître le soutien financier offert à plus de deux millions de enfants et à leurs familles . 
dans le budget de 1998 , nous avons annoncé que 850 millions de dollars de plus seraient débloqués à la suite de consultations avec les gouvernements provinciaux et territoriaux . 
la mesure législative de aujourd' hui fait suite à ces consultations . 
elle prévoit également 300 millions de dollars de plus pour accroître les prestations versées à les familles à revenu faible ou moyen . 
je dois expliquer que la prestation fiscale canadienne pour enfants a deux éléments clés , la prestation de base et le supplément de le Régime national de prestations pour enfants . 
le projet de loi propose de les modifications à le supplément et à la prestation de base . 
il permet de respecter le engagement pris dans le budget de 1998 de accroître de 850 millions de dollars les sommes versées à les familles à faible revenu à le titre de le supplément de le Régime national de prestations pour enfants . 
la mise en oeuvre de les modifications proposées exige que le on modifie divers articles de la Loi de le impôt sur le revenu , le article 122.6 pour être plus précis . 
ces modifications hausseront de 350 $ le maximum prévu par enfant à le titre de le Régime national de prestations pour enfants , en deux étapes , soit 180 $ en juillet 1999 et de 170 $ en juillet 2000 . 
finalement , les prestations de les familles à revenu modeste ou moyen augmenteraient de 184 $ . 
dans le ensemble , ces changements signifient , par exemple , pour une famille avec deux enfants qui gagne 20 000 $ , une hausse de les prestations de 700 $ , pour un total de 3 750 $ par année . 
examinons le ensemble de les résultats . 
les mesures prises dans nos deux précédents budgets à le chapitre de la prestation fiscale pour enfants assureront 1,7 milliard de dollars à les enfants de les familles à faible revenu . 
cette mesure législative prend appui sur cet effort , non pas seulement en précisant la distribution de les 850 millions alloués en 1998 , mais en affectant 300 millions de plus à la prestation fiscale pour enfants de les familles à revenu modeste et moyen . 
en conséquence , 100 000 nouvelles familles canadiennes deviendront admissibles à une partie ou à la totalité de la prestation de base . 
