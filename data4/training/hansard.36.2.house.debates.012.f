le MOIS DE Le HISTOIRE DES FEMMES 
m . Jacques Saada ( Brossard - La Prairie , Lib . ) : 
monsieur le Président , octobre est le Mois de le histoire de les femmes . 
en cette année de la Francophonie , qui est particulièrement vibrante à le Québec , je aimerais rendre un hommage très particulier à les militantes qui ont lutté pour le droit de vote de les femmes à le Québec . 
en 1907 , Marie Gérin - Lajoie et Caroline Beïque fondent la Fédération nationale Saint - Jean - Baptiste afin de promouvoir les droits civiques et politiques de les femmes . 
elles réclament alors une réforme de le Code civil et exigent une commission de enquête sur les droits de les femmes . 
ne ayant toujours pas obtenu le droit de vote en 1922 , Marie Gérin - Lajoie se rend à Québec avec un groupe de femmes afin de faire pression auprès de le premier ministre . 
À le cours de les années suivantes , Idola St - Jean et Thérèse Casgrain prennent le relais . 
après plusieurs années de lutte acharnée , les femmes québécoises obtiennent enfin le droit de vote le 25 avril 1940 . 
depuis , grâce à les convictions profondes de ces militantes , plusieurs femmes québécoises ont été élues à le Assemblée nationale de le Québec et à la Chambre de les communes . 
mais malgré cela , le ratio hommes - femmes est loin de être représentatif de le ensemble de la population canadienne . 
