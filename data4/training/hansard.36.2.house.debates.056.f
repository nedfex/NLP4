Mme Paddy Torsney ( secrétaire parlementaire de le ministre de le Environnement , Lib . ) : 
monsieur le Président , ce est avec plaisir que je vais répondre à la question de le député de Halifax - Ouest . 
il se agit de une question qui me a grandement tenu à coeur pendant toute ma carrière . 
comme la présidente de le Conseil de le Trésor le a déjà fait observer , le gouvernement se efforce de augmenter le nombre de membres de les minorités visibles dans la fonction publique fédérale . 
il est important de reconnaître que , pendant le exercice financier 1998 - 1999 , 14 338 personnes qui occupaient un poste de durée indéterminée ont quitté la fonction publique , et 3,7p. 100 de entre elles appartenaient à une minorité visible . 
pendant la même période , nous avons recruté 2 533 employés pour une période indéterminée , et 6,9p. 100 de entre eux étaient membres de une minorité visible . 
nous reconnaissons que ce pourcentage reste inférieur à les 8,7p. 100 que ce groupe représente sur le marché de le travail , mais nous continuons de évoluer vers une plus grande représentativité de la fonction publique . 
pendant le exercice financier 1998 - 1999 , 38 cadres de le extérieur ont été embauchés et 10,5p. 100 de entre eux appartenaient à une minorité visible . 
nous avons fait la preuve de notre engagement à accroître la représentation de les minorités visibles à le niveau de la direction . 
comme le a reconnu le député de en face , un groupe de travail sur la participation de les minorités visibles dans la fonction publique fédérale a été constitué en avril dernier . 
le groupe de travail est en train de étudier la situation de les minorités visibles en se appuyant sur les études déjà faites et de élaborer , pour le ensemble de le gouvernement , un plan de action accompagné de stratégies concrètes en vue de régler les problèmes et de améliorer la situation de les minorités visibles dans la fonction publique fédérale . 
comme le a aussi reconnu le député , nous nous attendons à ce que le plan de action soit prêt dans les mois à venir . 
le gouvernement est déterminé à ce que la fonction publique canadienne reflète mieux la diversité , la force et la croissance de la société canadienne . 
je me joins à le député de en face pour exhorter la présidente de le Conseil de le Trésor et tous les députés à suggérer de les mesures concrètes et à voir à ce que le plan soit mis en oeuvre . 
ce est important pour tous les Canadiens . 
