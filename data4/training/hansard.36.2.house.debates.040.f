les ENFANTS 
Mme Jean Augustine ( Etobicoke - Lakeshore , Lib . ) : 
monsieur le Président , la période de les Fêtes est synonyme de joie et de bonheur pour de nombreux enfants canadiens , mais pour un jeune enfant inconnu , ce ne sera pas le cas . 
mardi dernier , les gens de Etobicoke - Lakeshore ont été choqués de apprendre que le corps de un enfant non identifié a été trouvé démembré dans le Colonel Samuel Smith Park , un parc local . 
la mort de cet enfant est un exemple de le type de violence qui nous touche tous . 
la vie ne reprendra pas son cours normal dans nos collectivités tant que nous ne aurons pas identifié cet enfant et trouvé les coupables de ce meurtre . 
la vie de nos enfants est trop précieuse pour ne pas mériter notre protection . 
alors que nous pleurons la mort tragique de cet enfant , prenons le temps de réfléchir à les choses qui menacent la vie de nos enfants et de adopter de les mesures pour se y attaquer . 
nous devons nous assurer que les enfants vivent en sécurité dans Etobicoke - Lakeshore et dans toutes les collectivités de notre merveilleux pays . 
