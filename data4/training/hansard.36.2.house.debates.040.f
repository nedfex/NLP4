le DÉCÈS DE VICTOR QUELCH 
m . David Chatters ( Athabasca , Réf . ) : 
monsieur le Président , je interviens à la Chambre aujourd' hui pour réparer un oubli commis par inadvertance il y a bien de les années . 
m . Victor Quelch a été député de 1935 à 1958 . 
À le cours de ces 23 ans , il a servi le pays en tant que député créditiste de la circonscription de Acadia , dans le sud de le Alberta . 
parlementaire tenu en haute estime , il a exercé plusieurs fonctions dont celle de porte - parole de son parti pour les finances , le agriculture et les pêches . 
en 1950 , le premier ministre Louis St - Laurent le a nommé conseiller de le personnel canadien auprès de le Assemblée de les Nations Unies . 
avant de embrasser la carrière politique , M. Quelch avait pris part à la Première Guerre mondiale et reçu la Croix militaire pour bravoure . 
le sens de le devoir envers son pays et ses citoyens a guidé toute la vie deM. Quelch , et pourtant , à sa mort survenue en 1975 , aucun hommage ne lui a été rendu à la Chambre . 
même si cela ne a que trop tarder , je aimerais souligner les bons et loyaux services deM. Quelch . 
ses contributions à le Canada datent un peu peut - être , mais elles ne ont pas été oubliées . 
