les pêches et les océans 
les provinces maritimes - Le jugement de la Cour suprême confirmant les droits de pêche autochtones 
le honorable Lowell Murray : 
honorables sénateurs , je vous demanderais de vous montrer un peu indulgents , le temps que je touche un mot sur la situation qui a cours dans le secteur de les pêches sur la côte est . 
À les dernières nouvelles , et je dois avouer que je me en remets presque exclusivement à les bulletins de presse de ce matin , les chefs semblent avoir renoncé à le moratoire de 30 jours que ils avaient consenti à respecter . 
toujours de après les comptes rendus de presse de le débat qui a eu lieu à le autre endroit hier soir , il semble que le ministre de les Pêches , M. Dhaliwal , a été pris par surprise par cette nouvelle , tout comme le gouvernement est resté bouche bée , il y a quatre semaines , quand la Cour suprême de le Canada a rendu sa décision . 
loin de moi le idée , honorables sénateurs , de me poser en expert juriste et encore moins en interprète de le jugement de la Cour suprême de le Canada , mais la déclaration faite par un de les chefs ce matin , selon laquelle lui et ses collègues se réservent le droit de réglementer leurs propres activités de pêche , est , à mon humble avis , contraire à le jugement rendu par la Cour suprême de le Canada . 
dans la décision majoritaire , le juge Binnie fait état de : 
... la portée limitée de le droit issu de le traité . 
il écrit ceci : 
le droit issu de le traité est un droit réglementé qui peut , par règlement , être circonscrit à ses limites appropriées . 
de les limites de prises , dont il serait raisonnable de se attendre à ce que elles permettent à les familles mi' kmaq de se assurer une subsistance convenable selon les normes de aujourd' hui , peuvent être établies par règlement et appliquées sans porter atteinte à le droit issu de le traité . 
un tel règlement respecterait ce droit et constituerait pas une atteinte qui devrait être justifiée suivant la norme établie dans le arrêt Badger . 
honorables sénateurs , il me semble que ces déclarations sont une invitation lancée par la Cour suprême de le Canada à le gouvernement afin que il propose un règlement . 
quand le juge fait référence à le règlement , à les limites de captures et à le imposition de un règlement , il y a lieu de supposer que il parle de un règlement qui serait proposé par le gouvernement . 
il y a environ deux semaines , le premier ministre aurait dit que le gouvernement envisageait de demander à la Cour suprême de surseoir à son jugement . 
plus récemment , le ministre de les Pêches se en est pris à les députés de le opposition qui demandaient la même chose . 
je suppose que le gouvernement a étudié et rejeté le option de demander de surseoir à le jugement et que cette option a été éliminée . 
il me semble donc que tout ce que il reste à faire à le gouvernement , ce est de présenter une mesure législative ou un règlement , ou les deux . 
en outre , le gouvernement devrait envisager de renvoyer le règlement en question directement à la Cour suprême pour veiller à ce que il soit bien compatible avec le jugement rendu par cette dernière . 
je doute fort que les autochtones se satisfassent de moins de cela . 
ne importe quel règlement qui sera déposé sera obligatoirement contesté et devra passer par le ensemble de le processus judiciaire . 
je estime prudent que le gouvernement renvoie un tel règlement à la Cour suprême de le Canada . 
honorables sénateurs , si je dis cela , ce est simplement parce que la situation se aggrave de jour en jour . 
sans vouloir être alarmiste , je pense que nous ne sommes que trop conscients de les incidents violents qui se sont produits dans la région et sont à la veille de se produire . 
un vide juridique a été créé . 
de autres ont rempli ce vide créé par la Cour suprême de le Canada et il me semble que ça va être le chaos . 
