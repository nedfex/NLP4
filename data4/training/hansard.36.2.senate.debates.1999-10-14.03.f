le honorable Catherine S . Callbeck : 
honorables sénateurs , je aimerais souligner que nous célébrerons le lundi 18 octobre prochain la Journée de la personne . 
ce est le 18 octobre 1929 que le Conseil privé britannique a statué que les femmes étaient de les « personnes » en droit canadien et que elles pouvaient donc être élues à le Sénat . 
cette décision a été rendue à la suite de une longue bataille juridique et politique menée par Emily Murphy , Nellie McClung , Louise McKinny , Irene Parlby et Henrietta Muir Edwards , cinq femmes canadiennes mieux connues comme étant les « Famous Five » . 
depuis 1979 , en commémoration de le affaire « Personne » , de les prix de le Gouverneur général sont décernés chaque année à les alentours de le 18 octobre pour honorer de les femmes qui ont activement contribué à la promotion de la femme . 
je suis fière de faire savoir à les honorables sénateurs que une femme de Tignish , en Île - du - Prince - Édouard , Anne - Marie Perry , sera parmi les cinq lauréates de cette année . 
Anne - Marie est une personne remarquable qui continue de contribuer activement à le bien - être de sa communauté , de sa province et de son pays . 
ce est la deuxième femme de le île à recevoir ce prix prestigieux . 
Helen Yeo , de Charlottetown , en Île - du - Prince - Édouard , le a déjà remporté en 1986 . 
À le niveau provincial , elle a défini et appuyé de les changements de programmes en vue de accroître les chances de les femmes durant son mandat de membre et de présidente de le comité de les appels de les services sociaux de le Île - du - Prince - Édouard . 
À le niveau national , Mme Perry a été un membre actif de le Conseil consultatif canadien de la situation de la femme de 1981 à 1986 . 
quatre autres femmes recevront le même honneur que Anne - Marie cette année . 
il se agit de Mmes Bertha Allen , de Inuvik , dans les Territoires de le Nord - Ouest , Maris Eriksen , de Calgary , en Alberta , Enid Page , de Aylmer , à le Québec et Bette Stephenson , de Richmond Hill , en Ontario . 
honorables sénateurs , les cinq lauréates de les prix de le Gouverneur général de cette année méritent de les félicitations pour le remarquable travail que elles ont accompli dans leurs communautés en vue de faire avancer la cause de les femmes . 
