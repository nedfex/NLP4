la DÉFENSE NATIONALE 
m . Gordon Earle ( Halifax - Ouest , NPD ) : 
monsieur le Président , les hélicoptères Sea King et Labrador sont - ils sûrs ? 
le tragique écrasement survenu le 2 octobre était - il évitable ? 
de les militaires ou de les civils sont - ils actuellement en danger à cause de le état de les hélicoptères Sea King et Labrador ? 
voilà quelques - unes de les questions auxquelles le gouvernement libéral a , envers les Canadiens , le obligation de répondre publiquement et de manière exhaustive . 
le 2 octobre 1998 , un hélicoptère Labrador se est écrasé à proximité de Marsoui , en Gaspésie , et les six membres de son équipage sont morts . 
apparemment , le hélicoptère a pris feu et se est brisé en trois morceaux . 
en 1992 , un autre hélicoptère se est écrasé à proximité de Bella Coola , en Colombie - Britannique , tuant le caporal Phil Young , qui venait de ma province , la Nouvelle - Écosse . 
le 28 avril 1994 , le major Wally Sweetman et son copilote , le major Bob Henderson , ont perdu la vie lorsque la conduite de carburant de un hélicoptère Sea King a fait défaut . 
les hélicoptères Sea King et Labrador ont tous deux été mis en service il y a plus de un tiers de siècle , en 1963 . 
À le début de le année de entrée en service de ces hélicoptères , John Diefenbaker était premier ministre , 19 députés qui siègent actuellement à la Chambre ne étaient pas encore nés , Medgar Evers était encore en vie et Martin Luther King ne avait pas encore prononcé son fameux discours . 
les nouveaux hélicoptères de recherche et sauvetage Cormorant qui ont été commandés ne seront pas livrés et ne entreront pas en service avant le prochain millénaire . 
entre - temps , le gouvernement libéral , en grande partie à cause de les décisions que il a prises , a peut - être à se prononcer sur la santé et la sécurité de le personnel militaire , de une part , et les besoins de recherche et sauvetage de les civils qui sont en danger , de autre part . 
en 1994 , le gouvernement a publié un livre blanc de la défense dans lequel il exposait sa stratégie pour les forces armées . 
À le lieu de procéder immédiatement à un appel de offres pour de nouveaux hélicoptères , il a attendu trois ans , soit jusque en 1997 . 
pour comble , le gouvernement a commandé essentiellement le même type de hélicoptère que dans le contrat dont le annulation venait de coûter près de un demi - milliard de dollars à les contribuables . 
pourquoi , après son accession à le pouvoir , a il attendu encore quatre ans avant de commander de nouveaux hélicoptères ? 
pourquoi , se étant moqué de le choix initial de le gouvernement Mulroney , a il choisi le même type de appareil ? 
le gouvernement a dit que les équipages ne feraient pas le objet de mesures disciplinaires se ils refusaient de se servir de les Labrador . 
en faisant cette déclaration , il me semble que le gouvernement essaie de se protéger en cas de écrasement futur . 
la sécurité de ces appareils est certainement une source de inquiétude pour que le gouvernement fasse une telle déclaration . 
le lieutenant - colonel Dennis Hopping , ancien pilote de Labrador , a dit ceci : 
il est très difficile pour un membre de équipage de refuser de entreprendre une mission pour sauver une vie . 
ce est terrible de mettre un pilote professionnel dans une telle position . 
le major à la retraite Marvin Haagsma a dit ceci : 
je ai connu personnellement de les taux de panne moteur qui sont 500 fois plus élevés que ce qui est accepté comme étant « normal » pour la génération actuelle de hélicoptères . 
je vais terminer en posant deux dernières questions auxquelles le gouvernement doit répondre . 
pourquoi le gouvernement est - il assez certain de la sécurité de ces hélicoptères pour les mettre en service actif alors que on a tant de raisons de croire le contraire ? 
quelles autres options , comme de les contrats de location à court terme , ont été examinées ? 
m . Robert Bertrand ( secrétaire parlementaire de le ministre de la Défense nationale , Lib . ) : 
monsieur le Président , notre plus grande priorité est la sécurité de nos équipages et de nos appareils . 
le chef de état - major de la force aérienne a assuré le ministre de la Défense nationale que les Labrador peuvent effectuer de les vols en toute sécurité . 
le fait que on détecte les problèmes et que on prenne immédiatement de les mesures pour y remédier témoigne de la rigueur de notre programme de maintenance et de inspection . 
les Forces canadiennes disposent de normes élevées de navigabilité , non seulement dans le cas de les Labrador , mais aussi de tous les autres appareils de les Forces canadiennes . 
depuis le écrasement de un Labrador , à le début octobre , on peut aisément comprendre pourquoi les équipages et le personnel responsable de la maintenance de les Labrador prennent de les précautions supplémentaires pour se assurer que ces appareils sont sûrs . 
ils font tout simplement preuve de beaucoup de prudence . 
comme le honorable député le sait , une enquête est présentement en cours pour déterminer les circonstances de cet écrasement et il serait inopportun de se perdre en conjectures sur les causes de cet accident . 
il est important de laisser le enquête suivre son cours , afin de déterminer les causes de le écrasement et les mesures qui devraient être prises pour prévenir un nouvel accident de le même genre . 
il ne faut pas oublier que outre les hélicoptères Labrador , les Forces canadiennes possèdent de autres appareils , comme par exemple les Hercules et le Buffalo qui ont également , pour principales responsabilités , de faire de la recherche et de le sauvetage . 
en janvier 1998 , le gouvernement a annoncé que il achèterait 15 hélicoptères Cormoran pour remplacer les Labrador . 
les premiers Cormoran seront en service de ici le an 2001 . 
la stratégie de approvisionnement relative à le remplacement de les vieux Sea Kings est à le étude . 
la location est une de les possibilités que nous envisageons dans le cadre de la stratégie de approvisionnement . 
comme le ministre de la Défense nationale le a dit , il espère faire une annonce de ici un an . 
nous sommes toujours résolus de fournir à les Forces canadiennes le équipement dont elles ont besoin pour accomplir leurs importantes missions , à le pays et à le étranger . 
la PAUVRETÉ 
m . Lynn Myers ( Waterloo - Wellington , Lib . ) : 
en dépit de les programmes qui existent pour combler ce fossé , il ne en continue pas moins de se élargir . 
de plus en plus de Canadiens tombent sous le seuil de la pauvreté et , devant cette situation , nous devons nous demander ce que nous pouvons faire pour renverser la tendance . 
le gouvernement doit examiner la question et adopter de les mesures correctives . 
les députés ont certainement entendu parler de le rapport paru cet automne et qui faisait état de le écart croissant entre les riches et les pauvres . 
apparemment , les riches deviennent plus riches et les pauvres plus pauvres . 
À le Canada , les PDG gagnent en moyenne 862 000 $ par année , montant qui grimpe à 1,5 million de dollars lorsque on inclut les options sur actions . 
pendant ce temps , à le cours de la même période , les salaires de les travailleurs canadiens moyens ne ont pas augmenté de plus de 2p. 100 , ce qui est inférieur à le taux de inflation . 
qui plus est , un grand nombre de Canadiens sont licenciés et ne ont de autre choix que de accepter de les emplois moins bien rémunérés , ou de compter sur le aide de le gouvernement . 
en 1971 , le revenu moyen de le 10p. 100 de la population composé de les familles les plus riches était de 107 000 $ , soit 21 fois plus que celui de le 10p. 100 formé de les familles les plus pauvres . 
un écart existait déjà , mais celui - ci était moins marqué . 
en 1996 , les familles canadiennes les plus riches gagnaient en moyenne 314 fois plus que les familles canadiennes les plus pauvres . 
ces chiffres parlent de eux - mêmes . 
le écart a grandi considérablement . 
il nous incombe , en tant que parlementaires , de adopter de les mesures législatives pour essayer de corriger ce problème . 
la pauvreté est tout simplement inacceptable dans un pays comme le Canada . 
notre pays est riche en ressources naturelles et humaines . 
nous sommes fiers de être le une de les nations les plus riches et les plus industrialisées à le monde et nous ne pouvons laisser la pauvreté venir changer tout cela . 
nous nous sommes toujours enorgueillis de être un pays économiquement fort , et ce est le une de les raisons pour lesquelles nous ne accepterons pas que cet écart continue de croître . 
il faut faire quelque chose . 
en tant que parlementaires , il importe que nous fassions quelque chose pour régler le problème . 
je demande à la secrétaire parlementaire de le ministre de le Développement de les ressources humaines de nous dire exactement ce que nous , en tant que gouvernement , devrions faire pour corriger la situation . 
Mme Bonnie Brown ( secrétaire parlementaire de le ministre de le Développement de les ressources humaines , Lib . ) : 
monsieur le Président , nous avons pris un certain nombre de mesures visant justement à aider les gens à sortir de la pauvreté . 
de ici le an 2000 , le montant que nous versons chaque année à les familles à faible revenu qui ont de les enfants se élèvera à 1,7 milliard de dollars . 
ce montant est en sus de les 5,1 milliards de dollars que le gouvernement consacre déjà à les familles avec enfants qui sont dans le besoin . 
la solution à long terme pour faire disparaître la pauvreté consiste à aider les Canadiens et Canadiennes à trouver de le travail . 
aussi , notre Stratégie canadienne pour le égalité de les chances facilite elle le accès à le éducation et à les compétences dont les Canadiens ont besoin se ils veulent trouver de meilleurs emplois dans le nouvel ordre économique . 
dans le cadre de cette stratégie , nous avons investi 2,5 milliards de dollars dans le fonds de les bourses de le millénaire , grâce auquel 100 000 étudiants à revenu faible ou moyen pourront bénéficier chaque année de une bourse de environ 3 000 dollars . 
jusque à 25 000 autres étudiants ayant de les enfants à charge pourront bénéficier de les nouvelles subventions de aide à les étudiants de le Canada . 
il y a aussi la Stratégie Emploi - Jeunesse qui vient en aide à les jeunes . 
À le cours de les six à douze mois suivant la fin de le programme , quelque 86p. 100 de les participants ont soit trouvé de le travail , soit décidé de retourner à les études . 
le régime de assurance - emploi constitue un élément essentiel de notre filet de sécurité . 
il fournit à les travailleurs le aide financière temporaire et les outils nécessaires pour réintégrer le marché de le travail . 
le fonds transitoire de création de emplois a permis de créer plus de 31 000 emplois ; une somme de 2,7 milliards de dollars a été investie dans de les mesures actives de emploi destinées à aider les gens à retourner à le travail ; enfin , 350 000 familles à faible revenu qui ont de les enfants à charge ont bénéficié de le supplément de revenu familial . 
dans le cadre de un nouveau programme fédéral - provincial , 190 millions de dollars seront dépensés afin de faciliter le accès de les personnes handicapées à le marché de le travail . 
toutes ces mesures prouvent que nous sommes déterminés à faire tout notre possible pour réduire la pauvreté et garantir le égalité de les chances pour tous les Canadiens . 
