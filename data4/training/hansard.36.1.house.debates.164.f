DÉCISION DE LA PRÉSIDENCE SUR LE PROJET DE LOI S - 13 
le Président : 
je suis maintenant prêt à rendre ma décision sur le recours à le Règlement soulevé le 18 novembre 1998 par le leader de le gouvernement à la Chambre , à propos de la recevabilité de le projet de loi S - 13 , Loi constituant la Fondation canadienne de lutte contre le tabagisme chez les jeunes et instituant un prélèvement sur le industrie de le tabac . 
je veux de abord remercier le honorable leader de le gouvernement à la Chambre et le honorable députée deSt. Paul' s de leur contribution savante à la discussion de ce sujet . 
je veux également remercier les autres députés qui sont intervenus sur ce rappel à le Règlement , soit les honorables députés de Macleod , Winnipeg - Centre - Nord , Pictou - Antigonish - Guysborough , Haldimand - Norfolk - Brant , Esquimalt - Juan de Fuca , Hillsborough , Kamloops , Thompson and Highland Valleys , Pierrefonds - Dollard , Nouveau - Brunswick - Sud - Ouest , Lac - Saint - Louis , Delta - South Richmond , Whitby - Ajax , Burnaby - Douglas et Wentworth - Burlington . 
leurs contributions ont été très utiles pour la Présidence dans le cadre de le examen de cette affaire . 
nous avons entendu durant presque deux heures de les arguments sur ce rappel à le Règlement et , bien que je ne me propose pas de passer en revue ces arguments dans les détails , je demande à la Chambre de faire preuve de patience à mon égard pendant que je expliquerai les faits de le affaire dont nous sommes saisis et de les conclusions que je en ai tiré . 
le projet de loi S - 13 constitue la Fondation canadienne de lutte contre le tabagisme chez les jeunes , personne morale sans but lucratif dont la mission est de réduire le usage de les produits de le tabac chez les jeunes à le Canada . 
À cette fin , le projet de loi S - 13 propose que un prélèvement soit imposé à les fabricants de les produits de tabac pour fournir à la fondation les fonds nécessaires à la réalisation de sa mission . 
le projet de loi S - 13 de initiative parlementaire vient de le autre endroit , où il a été adopté le 10 juin 1998 , et il a franchi le étape de la première lecture à la Chambre de les communes le 18 novembre 1998 . 
dans son rappel à le Règlement , le honorable leader de le gouvernement à la Chambre allègue , pour ainsi dire , que le projet de loi S - 13 propose une mesure fiscale et , à ce titre , le projet de loi ne aurait dû émaner de la Chambre de les communes que après adoption de une motion de voies et moyens . 
pour ces raisons , il soutient que la Chambre ne a pas été saisie convenablement de le projet de loi et demande à la Présidence de statuer que la Chambre de les communes ne peut étudier le projet de loi . 
avant de disposer de ce rappel quant à le fond , je voudrais répondre à le assertion faite par la députée deSt. Paul' s . 
elle soutient que , en ne se en tenant pas à le libellé de le projet de loi et en mettant en question les dispositions précises de ce dernier , on déborde de le cadre de la procédure et on tombe dans le domaine juridique , dont le Président ne devrait pas se occuper . 
la députée fait valoir que la question de savoir si le projet de loi S - 13 impose une taxe est une question de droit et de interprétation juridique et que , partant , elle ne relève pas normalement de la compétence de le Président . 
les exemples cités dans Beauchesne portaient sur de les questions qui ne pouvaient être considérées que comme de les questions juridiques et qui ne comportaient pas de aspect de procédure . 
dans les deux cas , la question était de savoir si la proposition législative devant la Chambre relevait de les pouvoirs législatifs de la Chambre en conformité avec la Loi constitutionnelle de 1867 . 
la question que je dois examiner relativement à le projet de loi S - 13 , à savoir si la redevance imposée par le projet de loi constitue ou non une taxe , se rapporte à les règles de procédure et à les usages de la Chambre ainsi que à le privilège traditionnel de la Chambre à le égard de les mesures fiscales . 
deuxièmement , est - ce que ce projet de loi aurait dû émaner de la Chambre et non de le autre endroit ? 
ces deux questions dépendent toutefois de la réponse qui doit être donnée à une troisième question : est - ce que le projet de loi S - 13 impose une taxe ? 
dans le affirmative , une motion de les voies et moyens se impose et le projet de loi aurait dû émaner de la Chambre . 
il faut nécessairement répondre à cette troisième question pour pouvoir trancher les questions de procédure et de privilège . 
dans sa présentation , le honorable leader de le gouvernement à la Chambre a fait valoir que le projet de loi S - 13 aurait dû émaner de la Chambre de les communes puisque il impose une taxe . 
le article 53 de la Loi constitutionnelle de 1867 est libellé comme suit , et je cite : 
tout bill ayant pour but le appropriation de une portion quelconque de le revenu public , ou la création de taxes ou de impôts , devra originer dans la Chambre de les Communes . 
par ailleurs , comme le dit la citation 980 , à la page 274 de Beauchesne , 6 e édition , les projets de loi qui imposent une taxe doivent être précédés de le adoption de une motion de voies et moyens . 
afin de sauvegarder les privilèges financiers de la Chambre de les communes , chaque député de cette Chambre doit faire preuve de vigilance et se assurer que chaque projet de loi présenté devant la Chambre respecte ce critère . 
le article 80 de le Règlement est catégorique à ce sujet et se lit , en partie , comme suit : 
pour résumer , la Chambre de les communes fait valoir sa prééminence sur les questions financières - à savoir les dépenses publiques et la taxation - et la législation se y rapportant doit émaner de la Chambre . 
afin de savoir si le projet de loi S - 13 a été présenté régulièrement à la Chambre , la présidence doit déterminer se il impose une taxe . 
si tel est le cas , le projet de loi aurait dû émaner de la Chambre de les communes et être précédé de une motion de les voies et moyens . 
les députés comprendront que cette affaire comporte de les questions de nature technique complexes . 
ce est pourquoi la présidence a apporté un soin particulier à le étude de les autorités pertinentes sur le sujet . 
je ai consulté de façon approfondie les ouvrages de Erskine May et je suis de avis que ils constituent une source de information complète et fiable sur les questions de procédure financière . 
je demande à la Chambre de être patiente pendant que je vais faire le exposé de le problème qui nous occupe . 
comme les députés le savent , la procédure financière concerne principalement le autorisation de les dépenses publiques et le imposition . 
on a fait valoir que la redevance proposée par le projet de loi S - 13 ne était pas une taxe parce que les fonds perçus ne seront pas versés à le Trésor . 
sous la rubrique « Question nécessitant une autorisation par voie de résolution de les voies et moyens » , May écrit , à la page 777 de la 22e édition : 
même si les impositions ne sont pas généralement de les redevances prélevées sur les contribuables [ ce est à dire de les taxes ] à moins que le produit ne soit versé à le Trésor , le absence de obligation de versement à le Trésor ne est pas en soi une indication concluante que il ne se agit pas de un prélèvement sur les contribuables . 
si , par exemple , le argent perçu par imposition par voie législative ne doit pas être versé à le Trésor , mais doit néanmoins être utilisé à le profit de le public en général ou à de les fins qui auraient nécessité par ailleurs un financement de le Trésor , il est probablement nécessaire que cette imposition soit autorisée par une résolution de voies et moyens . 
autrement dit , si une redevance permet de recueillir de les fonds qui sont versés à le Trésor , elle constitue une taxe . 
même si les fonds sont dirigés ailleurs , la redevance peut tout de même être une taxe . 
toutefois , une redevance ne peut être considérée comme une taxe , et donc échapper à les contraintes habituelles de la procédure financière , que si elle est prélevée à les fins de une industrie . 
par conséquent , le rappel à le Règlement , ainsi que je le vois , dépend de la nature de la redevance prévue dans le projet de loi S - 13 et de son objet . 
un examen plus poussé de le projet de loi se impose donc . 
il a été soutenu que le projet de loi S - 13 imposait un prélèvement « aux fins de une industrie » . 
À le chapitre 32 , consacré à les voies et moyens et à les projets de loi financiers , dans la 22e édition de May , nous pouvons lire ce qui suit , à la page 779 : 
les prélèvements sur les employeurs dans une industrie en particulier afin de constituer un fonds destiné à financer de les activités dont bénéficiera le industrie ne sont pas normalement considérés comme de les redevances [ ce est - à - dire de les taxes ] . 
may ajoute : 
la législation moderne , toutefois , prévoit fréquemment le imposition de autres types de droits ou de paiements qui , bien que ils ne constituent pas une taxe dans le sens propre de le terme , comportent suffisamment de caractéristiques de la taxation pour que ils doivent être traités comme de les « redevances prélevées sur les contribuables » et donc être autorisés par résolution de les voies et moyens proposée par un ministre . 
cette distinction entre les types de paiements qui sont ou non visés par les règles de la procédure financière ne est pas toujours claire en pratique . 
en de autres termes , la question centrale en le espèce est de savoir si le prélèvement imposé par le projet de loi S - 13 est une redevance imposée principalement dans le intérêt de le industrie de le tabac . 
si tel est le cas , la redevance ne constituerait pas une taxe . 
encore ici , May est utile , car il décrit un cas qui présente de les similitudes avec le projet de loi S - 13 , soit le projet de loi britannique intitulé « Merchant Shipping Bill » de 1973 - 1974 . 
ce projet de loi obligeait les importateurs de pétrole à contribuer à un fonds international à de les fins de indemnisation pour les dommages imputables à la pollution par le pétrole . 
dans la 21e édition , à la page 731 , May écrit ceci : 
il était si évident que cette imposition ne était pas à le profit de le industrie concernée que il a été décidé que elle constituait une taxe malgré le fait que son produit ne était pas versé à le Trésor . 
en dernière analyse , il a été décidé que le projet de loi en question était régi par les règles de la procédure financière et que , par conséquent , il devait être précédé de une résolution de voies et moyens avant de être étudié par la Chambre de les communes de le Royaume -Uni. 
dans le cas qui nous occupe , je ai examiné la question de savoir si la Chambre a déjà eu affaire à un projet de loi prévoyant un prélèvement sur une industrie . 
À le cours de la deuxième session de la 35e législature , le projet de loi C - 32 intitulé , Loi modifiant la Loi sur le droit de auteur , prévoyait le imposition de une redevance sur les bandes sonores vierges . 
cette industrie bénéficiait de cette redevance puisque cette dernière permettait la reproduction de éléments audio visés par le droit de auteur à de les fins personnelles . 
cela aurait pour effet de faire croître le marché pour les bandes sonores vierges . 
la redevance sur les bandes sonores était destinée à percevoir de les fonds permettant de indemniser les propriétaires de droits de auteur pour les pertes subies de le fait de la reproduction de ces éléments à de les fins personnelles . 
le lien entre le avantage pour le industrie et le imposition de la redevance semble clair dans ce cas . 
la redevance semble satisfaire le critère selon lequel elle constituait un avantage pour le industrie et ne constituerait donc pas une taxe . 
le projet de loi C - 32 ne a pas été subordonné à les procédures financières habituelles et ne a pas été précédé de une motion de voies et moyens . 
dans le cas de le projet de loi S - 13 , la présidence doit déterminer la nature de la redevance imposée par celui - ci . 
on a fait valoir que la redevance est un prélèvement dont le industrie de le tabac bénéficie . 
pour étayer ce point de vue , on nous renvoie à le article 3 de le projet de loi à le intertitre « Objet » , dont le paragraphe ( 1 ) est formulé en partie comme suit : 
( 1 ) La présente loi a pour objet de donner à le industrie canadienne de le tabac les moyens de réaliser le objectif que elle se impose publiquement de réduire le usage de le tabac chez les jeunes à le Canada ... 
je mettrai de côté sans faire de commentaire la question de savoir si le industrie a affirmé publiquement que elle avait pour objectif la réduction de le nombre de fumeurs dans un segment quelconque de la population . 
permettez - moi seulement de continuer la citation de le paragraphe ( 1 ) de le article 3 , qui précise comme suit le objet de le projet de loi pour réduire la consommation de les produits de le tabac chez les jeunes à le Canada : 
... du fait que : 
a ) de nombreuses maladies débilitantes ou mortelles sont liées à le usage de le tabac , qui peut entraîner de les conséquences néfastes ; 
b ) les jeunes à le Canada souffrent de dépendance envers les produits de le tabac que ils consomment ; ... 
d ) ces produits sont fabriqués et commercialisés , dans la mesure où les jeunes y ont accès ; ... 
le texte continue à le paragraphe ( 2 ) de le même article , et se lit comme suit : 
( 2 ) La présente loi vise à compléter la réponse législative générale ... à le grave problème de santé publique . 
il me semble que ces énoncés montrent que le objet de le projet de loi concerne une question de ordre public , soit la santé de les jeunes à le Canada et non , comme plusieurs députés le ont soutenu , la création de un avantage pour le industrie de le tabac . 
il y a ceux qui disent que le un ne empêche pas le autre et que le avantage pour le industrie est tel que la redevance en question est un prélèvement et non une taxe . 
les partisans de ce point de vue citent le alinéa ( 1 ) c ) de cet article 3 , qui se lit comme suit : 
c ) le industrie est incapable de régler elle - même ce problème de le usage de le tabac chez les jeunes parce que , de son propre aveu , ses membres et ses mandataires manquent de crédibilité pour promouvoir la diminution de le usage de les produits de le tabac ; 
sûrement , le manque de crédibilité dont il est fait mention ici fait appel à le bon sens qui nous dicte que le industrie de le tabac sert ses propres intérêts , à savoir que , en tant que entreprise commerciale , son but premier est de faire croître ses marchés et ainsi de augmenter ses bénéfices . 
les jeunes représenteraient pour cette industrie la croissance éventuelle de son marché . 
comment pourrait - il être avantageux pour le industrie de réduire le nombre de fumeurs chez ceux - là même qui représentent la croissance de son marché ? 
ce est cette proposition peu plausible qui sous - tend le problème de crédibilité dont le projet de loi fait mention . 
les partisans de le projet de loi font valoir que le avantage que procure le établissement de la fondation sur le plan de les relations publiques constituerait un avantage pour le industrie . 
ils citent le autonomie de la fondation projetée et son rôle dans la coordination , à le échelle nationale , de la lutte à le tabagisme . 
ne est - il pas raisonnable de supposer que , si le industrie avait voulu améliorer son image publique de cette façon , elle aurait pu elle - même créer un organisme sans lien de dépendance comme la fondation ? 
pourquoi une mesure législative comme celle - ci est - elle nécessaire ? 
revenons à le article 3 , pour examiner cette fois le alinéa ( 1 ) e ) , qui se lit comme suit : 
e ) il est prévisible que de nouvelles restrictions seront apportées à la capacité de le industrie de produire et vendre de les produits de le tabac si le taux de usage de le tabac chez les jeunes ne est pas réduit ; 
on a fait valoir que cette disposition procure encore un avantage pour le industrie puisque les activités de la fondation peuvent prévenir le imposition de autres restrictions à le industrie . 
on anticipe de ce fait les mesures gouvernementales qui pourraient être prises dans le avenir et on conclut que le établissement de la fondation permettra de éviter la nécessité de adopter de telles mesures . 
cela ne revient - il pas à dire que cette redevance dans ce projet de loi est avantageuse pour le industrie de le seul fait que de les mesures futures pourraient être moins intéressantes ? 
je ai examiné avec attention tous les arguments présentés ainsi que tous les cas que les députés ont portés à mon attention , même si , dans la présente décision , je ne ai pas parlé de chacun de façon détaillée . 
je suis forcé de conclure que la redevance imposée par le projet de loi S - 13 ne vise pas à créer un avantage pour le industrie de le tabac mais vise une question de ordre public , à savoir , la santé de les jeunes à le Canada , un but louable sans aucun doute . 
on cherche par le projet de loi à établir la Fondation canadienne de lutte contre le tabagisme chez les jeunes dont le objectif est de réduire et finalement de éliminer le usage de les produits de le tabac par les jeunes à le Canada . 
cela frise la crédulité que de vouloir prétendre que cet objectif constitue un avantage pour le industrie de le tabac . 
quelle que soit le ingéniosité avec laquelle on se est employé à rédiger et à structurer le projet de loi de sorte que il ressemble à un projet de loi dont le objet vise une industrie , objet qui améliorerait peut - être la place que occupe le industrie de le tabac dans notre société , le projet de loi S - 13 a pour objet principal la réduction et le élimination de la consommation de le tabac . 
ceci constitue une question de santé publique et ce est en raison de cet objet de ordre public que je en suis arrivé à la conclusion que la redevance que impose le projet de loi S - 13 sur le industrie constitue une taxe . 
seule la Chambre de les communes a le droit et le obligation de légiférer sur de les mesures financières . 
seule la Chambre de les communes , agissant sur le initiative de les ministres de la Couronne , peut imposer de les taxes pour générer les fonds nécessaires à le financement de programmes de ordre public . 
je ai le obligation , en tant que Président de la Chambre , de faire en sorte que ces privilèges fondamentaux en matière financière ne soient pas compromis . 
en bref , les projets de loi imposant une taxe doivent émaner de la Chambre de les communes et doivent être précédés de une motion de voies et moyens . 
en conséquence , le étape de la première lecture est nulle et non avenue et cette affaire est rayée de le Feuilleton . 
je remercie les députés de leur attention . 
