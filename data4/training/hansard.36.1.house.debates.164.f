le gouvernement prétend que cette agence pourra fonctionner plus efficacement , que elle pourra se montrer plus souple dans ses politiques concernant les ressources humaines et que elle sera moins liée par les restrictions de le Conseil de le Trésor et les autres lignes directrices légales qui ont limité sa capacité de recruter et de garder à son emploi de les professionnels hautement spécialisés , notamment dans la vérification fiscale et dans la technologie de le information . 
le gouvernement dit aussi que cette nouvelle agence serait structurée de façon à collaborer avec les provinces sur une base contractuelle pour percevoir en leur nom les impôts provinciaux à la source tels que le impôt provincial sur les sociétés et la taxe de vente provinciale . 
