le ENVIRONNEMENT 
Lehon. Charles Caccia ( Davenport , Lib . ) : 
monsieur le Président , vous vous rappellerez peut - être que , le 5 novembre , la ministre de le Environnement a dit à la Chambre que le protocole , le accord sur la réduction et le élimination de le mercure et de autres métaux lourds , signé à Aarhus , à le Danemark , plus tôt cette année , sera ratifié par le Canada de ici la fin de le année . 
À la différence de le fer et de le calcium , le mercure ne a aucune fonction chez le homme . 
ce est un métal lourd persistant et une substance hautement toxique qui se retrouve dans la chaîne alimentaire . 
il importe de signaler que , même si le mercure se retrouve dans la nature - dans le eau , le air et le sol - les activités humaines en accentuent la présence dans le environnement à le point que la santé de le homme et de les écosystèmes en est menacée . 
ce est pourquoi , dans six provinces et dans les territoires , de les avertissements ont été lancés pour informer la population de les dangers que présente la consommation de poisson contaminé à le mercure . 
de plus , il a été constaté que certaines espèces animales qui ont une forte teneur de mercure dans le sang ont de les problèmes de reproduction . 
selon une étude récente de Environnement Canada , les huards ont de le mal à nidifier et à élever leurs petits à cause de cette haute teneur en mercure . 
les chercheurs ont également observé , chez les huards , que les niveaux de mercure augmentaient de ouest en est , probablement en raison de les activités industrielles qui sont concentrées dans le est de le Amérique de le Nord . 
de toute évidence , le activité industrielle partout en Amérique de le Nord fait augmenter les niveaux de mercure dans le environnement et dans la faune . 
de récents échantillons prélevés , en Arctique , auprès de phoques annelés et de bélugas , nous ont permis de constater de les concentrations et de les taux de accumulation de le mercure plus élevés que dans les échantillons prélevés à le cours de les années 70 et 80 . 
le charbon qui alimente les centrales thermiques est une grande source de pollution à le mercure . 
Ontario Hydro , à la suite de la fermeture de certains réacteurs nucléaires , a le intention de satisfaire à les besoins à court terme en exploitant de les centrales thermiques alimentées à le charbon . 
plus on brûle de charbon , plus on libère de le mercure dans le atmosphère . 
en Ontario , entre 2,7 tonnes et 3,4 tonnes de mercure sont libérées dans le atmosphère chaque année , à cause de les activités humaines . 
jusque à 2,5 tonnes sont libérées chaque année dans les eaux de les Grands Lacs . 
le Inventaire national de les rejets de polluants nous est peu utile pour déterminer le volume de mercure que chaque entreprise ou activité libère , puisque il ne livre de les informations que si le usine ou le activité libère 10 tonnes de mercure ou plus . 
de toute évidence , nous devons applaudir à la ratification de le protocole , mais une réduction de 50p. 100 ou même de 64p. 100 est loin de être suffisante . 
À le instar de le Danemark , où les émissions de mercure sont interdites ou sur le point de le être , nous devons protéger la population en interdisant complètement les émissions de mercure provenant de le activité humaine . 
par conséquent , je demande à la ministre si le gouvernement a le intention de interdire les émissions de mercure ou de adopter un règlement visant à prévenir complètement la pollution par le mercure . 
Mme Paddy Torsney ( secrétaire parlementaire de la ministre de le Environnement , Lib . ) : 
alors que 16 pays doivent ratifier le protocole sur les métaux lourds pour que celui - ci prenne effet , le gouvernement ne reste pas à rien faire dans le attente de le soutien de les autres pays . 
si le Canada peut remplir la plupart de ses obligations , ou le a déjà fait , le un de les principaux gains de le protocole de les Nations Unies sur les métaux lourds , ce est que de les pays membres de la Commission économique de le ONU pour le Europe se sont engagés à réduire leurs émissions de mercure et de autres polluants atmosphériques qui , nous le savons , sont transportés par les courants aériens de divers pays vers les régions de le Arctique et de le est de le Canada . 
le Canada a déjà élaboré de les programmes et de les initiatives extrêmement efficaces , qui ont grandement réduit la pollution atmosphérique à le mercure à le Canada . 
les mesures de réglementation de les émissions de mercure imposées conformément à la Loi canadienne sur la protection de le environnement et à la Loi sur les pêches ont convaincu le industrie de le chlore et de la soude caustique et les fabricants de piles à le mercure de modifier leur technologie et de adopter un processus sans mercure . 
il y avait 16 de ces entreprises à le Canada , mais il ne en reste plus que une et elle respecte constamment les conditions antiémission strictes prévues dans les deux lois . 
de concert avec le programme de réduction accélérée pour la suppression de les substances toxiques de Environnement Canada , le secteur minier a fait de les progrès considérables dans la réduction de les émissions de mercure . 
de nouvelles technologies mises à le point par de les sociétés canadiennes ont permis de réduire de 18 tonnes les émissions de mercure dans le atmosphère . 
les hauts fonctionnaires de Environnement Canada estiment que nos plus récentes données montrent que nos émissions de mercure ont été réduites de 64p. 100 et font état de réductions similaires de les émissions de plomb et de cadmium . 
le Canada ne en restera pas là . 
notre objectif est de annoncer de autres réductions nationales substantielles de ici à le an 2000 . 
en tant que partenaire à part entière de le Accord nord - américain de coopération dans le domaine de le environnement , le Canada dirige la mise à le point de un plan de action nord - américain pour le mercure qui devrait être soumis à les ministres de ici à juin 1999 . Les citoyens canadiens , américains et mexicains auront le occasion de dire leur mot sur cette importante initiative avant que le plan ne soit adopté par les trois pays . 
À le nombre de les autres importants programmes de réduction de les émissions de mercure , il y a le plan de action de les gouverneurs de la Nouvelle - Angleterre et de les premiers ministres de le est de le Canada et on est à mettre à le point un programme de réduction de les émissions de mercure conformément à le accord binational de réduction de les substances toxiques dans les Grands lacs ... 
