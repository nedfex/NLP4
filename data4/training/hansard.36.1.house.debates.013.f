je ai abordé le projet de loi C - 2 de le point de vue suivant . 
je me suis de abord demandé se il y avait un problème . 
et je ai répondu oui , il y a un problème . 
ce problème ne est pas nouveau . 
il existe depuis un bon bout de temps . 
nous pourrions accuser le gouvernement conservateur de ne avoir rien fait pour remédier à le problème . 
mais cela ne donnerait rien , même si ce est vrai . 
le gouvernement actuel a reconnu que il y avait un problème . 
que a il fait ? 
deux choses . 
il a commencé par consulter les Canadiens pour que ils soient conscients de le problème . 
la population de Thornhill est très consciente de les problèmes de le Régime de pensions de le Canada . 
elle sait que à défaut de être modifié , le régime disparaîtra . 
il ne existera pas pour les générations à venir . 
il est important que le on informe les gens . 
le gouvernement ne a pas seulement consulté largement la population , il a également discuté avec les provinces . 
alors que pour le commun de les mortels le RPC est un régime fédéral , la réalité est que il est plutôt un régime fédéral - provincial . 
il ne peut être modifié que si une majorité de provinces approuvent les changements et reconnaissent que une importante majorité de la population de le pays les approuve . 
autrement dit , le gouvernement ne peut apporter ces modifications de façon unilatérale . 
À les députés et à les électeurs de Thornhill qui me demandent si ce est possible pour les gouvernements de travailler ensemble , je réponds que le projet de loi C - 2 est un exemple de collaboration entre les ordres de gouvernement . 
Était - ce une décision unanime ? 
absolument pas . 
le gouvernement a il cherché à obtenir un consensus national ? 
je crois que oui . 
la majorité de les provinces , y compris le Ontario , représentant la vaste majorité de la population de le Canada , ont signé une entente pour apporter à le Régime de pensions de le Canada de les changements qui régleront le problème . 
