trente - SIXIÈME LÉGISLATURE OUVERTURE DE LA PREMIÈRE SESSION 
le Parlement ayant été convoqué par proclamation pour le expédition de les affaires aujourd' hui , 
la séance est ouverte à 10 h 30 . 
communication DE LA RÉSIDENCE DU GOUVERNEUR GÉNÉRAL 
son Honneur le Président : 
honorables sénateurs , je ai le honneur de vous informer que je ai reçu la lettre suivante de la résidence de le Gouverneur général : 
le 22 septembre 1997 
monsieur le Président , 
je suis chargé de vous faire part que le très honorable Antonio Lamer , Juge en chef de la Cour suprême de le Canada , en sa qualité de Gouverneur général suppléant , se rendra à la salle de le Sénat aujourd' hui , le lundi 22 septembre 1997 , à 11 heures pour ouvrir la première session de la trente - sixième législature de le Canada . 
veuillez agréer , 
la secrétaire de le Gouverneur général 
le Président de le Sénat 
son Honneur le Président : 
honorables sénateurs , conformément à le paragraphe 135 ( 4 ) de le Règlement , la séance est suspendue jusque à 10 h 55 . 
( le Sénat se ajourne à loisir . ) 
À 10 h 55 , le Sénat reprend ses travaux puis les ajourne en attendant le arrivée de le honorable suppléant de Son Excellence le Gouverneur général . 
le très honorable Antonio Lamer , suppléant deSon Excellence le Gouverneur général , étant arrivé à le Sénat et ayant pris place sur le trône , 
son Honneur le Président ordonne à le gentilhomme huissier de la verge noire de aller informer la Chambre de les communes que ce est le désir de le honorable suppléant de Son Excellence le Gouverneur général que elle se rende immédiatement auprès de lui dans la salle de le Sénat . 
la Chambre de les communes étant arrivée , 
son Honneur le Président dit : 
honorables membres de le Sénat , 
membres de la Chambre de les communes , 
je suis chargé de vous informer que Son Excellence le Gouverneur général ne juge pas à propos de faire connaître , avant que la Chambre de les communes ne ait choisi son président conformément à la loi , les motifs qui le ont porté à convoquer le Parlement , mais que il les exposera demain , le mardi 23 septembre 1997 , à 14 h 30 . 
la Chambre de les communes se retire . 
il plaît à le honorable suppléant de Son Excellence le Gouverneur général de se retirer . 
le Sénat reprend sa séance . 
communication DE LA RÉSIDENCE DU GOUVERNEUR GÉNÉRAL 
son Honneur le Président : 
honorables sénateurs , je ai le honneur de informer le Sénat que je ai reçu de la résidence de le Gouverneur général la communication suivante : 
le 22 septembre 1997 
monsieur le Président , 
je ai le honneur de vous informer que Leurs Excellences , le Gouverneur général et Madame Diana Fowler LeBlanc , arriveront à le entrée de la Tour de la Paix à 14 h 30 , le mardi 23 septembre 1997 . 
lorsque on aura avisé Leurs Excellences que tout est en place , elles se rendront à la Chambre de le Sénat pour ouvrir officiellement la première session de la trente - sixième législature de le Canada . 
veuillez agréer , 
la secrétaire de le Gouverneur général Judith A . LaRocque 
