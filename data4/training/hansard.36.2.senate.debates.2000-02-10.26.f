Wiarton - Le influence de Wiarton Willie sur la communauté 
le honorable Lorna Milne : 
honorables sénateurs , je ai entendu avec grand plaisir prononcer le nom de « Wiarton Willie » de le autre côté de la Chambre , hier . 
il semble que Mike Harris ait lu le hansard de le Sénat et que il se en prenne lui aussi à Wiarton Willie , tout en donnant à le passage une taloche à le Sénat . 
permettez - moi de vous parler de Wiarton , en Ontario . 
cette magnifique localité de 2 300 habitants est située à le extrémité de la péninsule Bruce en Ontario , sur les rives de la baie Georgienne et à proximité de le Escarpement de le Niagara . 
on y trouve un microclimat unique puisque , à la différence de les communautés environnantes dont le climat est tempéré par les eaux de les Grands Lacs , Wiarton connaît un climat extrêmement froid en hiver . 
la plus grande partie de les emplois étant saisonniers , lorsque le service de traversier de Chi - Cheemaun ferme pour le hiver , de nombreux emplois disparaissent aussi . 
cette situation et les grands froids forcent de nombreuses personnes à quitter la ville chaque hiver pour aller chercher de le emploi ailleurs . 
que signifie pour la ville le aide que a apportée DRHC à « Wiarton Willie » , cette gentille petite marmotte albinos ? 
en plus de aider à égayer les esprits à le milieu de le hiver , une subvention de 50 000 $ par année permet de donner de le emploi à deux jeunes gens . 
le site Web que ces jeunes ont mis sur pied reçoit de les dizaines de milliers de visites chaque année . 
de les millions de dollars sont versés à le économie locale , ce qui permet de créer de les emplois . 
cette année , 10 000 personnes ont visité Wiarton le 2 février pour voir Willie prédire six semaines supplémentaires de climat canadien . 
le défilé de mode de Wiarton Willie a permis de amasser 860 $ qui ont servi à le achat de un numériseur pour le hôpital de Wiarton . 
les magasins locaux ont rapporté que leurs ventes avaient atteint un niveau inégalé à le cours de cette fin de semaine de le 2 à le 6 février . 
il semble donc que chaque dollar de la subvention de DRHC a été utilisé à bon escient pour aider une ville dont la main - d' 9uvre est principalement saisonnière à maintenir sa population et que il a permis à à le moins deux jeunes de le endroit de se assurer un bel avenir . 
la subvention de DRHC accordée à Wiarton Willie à titre de capital de lancement a remporté un franc succès . 
