le développement de les ressources humaines 
le programme de prêts de études - La proposition de hausser les primes versées à les banques 
le honorable Erminie J . Cohen : 
honorables sénateurs , je me interroge sur la proposition de le gouvernement de hausser les primes versées à les banques dans le cadre de le Programme national de prêts à les étudiants . 
on a dit que cette proposition aurait pour effet de accroître de 100 millions de dollars la contribution de le gouvernement fédéral . 
cette mesure ne fait que traiter un symptôme de un problème beaucoup plus grand . 
je ne comprends pas pourquoi le gouvernement est si prompt à répondre à les préoccupations de nos riches banquiers alors que il fait si peu pour remédier à le incapacité de les étudiants de rembourser leurs emprunts . 
depuis 1993 , le gouvernement a amputé de 6 milliards de dollars les fonds destinés à le éducation . 
malgré la modeste augmentation prévue à cet égard dans le budget de le année dernière , les universités et les collèges ont toujours de le mal à répondre à les besoins de leurs étudiants en essayant de faire plus avec moins de ressources . 
pour compenser les moins - perçus en revenus , les frais de scolarité ont grimpé en flèche et les étudiants sont forcés de emprunter encore davantage . 
les étudiants canadiens , après avoir complété un cycle de études de quatre ans , figurent parmi les plus endettés à le monde avec une dette moyenne de 25 000 $ . 
il est clair , honorables sénateurs , que ce ne est pas un bon départ pour nos jeunes qui entrent dans la vie active . 
cet endettement inévitable dissuade les étudiants , surtout ceux de milieux à faible revenu , de entreprendre de les études . 
vous en conviendrez sûrement , honorables sénateurs , ce montant de 100 millions de dollars serait dépensé à bien meilleur escient se il servait à accroître les programmes de bourses et à augmenter les paiements de transfert à les provinces . 
cela permettrait de accroître le accessibilité à les études et de faire en sorte que les étudiants ne accumulent pas de les dettes aussi élevées et puissent entreprendre leur carrière en partant de le bon pied . 
uniquement à le cours de le mois dernier , le gouvernement a pris plusieurs décisions contestables en ce qui concerne le argent de les contribuables . 
en parcourant le pays avec le groupe de travail de le Parti progressiste - conservateur sur la pauvreté , je suis devenue plus consciente de le problème et je ne saurais trop insister sur la nécessité de décisions de principe capables de briser les cycles contribuant à la pauvreté . 
il est encore temps pour le gouvernement de repenser cette proposition et de investir le argent là où cela se impose , ce est - à - dire pour former nos jeunes et les préparer à relever les défis que ils auront à affronter dans le siècle qui commence . 
