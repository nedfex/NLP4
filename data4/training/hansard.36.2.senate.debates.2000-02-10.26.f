un processus de enquête auprès de le patient devrait être prévu dans le projet de loi afin de se assurer que sa volonté de cesser les traitements est réellement libre et éclairée , et que elle ne résulte pas plutôt de une dépression passagère . 
honorables sénateurs , toujours à le article 3 , paragraphe 2 b ) , il est mentionné que la volonté de le patient , quant à le interruption de les traitements de survie , pourrait se faire , entre autres , par signes , et en présence de à le moins un témoin qui ne est pas un soignant . 
cette façon de faire risque de créer , à mon avis , plusieurs problèmes de interprétation . 
en effet , il pourrait être hasardeux de interpréter les signes de certains patients . 
un risque de erreur pourrait causer la mort de patients qui ne sont pas prêts à mourir . 
cette situation pourrait , par la suite , inciter de les patients ayant perdu le usage de la parole à ne pas exprimer leurs besoins de peur que leur geste soit mal interprété . 
je crois aussi que le alinéa c ) de le même article devra être clarifié lorsque il sera étudié en comité . 
on y énonce que le conjoint , le compagnon ou le parent qui est le plus intimement lié à le patient pourrait demander le interruption de soins de survie si celui - ci ne a plus la capacité de formuler une telle demande et que il ne y a pas de représentant légal désigné pour prendre en son nom les décisions en matière de soins de santé . 
la situation risque de être particulièrement problématique si les enfants de une même famille ont de les opinions divergentes sur les soins qui doivent être dispensés à leurs parents . 
cette situation a été vécue par une famille de amis . 
le un de les enfants voulait que les traitements de survie soient interrompus et le autre voulait que ces traitements soient maintenus . 
les deux enfants étaient très près de leur mère et je ai été obligée de prendre la décision finale . 
ce fut une situation très difficile . 
enfin , je espère que , à le comité , on étudiera de près la définition de « traitement de survie » . 
je ne suis pas de accord pour que cette définition dise que le hydratation et le alimentation artificielles sont de les traitements de survie . 
selon moi , le alimentation et le hydratation sont de les besoins élémentaires , et non de les traitements . 
nous ne avons pas le droit de laisser une personne mourir de faim . 
considérer le hydratation et le alimentation artificielles comme de les traitements médicaux est peut - être une étape vers la légalisation de le euthanasie et de le suicide assisté . 
si cette définition était adoptée dans un texte de loi , elle permettrait à les services de santé de faire indirectement ce que ils ne peuvent faire directement . 
il ne faut pas ouvrir la porte à ces pratiques . 
on pourrait me répondre que certaines de mes préoccupations seront traitées lors de le établissement de lignes directrices nationales , comme le suggère le alinéa a ) de le article 6 . Je crois toutefois que il serait préférable que nous adoptions un projet de loi complet afin de laisser le moins de trous noirs et ainsi , éviter tout risque de abus . 
voici donc quelles sont mes inquiétudes quant à le projetde loi S - 2 . 
je ai soulevé plusieurs questions et je espère que mes réflexions seront utiles à le comité qui sera chargé de étudier ce projet de loi . 
le projet de loi S - 2 , qui devra faire preuve de compassion à le égard de les gens qui souffrent , ne devra pas ouvrir la porte à une législation encourageant le euthanasie et le aide à le suicide . 
je ose espèrer que cet équilibre sera atteint . 
