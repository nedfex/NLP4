la LOI SUR LA MONNAIE ROYALE CANADIENNE 
- madame la Présidente , il y a exactement dix ans , la loi régissant la Monnaie royale a été modifiée pour permettre à cet organisme de devenir une société de État entièrement commerciale . 
il est maintenant temps de revoir la loi pour la mettre à jour en fonction de les réalités de le marché et de donner à la Monnaie les pouvoirs voulus pour relever les défis de le avenir . 
le mandat de la Monnaie consiste à fournir à les Canadiens de les pièces de monnaie de haute qualité à un coût abordable tout en étant rentable . 
la Monnaie fabrique et vend également de les pièces de collection de haute qualité ainsi que de les pièces de monnaie pour de les pays étrangers . 
les Canadiens peuvent être fiers de la Monnaie qui , le an dernier , a produit un milliard de pièces de monnaies pour 16 pays différents . 
À la fin de cette année , le total se élèvera à le chiffre impressionnant de deux milliards de pièces . 
les affaires internationales , qui comptent , en moyenne , pour 70p. 100 de les revenus de la Monnaie , réduisent le coût global de les pièces de monnaie en circulation à le Canada . 
la Monnaie couvre ses frais et réalise même de les profits pour son actionnaire , le gouvernement de le Canada . 
le an dernier , ces profits se sont élevés à un remarquable montant de 4,1 millions de dollars . 
les députés reconnaîtront sans aucun doute que la Monnaie royale canadienne est une source de fierté nationale et fournit un service essentiel à le économie canadienne en produisant de belles pièces qui soulignent les fiers symboles de notre esprit national . 
selon sa vision stratégique , la Monnaie doit être un leader mondial dans la production de pièces de monnaie grâce à ses employés , à son sens de le innovation et à la qualité de ses produits . 
les modifications proposées à la Loi sur la Monnaie royale dont nous discutons aujourd' hui sont essentielles pour lui permettre de réaliser sa vision stratégique . 
abordons maintenant ces modifications un peu plus en détail . 
pour moderniser la loi , nous proposons de simplifier le processus de approbation de les pièces . 
cela permettrait à la Monnaie royale canadienne de répondre plus rapidement à la demande de ses marchés . 
les collectionneurs de pièces ou ceux qui veulent en donner en cadeau se attendent à pouvoir choisir parmi un large éventail de modèles , de dénominations et de métaux . 
pour pouvoir continuer à générer de les profits pour les Canadiens , la Monnaie a besoin de un cadre législatif qui lui permet de réagir rapidement et efficacement afin de profiter de les occasions qui surviennent dans le domaine de les pièces non destinées à la circulation . 
les amendements que nous proposons donneraient à la Monnaie les pouvoirs de établir et de modifier toutes les caractéristiques de les pièces à le exception de leur conception . 
ce est toujours le ministre responsable de la Monnaie qui disposerait de le pouvoir de déterminer leur conception . 
ceci donnerait à la Monnaie un processus de prise de décisions plus rationalisé , ce qui lui permettrait de demeurer à le écoute de le marché , et axé sur sa clientèle , tout en préservant la responsabilité à le niveau de le design . 
en effet , les décisions relatives à la conception de les pièces seraient toujours prises par un élu . 
comme je le ai déjà mentionné , le mandat de la Monnaie consiste à fournir à les Canadiens de les pièces de circulation rentables et de haute qualité quand ils en ont besoin . 
les amendements proposés rationaliseront le processus de approbation et , de ce fait , amélioreront la capacité de la Monnaie de remplir son mandat . 
je tiens à rassurer la Chambre que ce seront toujours les élus et le ministre responsable devant cette Chambre qui continueront à avoir le dernier mot à le sujet de les pièces que les Canadiens et Canadiennes utilisent à chaque fois que le occasion leur est donnée . 
par conséquent , je invite mes collègues à appuyer le projet de loi afin que il soit renvoyé en comité où nous pourrons le examiner article par article et entendre de les témoins . 
