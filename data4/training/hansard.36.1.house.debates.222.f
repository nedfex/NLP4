reconnaître le cheval de race canadienne pour les mauvaises raisons , ce est diminuer son importance comme symbole de notre histoire et de les valeurs qui sont chères tant à le Québec que à le Canada . 
le cheval de race canadienne a certes joué un rôle clé dans le histoire de le Québec , mais il a aussi joué un rôle crucial dans le édification de le ensemble de le Canada . 
il symbolise le développement de notre pays et notre diversité . 
il prouve que nous sommes forts , indépendants et à la hauteur de la tâche . 
il symbolise non seulement le héritage de le Québec , mais aussi le histoire de tout notre pays . 
