les symboles constituent une façon de communiquer notre patrimoine , notre histoire , nos valeurs et notre identité . 
les pays se sont toujours identifiés à le aide de drapeaux , de armoiries et de autres emblèmes . 
les Canadiens ne sont pas différents . 
nous éprouvons un profond respect pour nos symboles . 
nous traitons notre drapeau avec vénération et enthousiasme . 
nous respectons le castor et les armoiries de notre pays . 
