quand on demanda à le conducteur de le attelage ce qui se était passé , il dit que le cheval de race canadienne le avait tant fait travailler que il en était mort . 
un autre cheval lourd a également été attelé avec le même cheval de race canadienne , et il est mort le année suivante , pendant que le cheval de race canadienne a continué de travailler . 
il ne est pas étonnant que le auteur de l ' International Encyclopaedia of Horse Breeds dise que le cheval de race canadienne est le secret le mieux gardé de le XXe siècle . 
