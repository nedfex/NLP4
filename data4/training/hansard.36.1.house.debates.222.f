le cheval de race canadienne ne a jamais été considéré comme un pur - sang à le même titre que le quarter horse ou le painted thoroughbred , mais plutôt comme une race obscure et en décroissance ne constituant aucune menace pour les races établies ou le élevage à de les fins commerciales . 
le cheval de race canadienne est unique à le Canada . 
on célèbre son 350e anniversaire à le Canada cette année . 
surnommé le petit cheval de fer , ce cheval est le résultat de la sélection naturelle et de le élevage , bien adapté à le climat canadien particulier . 
avec seulement 2 200 chevaux de race canadienne en existence , cet animal est inscrit sur la liste de les espèces menacées et classé comme vulnérable . 
le premier cheval à galoper sur le sol canadien a été débarqué à Québec , le 25 juin 1647 . 
À le départ , les stocks étaient constitués de chevaux arabes , bretons et anglo - normands transportés de France à le Québec . 
avec le temps , ces chevaux se sont adaptés à le froid climat de le Canada , rapetissant et devenant extrêmement résistants . 
le cheval de race canadienne est généralement noir , mais il peut aussi aller de la couleur baie à le marron clair . 
un étalon pèse de 1 050 à 1 350 livres et la jument , de 1 000 à 1 250 livres . 
ils ont de 14 à 16 mains de hauteur . 
le corps est bien proportionné et les pattes sont particulièrement bien musclées . 
la crinière et la queue sont longues , abondantes et vaguées . 
le cheval de race canadienne est en règle générale très docile . 
il vit longtemps et demeure utile à un âge avancé . 
la jument est très fertile et se reproduit régulièrement jusque à le âge de 20 ans et plus . 
le cheval de race canadienne était beaucoup apprécié et utilisé par les premiers colons en raison de sa force , de sa volonté et de le peu de aliments que il consommait . 
le nombre de bêtes a connu une croissance rapide , de sorte que en 1850 on comptait environ 150 000 chevaux de race canadienne . 
toutefois , le importation de autres races et les exportations à les États - Unis pour les fins de diverses guerres ont fait que la race est devenue en danger de extinction . 
en 1976 , on comptait moins de 400 chevaux de race canadienne . 
dès 1886 , de les efforts avaient été consentis afin de accroître le nombre de chevaux de cette race . 
les efforts se sont poursuivis tout à le long de le siècle , de sorte que le on en recense maintenant plus de 2 000 . 
toutefois , comme il ne reste plus que huit souches de reproducteurs , les éleveurs de chevaux de race canadienne ne sont pas sûrs de le avenir de le petit cheval de fer . 
