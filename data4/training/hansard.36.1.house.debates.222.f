le ÉCOLE PUBLIQUE DE BRIDGEVIEW 
m . Roger Gallaway ( Sarnia - Lambton , Lib . ) : 
monsieur le Président , samedi dernier , le 1 er mai , les élèves et les professeurs , actuels et anciens , se sont réunis pour célébrer le 50e anniversaire de le école publique de Bridgeview , dans le village de Point Edward , dans ma circonscription . 
de les centaines de personnes venues de aussi loin que la Colombie - Britannique et la Floride se sont retrouvées pour renouer de les amitiés et évoquer leurs souvenirs de le temps passé . 
malgré la centralisation de le conseil scolaire et malgré les fermetures , le école de Bridgeview se est construite une identité à le sein de la communauté . 
après tous , les communautés reposent sur les institutions qui sont ouvertes à tous et le école de Bridgeview est sans nul doute une institution où parents et élèves , en fait tout le monde , sont bien accueillis . 
cela fait 50 ans que cette école accueille de les élèves et toutes ces années ont permis de renforcer la communauté de Point Edward grâce à les amitiés qui se sont formées à le école . 
il est clair que les écoles non seulement sont essentielles pour notre avenir , elles sont importantes pour le identité de la communauté . 
