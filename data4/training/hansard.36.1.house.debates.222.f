le MOIS NATIONAL DE Le ACTIVITÉ PHYSIQUE 
Mme Jean Augustine ( Etobicoke - Lakeshore , Lib . ) : 
monsieur le Président , mai est le Mois national de le activité physique . 
mai est un mois spécial , ce est le temps de les fleurs , de le beau temps et de la Fête de les mères . 
pourtant , certains membres de notre société semblent oubliés et ne être les enfants de personne . 
ce sont les jeunes exploités sexuellement , qui vivent invisibles dans le ombre . 
la voix de les enfants et de les jeunes victimes de exploitation sexuelle doit être entendue et de les stratégies conçues pour répondre à leurs besoins . 
un programme spécial , Out from the shadows and into the light ( Sortir de le ombre et aller vers la lumière ) me a été signalé aujourd' hui . 
ce programme spécial , qui repose sur une approche de développement communautaire , fait directement intervenir les jeunes eux - mêmes , qui racontent leur histoire et font de les recommandations . 
je encourage tous les députés , comme le disait le ministre de les Affaires étrangères , à accorder leur soutien à ce programme et à combattre le exploitation sexuelle de les enfants . 
