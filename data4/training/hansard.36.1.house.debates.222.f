le AÉROPORT DE PENTICTON 
m . Jim Hart ( Okanagan - Coquihalla , Réf . ) : 
monsieur le Président , je prends la parole à le nom de les habitants de la population de Okanagan - Coquihalla qui se préoccupent de le avenir de le aéroport de Penticton . 
les négociations concernant le transfert de le administration aéroportuaire à la ville de Penticton ont débuté en 1996 . 
trois ans plus tard , le processus de négociation est interrompu pour une huitième fois . 
le aéroport de Penticton a de les retombées économiques de plus de 12 millions de dollars par année dans la partie sud de le Okanagan . 
le aéroport gère plus de 44 000 vols par année . 
la perte de cet important chaînon de le infrastructure de les transports aurait de les répercussions économiques dévastatrices . 
afin de garder le aéroport ouvert , la population de la partie sud de le Okanagan est intervenue directement en signant une pétition dans laquelle elle demande à le gouvernement fédéral de nommer un médiateur pour ramener les parties à la table de négociation . 
la fermeture de le aéroport régional de Penticton serait de toute évidence imputable à le ministre libéral de les Transports , qui a mal géré le dossier dès le début . 
quand le gouvernement libéral va il enfin se décider à nommer un médiateur pour mettre à le point cette entente indispensable ? 
