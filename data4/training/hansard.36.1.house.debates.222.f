loi SUR LA RACE NATIONALE DE CHEVAUX DU CANADA 
- monsieur le Président , ce est pour moi un honneur que mon projet de loi de initiative parlementaire C - 454 fasse aujourd' hui le objet de un débat à la Chambre . 
je souhaite tout de abord reconnaître le travail que a fait le député de Lanark - Carleton en présentant un projet de loi similaire à le cours de la législature précédente . 
il a travaillé dur pour obtenir la reconnaissance de une partie importante de le histoire de notre pays . 
je vais donner aujourd' hui les raisons pour lesquelles je ai présenté ce projet de loi et expliquer pourquoi , à mon avis , le cheval de race canadienne doit être reconnu comme le cheval national de le Canada . 
le cheval de race canadienne fait partie intégrante de notre histoire et de notre patrimoine . 
les scientifiques pensent maintenant que le cheval est apparu en Amérique de le Nord il y a 50 millions de années . 
lorsque les premiers hommes sont arrivés en Amérique de le Nord , ils sont venus de Asie par une bande de terre qui a maintenant disparu . 
les chevaux sont venus de Asie en même temps et par le même chemin . 
nos premières nations ont été les premiers humains à avoir de les contacts avec ces chevaux . 
ces chevaux ont disparu de Amérique de le Nord par la suite . 
ils sont passés en Chine , à le Moyen - Orient et , enfin , à le nord de le Europe . 
leur migration a pris fin vers le milieu de le XVIIe siècle . 
les ancêtres de notre cheval de race canadienne sont arrivés de France avec les premiers colons français . 
ils ont été introduits à le Canada entre 1647 et 1670 par Louis XIV , qui a envoyé environ 30 chevaux de ses propres écuries de Normandie et de Bretagne . 
il ne y avait pas de race normande ou bretonne standard à le XVIIe siècle . 
ainsi , le cheval de race canadienne a pour ancêtres plusieurs races , y compris les chevaux andalou , arabe et percheron . 
le cheval de race canadienne a acquis ses propres caractéristiques avec le temps , alors que il se est adapté à les conditions canadiennes . 
le manque de nourriture a fait que la race est devenue plus petite que ses ancêtres français . 
de nos jours , le cheval de race canadienne peut être considéré comme un cheval de taille moyenne . 
le climat froid et le manque de écuries ont fait que seuls les chevaux les plus forts ont survécu . 
la race est devenue exceptionnellement forte et elle a réussi à très bien se en sortir dans de les conditions difficiles à le Canada . 
le cheval a une robe de hiver épaisse , une crinière et une queue épaisses et les oreilles courtes , ce qui le protège contre les durs hivers canadiens . 
le cheval de race canadienne est le cheval le mieux adapté à le climat canadien . 
en fait , ce est la race la plus résistante de le monde et le appelle souvent le petit cheval de fer . 
les chevaux de race canadienne étaient indispensables à les colons de la Nouvelle - France . 
ils ont aidé à déboiser , à labourer et à cultiver le sol . 
en fait , ils ont été la base de le bien - être économique de la Nouvelle - France . 
il ne est pas surprenant que les habitants de la Nouvelle - France aient eu une grande admiration pour leur cheval . 
lorsque , en 1757 , les habitants ont été désespérément à court de nourriture , les administrateurs de la colonie ont ordonné à les gens de tuer certains de leurs chevaux pour se nourrir . 
les habitants ont été horrifiés . 
ils refusaient de manger leurs chevaux parce que , disaient - ils , ce serait un peu comme manger un de les leurs . 
le cheval de race canadienne a joué plus tard un rôle important à le Manitoba , en Ontario et dans les Maritimes . 
ces chevaux ont servi à déboiser pour permettre le établissement de exploitations agricoles en Nouvelle - Écosse , à le Nouveau - Brunswick et à le Île - du - Prince - Édouard . 
ils servaient à le transport de le bois de oeuvre destiné à la fabrication de les célèbres navires de bois construits dans les Maritimes . 
après la conquête , de nombreux habitants de la Nouvelle - Écosse se sont dirigés à le Ouest , vers la vallée de la rivière Rouge , emportant avec eux leurs robustes chevaux . 
les loyalistes de le empire uni chassés de leur foyer durant la révolution américaine passaient souvent par ce que on appelle de nos jours Québec pour se rendre dans la nouvelle région où ils étaient installés . 
beaucoup ont acheté de les chevaux de race canadienne pour les aider à les travaux agricoles ou comme moyen de transport . 
le cheval de race canadienne était très commun dans le Haut - Canada à le XIXe siècle . 
en fait , dans la région de où je viens , le comté de Grey , ces chevaux étaient utilisés par les arpenteurs qui transportaient de le matériel de Owen Sound à Guelph . 
À partir de Hamilton , ils devaient tout transporter à dos de cheval pour pouvoir faire les levés nécessaires pour ouvrir la région . 
je ai marché derrière une charrue à traction animale et de voir ces chevaux travailler . 
ce sont de les bêtes très travaillantes et efficaces qui nous ont aidés à devenir ce que nous sommes devenus aujourd' hui . 
ils ont aidé à ouvrir les routes rudimentaires de le début de les années 1700 et 1800 , les précurseurs de nos autoroutes modernes . 
dans ma région , il y a toujours de les gens qui font le élevage de le cheval de race canadienne , dont Brenda et Geoff Pantling , de Orton , Patricia Cooper , de Palgrave , Kathleen MacRobbie , de Mount Forest , et Barbara Kidd , de Arthur . 
les Tennessee Walking Horse et les Saddlebred peuvent également prétendre à une lignée canadienne . 
la guerre et les croisements ont cependant mis en danger le existence même de le cheval de race canadienne . 
vers la fin de le XIXe siècle , le cheval de race canadienne était menacé de extinction . 
les éleveurs ont alors créé un livre de haras en 1886 et formé , en 1895 , la Société de les éleveurs de chevaux canadiens . 
puis le gouvernement fédéral a reconnu le importance de ce cheval et a pris de les mesures importantes pour protéger ce symbole de le histoire de le Canada . 
on a mis un terme à le exportation . 
on a créé un livre de haras plus sélectif en 1907 . 
il y a maintenant plus de 1 000 chevaux de race canadienne à le Canada . 
le cheval de race canadienne est un symbole tout indiqué pour le Canada . 
il possède de les caractéristiques que nous , les Canadiens , apprécions . 
il est à la fois déterminé et résistant . 
il est intelligent et a bon caractère . 
tout comme notre pays , il a une longue histoire . 
les chevaux de race canadienne sont paisibles . 
depuis le époque de la Grèce ancienne , le cheval est un symbole de courage et de force . 
le cheval de race canadienne est le un de les plus courageux et de les plus forts à le monde . 
ce est donc le parfait emblème pour le Canada . 
le honorable Sydney Fisher , qui fut ministre de le Agriculture pendant une période incroyablement longue , de 1896 à 1911 , soit 15 ans , a dit ceci de le cheval de race canadienne : 
quoi que il fasse , il ne abandonne jamais . 
il ne rue jamais et les enfants peuvent le approcher sans aucune crainte . 
partout et en tout temps , il est docile et gentil . 
