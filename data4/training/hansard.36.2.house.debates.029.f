cependant , après le avoir examiné de plus près , il me est apparu évident que il visait à promulguer une loi qui existe déjà . 
le Parti progressiste - conservateur appuie les prémisses de ce projet de loi . 
il a toujours soutenu la loi et le ordre , la protection de la société et la défense de les droits de les victimes . 
À part la mention de la protection de les témoins , toutes les autres modifications existent déjà dans la loi actuelle . 
si le Parti réformiste estime nécessaire de modifier la loi pour que la protection de les conjoints soit bien mentionnée , nous ne nous y opposerons pas . 
le projet de loi instaure un programme de protection pour certaines personnes dans le cadre de certaines enquêtes ou poursuites et pour certains conjoints dont la vie est en danger . 
comme la loi actuelle , le projet de loi protège les membres de la société qui acceptent de témoigner contre de les délinquants qui ont les moyens , même en prison , de se venger de leur accusateur . 
le renvoi spécial à les conjoints reconnaît que les conjoints de accusés ont souvent à pâtir de leur témoignage . 
actuellement , le Code criminel prévoit que on ne peut obliger une personne à témoigner contre son conjoint . 
À tort ou à raison , on pourrait faire valoir que ce est le une de les dispositions les plus nobles de le Code criminel . 
cette disposition a toutefois occasionné de les problèmes pour le système de justice et le conjoint de un contrevenant contre lequel de les accusations sont portées . 
souvent , ce conjoint est la personne qui sait le plus de choses personnelles sur le accusé . 
souvent , il peut se agir de informations dont la police ou les procureurs ont besoin pour obtenir la condamnation de le contrevenant . 
le projet de loi C - 223 reconnaît le fait que il y a de les conjoints qui sont disposés à témoigner contre leur partenaire . 
il reconnaît aussi le fait que le témoignage de les conjoints est souvent nécessaire pour que on puisse traduire en justice certains de les criminels les plus dangereux de notre pays . 
il reconnaît également que bon nombre de ces criminels disposent de moyens pour intimider leur conjoint , en menaçant de le blesser , de blesser les enfants et ainsi de suite . 
cette intimidation peut être amorcée par le accusé ou par ceux qui le appuient . 
les informations personnelles que les conjoints détiennent peuvent aussi être utilisées dans le intérêt de le accusé . 
dans de les affaires comme celles - là , le accusé connaît souvent de les moyens efficaces de terroriser son conjoint . 
afin de régler ce problème qui existe depuis longtemps , on a mis en place le Programme de protection de les témoins . 
ce programme vise à protéger une personne et à changer son identité et son lieu de résidence en échange de son témoignage contre un criminel dangereux . 
cette protection est offerte à tout le monde , y compris à les conjoints . 
voilà pourquoi je me interroge sur la nécessité de apporter de les modifications afin de inclure les conjoints . 
ils sont déjà inclus . 
cependant , si la Chambre estime que pareille modification est nécessaire , notre parti ne se y opposera pas . 
en ce qui concerne la protection de les témoins , nous ne savons pas grand - chose à le sujet de ce programme , si ce ne est les faits essentiels . 
la GRC ne donne aucun renseignement à le exception de ceux requis en vertu de le projet de loi C - 78 qui a été adopté en 1996 . 
cette mesure législative établit de les règles précises en ce qui concerne la protection de les témoins , mais , pour de les raisons de sécurité , permet seulement la divulgation de les renseignements de base . 
ce que nous savons , ce est que dans les procès qui exigent la protection de les témoins , le témoignage vient souvent de les victimes de le accusé . 
la divulgation de renseignements autres que les renseignements de base pourrait mettre en danger la vie de ces personnes . 
le programme de protection de les témoins ne est guère une récompense pour avoir aidé à mettre un criminel sous les verrous . 
cependant , grâce à la participation de la GRC à cette opération secrète , les témoins qui font partie de ce programme sont à le abri de une vengeance criminelle , à le moins . 
face à la prolifération de les gangs de criminels à le Canada et à le intensification de la criminalité technologique , de la contrebande de stupéfiants et de le blanchiment de argent , les Canadiens se rendent compte que ce ne est plus le criminel que il faut arrêter , mais toute le organisation à laquelle il appartient . 
sous le gouvernement libéral , le Canada est devenu le lieu privilégié de le crime organisé . 
je le répète , quand il se agit de un criminel qui fait partie de le crime organisé , nous avons affaire à toute le organisation . 
ainsi , toute personne appelée à témoigner contre le accusé se expose à subir les représailles de le organisation entière . 
contrairement à ce que le on voit à le cinéma , les membres de le crime organisé de aujourd' hui ne ont pas de code de honneur . 
dans bien de les cas , si une personne accepte de témoigner contre le un de les leurs , ils veilleront à ce que cette personne ne arrive jamais à le tribunal . 
or , sans témoin , il ne y a pas de procès . 
le projet de loi C - 223 attire notre attention sur le problème , mais il serait plus simple de le régler en modifiant la loi actuelle . 
le projet de loi C - 223 modifie le nom de le programme de protection de les témoins en celui de programme de protection de les témoins et de les conjoints . 
tout comme dans le actuelle loi , le projet de loi C - 223 favorisera le respect de la loi en facilitant la protection de les personnes qui , directement ou indirectement , contribuent à la faire appliquer , lorsque ces personnes ont de les motifs raisonnables de croire que leur vie est en danger de le fait de leur témoignage . 
À le « conjoint » sera assimilé un ex - conjoint et toute personne qui a cohabité avec une autre personne pendant à le moins un an dans une situation assimilable à une union conjugale . 
nous convenons que la notion de conjoint doit être utilisée libéralement de façon à pouvoir protéger tous ceux qui pourraient être en danger . 
nous reconnaissons également que certains voudront bénéficier de ce programme sans nécessairement avoir besoin de la protection que il assure . 
ce est pourquoi que , avant de pouvoir y être inscrit , le témoin ou le conjoint doit être recommandé par un organisme chargé de le application de la loi , et le commissaire de le programme doit étudier la recommandation . 
enfin , un accord précisant les obligations de les deux parties doit être conclu par le témoin ou le conjoint et le commissaire . 
ce sont toutes de les étapes positives , mais elles existent déjà dans la loi actuelle . 
dans le cas précis de le conjoint , le article 7 de la loi est modifié de façon à ce que le on tienne compte de les facteurs suivants pour désigner un conjoint à titre de bénéficiaire de le programme : 
a ) la nature de le risque encouru par le conjoint ; 
b ) la nature de les sévices et de les dommages psychologiques graves causés à ce conjoint par le autre conjoint et , selon le cas , les antécédents criminels de ce dernier ; 
c ) les circonstances qui permettent à le conjoint de croire que sa vie est en danger ; 
d ) les autres formes possibles de protection que le programme ; 
e ) tous autres facteurs que il estime pertinents . 
ce sont toutes de les recommandations raisonnables qui , à mon avis , accorderont une protection à ceux qui en ont besoin tout en éliminant de le processus ceux qui pourraient abuser de ce dernier . 
je dois cependant préciser de nouveau que la loi actuelle comprend de semblables critères pour les gens en général . 
le obligation de rendre de les comptes est prévue dans le processus de sélection tout comme elle le était dans la mesure législative précédente pour les cas de refus . 
en pareille situation , le commissaire fournira par écrit à le organisme de exécution de la loi les motifs qui permettront à ce dernier , à le témoin ou à le conjoint de comprendre la décision . 
en résumé , je tiens à remercier le député de Prince George - Peace River de avoir présenté le projet de loi . 
À mon avis , ce est un bon projet de loi car il fait ressortir indirectement que le gouvernement fédéral ne affecte pas assez de argent à les questions de sécurité publique . 
il renforce les efforts que consentent déjà les organismes de exécution de la loi afin de veiller à ce que les témoins ne soient pas intimidés dans le exercice de leur devoir public . 
nous devons veiller à ce que les criminels ne puissent exercer une influence négative sur nos tribunaux et sur la société en général . 
le projet de loi C - 223 ne modifie pas la loi en vigueur . 
toutefois , si le débat montre que la Chambre est favorable à une forme de protection précise de le conjoint , le Parti progressiste conservateur ne hésitera pas à appuyer le projet de loi . 
