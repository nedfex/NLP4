toutefois , les séances de consultation préliminaires menées auprès de les divers organismes intéressés , y compris de les groupes de défense de les droits de les victimes , ont fait ressortir que une intégration de la protection de les conjoints à la Loi sur le programme de protection de les témoins ne constitue pas la meilleure solution . 
avant même de envisager de retenir cette option , il faudrait de abord faire en sorte de régler les questions complexes sous - jacentes à celle - ci , notamment en ce qui concerne les services offerts à les victimes et la coordination de le action de les gouvernements fédéral , provinciaux et territoriaux . 
