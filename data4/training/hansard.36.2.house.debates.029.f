ce est une industrie qui , à le Québec , est composée de 29 000 entreprises qui emploient plus de 100 000 personnes , et qui sont concentrées dans les secteurs de le hébergement , de la restauration , de le transport , de les agences de voyage , de le divertissement et de les loisirs . 
ce est aussi le 6 e produit de exportation de le Québec , après le aéronautique , avec de les exportations internationales de 1,9 milliard de dollars . 
donc , le Québec se est déjà donné une politique et intervient dans ce secteur qui est de sa juridiction . 
