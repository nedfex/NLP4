initiatives PARLEMENTAIRES 
la LOI SUR LE PROGRAMME DE PROTECTION DES TÉMOINS 
- monsieur le Président , ce est un grand jour pour moi . 
bien que je aie présenté de les projets de loi de initiative parlementaire à de nombreuses reprises à le cours de les six dernières années , ce est mon premier projet de loi qui a été jugé digne de faire le objet de un vote . 
le projet de loi C - 223 , Loi modifiant la Loi sur le programme de protection de les témoins , ou loi sur le changement de identité , comme je le ai appelée , a un objectif : sauver de les vies . 
dans mon discours , aujourd' hui , je vais aborder le problème de la violence familiale dans notre société , de le insuffisance de les lois actuelles et de le programme actuel de changement de identité , et je vais expliquer quelles sont les initiatives proposées dans le projet de loi C - 223 . 
la violence familiale est le un de les problèmes les plus horribles auxquels est confrontée notre société . 
ce est une question qui exige que nous laissions de côté notre esprit de parti et que nous répondions à les véritables besoins de les personnes en danger . 
je aimerais donner quelques chiffres . 
en 1996 , 21 901 cas de violence conjugale ont été enregistrés dans 154 services de police à le pays . 
en 1996 , environ 80p. 100 de les victimes de harcèlement criminel ou de harcèlement avec menaces étaient de les femmes . 
plus de la moitié de ces femmes étaient victimes de ex - conjoints ou autres partenaires . 
entre 1977 et 1996 , il y a eu 2 048 homicides entre conjoints à le Canada . 
dans plus de 56p. 100 de ces cas , les policiers avaient déjà été mis à le courant de la violence exercée par un de les conjoints . 
la lutte contre la violence familiale est très difficile , et je suis conscient que il ne sera pas facile de mettre un terme à ce fléau . 
entre - temps , il y a de les femmes et de les enfants à le pays qui craignent pour leur vie . 
lorsque je ai commencé à me intéresser à la question , je ai été étonné de constater que il y avait très peu de lois qui visaient à prévenir la violence familiale et conjugale . 
il y a peu de recours dans ce domaine , si ce ne est les dispositions législatives interdisant le harcèlement avec menaces et les ordonnances de non - communication . 
la plupart de les lois sont axées sur le châtiment pour de les crimes commis , et il est alors souvent trop tard pour les victimes . 
ce est ce qui est arrivé à Mary - Lynne Miller , de Dawson Creek , dans ma circonscription . 
gravement atteinte à la tête , elle a souffert pendant 21 heures sur le plancher de la cuisine avant de obtenir de le aide . 
elle est toujours dans le coma . 
ce est cette histoire qui me a vraiment sensibilisé à la violence familiale . 
malheureusement , il y a eu de les milliers de autres cas tout aussi tragiques . 
en janvier de cette année , je lisais le Province de Vancouver dans le avion qui me menait à Ottawa . 
je suis tombé sur un article qui décrivait un programme non conformiste appliqué par de les fonctionnaires de le ministère de le Revenu national conjointement avec le ministère de le Développement de les ressources humaines , ayant pour objet de fournir de nouvelles identités à de les personnes qui se trouvent dans de les situations où leur vie est en danger . 
ce programme leur permet de changer de nom et de numéro de assurance sociale afin de se cacher de un conjoint ou ex - conjoint dangereux . 
le article parlait de une femme qui avait subi de les raclées , qui avait fait le objet de menaces de mort et qui avait vécu une véritable torture morale . 
le programme de les nouvelles identités le avait aidée à se établir ailleurs et à obtenir de nouveaux documents . 
malheureusement , ses diplômes universitaires ne étaient pas transférables à sa nouvelle identité , ni son curriculum vitae . 
cela voulait dire que elle a dû se contenter de ne importe quel emploi que elle a pu trouver afin de subvenir à ses besoins et à ceux de son enfant . 
quand son mari retrouvait ses traces , il lui fallait fuir et recommencer le même processus panique de auto - conservation . 
après avoir lu cet article , je ai ressenti de le exaspération et je ai pris la résolution de faire quelque chose . 
je me suis alors mis à faire de les recherches sur la question afin de trouver une solution . 
je ai reçu en réponse de les lettres de appui , de les histoires personnelles tragiques et horribles et de les pétitions signées par de les centaines de personnes qui appuyaient le projet de loi . 
il était rassurant de savoir que de les gens qui sont quotidiennement à les prises avec la brutalité ou les mauvais traitements de conjoints approuvent les principes dont se inspire le projet de loi . 
cet été , une femme a communiqué avec moi après avoir entendu parler de le projet de loi . 
elle est venue à mon bureau , affublée de un déguisement , pour me raconter les mauvais traitements dont elle était victime . 
elle fuit son ex - mari depuis plus de 10 ans . 
elle et son fils sont constamment prêts à changer de logis . 
cet automne , son mari , avec le aide de un enquêteur privé , les a retrouvés . 
les menaces de violence sont tellement graves que le école de son fils a été fermée à clé pour assurer la sécurité de le enfant . 
cette femme a été harcelée , suivie , battue , défigurée à jamais et a reçu de les menaces de mort . 
il est difficile de croire que ces citoyens canadiens vivent dans une telle crainte dans notre propre pays . 
À défaut de une nouvelle identité , cette femme est convaincue que son ex - mari la tuera . 
même si plusieurs ordonnances de non - communication ont été rendues contre lui , on ne peut pas faire grand - chose tant que il ne décidera pas de frapper encore une fois . 
comme je le ai mentionné plus tôt dans mes observations , le programme de nouvelles identités est un programme spécial qui offre une aide à ceux qui en ont besoin . 
cependant , le programme ne est pas bien connu et est appliqué sans mandat ni financement officiel . 
je voudrais saisir cette occasion pour souligner les services remarquables que rendent les fonctionnaires de ces ministères . 
ces gens ont largement débordé le cadre de leurs fonctions pour venir en aide à ceux qui en ont besoin . 
ils montrent que ils sont vraiment à le service de la population . 
je estime que le programme de protection de les témoins est tout indiqué pour offrir de nouvelles identités . 
les femmes qui participeront à ce programme bénéficieront de le savoir - faire et de les connaissances de les agents de la GRC , qui relogent déjà de les témoins à charge . 
tous les organismes chargés de appliquer la loi dans le pays sont reliés électroniquement , offrant un accès à ce programme depuis les coins les plus reculés de le Canada . 
je suis de avis que en lançant le programme de les nouvelles identités à les termes de Loi sur le programme de protection de les témoins , nous pourrons faire davantage pour ces femmes et ces enfants . 
la Loi sur le programme de protection de les témoins définit la protection , incluant le déménagement , le logement et le établissement de une nouvelle identité ainsi que un soutien psychologique et financier . 
le programme de les nouvelles identités est un dernier recours pour ceux que la loi ne protège pas et qui craignent pour leur vie et celle de leurs enfants . 
je ai mentionné plus tôt les nombreux sacrifices que suppose un changement de identité . 
toutefois , certains pourraient être tentés de recourir à ce programme à de autres fins . 
pour empêcher que cela se produise , il y a une liste de critères auxquels il faut répondre pour avoir droit à le programme : la nature de le risque pour la sécurité de la personne , la possibilité de lui offrir une protection sans la faire entre dans le programme , la nature et la gravité de les blessures physiques ou psychologiques infligées par le conjoint ainsi que les antécédents criminels de ce dernier , les circonstances qui amènent la personne à croire que sa vie est en danger , et tout autre facteur que le commissaire de la GRC juge pertinent . 
je crois que ces critères sont équitables et que ils garantiront que ceux qui ont vraiment besoin de le programme pourront en bénéficier . 
le Canada est censé être le meilleur pays de le monde à habiter . 
mais pour la femme qui vit dans la crainte de un conjoint violent , ce est le enfer sur terre . 
je me rends bien compte que , selon le Règlement , un député peut intervenir pendant 20 minutes quand il propose un projet de loi faisant le objet de un vote . 
il va sans dire que ce projet de loi est tragiquement indispensable et que , même si je en parlais toute la journée , je ne pourrais pas lui rendre justice . 
toutefois , je crois sincèrement que cette question va au - delà de la ligne de parti et que elle revêt tellement de importance que je vais limiter mes observations pour permettra à un plus grand nombre de députés de en débattre . 
en guise de conclusion , je invite tous les députés à appuyer ce projet de loi . 
ce ne est un petit pas que nous pouvons franchir pour protéger ceux qui ont le plus besoin de notre aide . 
m . Jacques Saada ( secrétaire parlementaire de le solliciteur général de le Canada , Lib . ) : 
monsieur le Président , je voudrais parler de le projet de loi C - 223 , la Loi modifiant la Loi sur le programme de protection de les témoins . 
mon collègue le a dit il y a quelques secondes , ce est un projet de loi qui ne porte pas sur une question de politique , qui ne a aucune saveur politique . 
le gouvernement actuel appuie fermement toute mesure visant à assurer la sécurité de les personnes dont la vie , et parfois de ailleurs celle de leurs enfants , est menacée par un conjoint ou un ex - conjoint . 
il ne peut pas y avoir grand désaccord à ce sujet . 
ce est un problème que le gouvernement a pris très à le sérieux . 
un processus fédéral , provincial et territorial est en place depuis un certain nombre de années . 
il se agit de le programme Changement de identité pour les personnes menacées de mort par leur partenaire . 
il a été élaboré sous la conduite de Développement de les ressources humaines Canada . 
dans les situations où il ne y a aucune autre solution , cette initiative donne à les personnes menacées de mort la possibilité de changer leur identité pour échapper à les risques de agression grave ou de mort . 
en bref , ce est ce à quoi tend le projet de loi C - 223 . 
mais , dans la nouvelle initiative , les choses se font de une façon qui me paraît plus acceptable . 
je reviendrai dans un moment sur les avantages de le processus de Nouvelles identités par rapport à le projet de loi C - 223 , mais je aimerais de abord vous entretenir brièvement , de façon plus générale , de le changement de identité en tant que moyen de assurer la sécurité de les victimes de actes de violence familiale . 
en ce qui concerne les personnes victimes de actes de violence conjugale mettant leur vie en danger , le changement de identité est une mesure extrême , une mesure qui , pour de les raisons évidentes , ne se applique que dans de les circonstances exceptionnelles et en dernier recours . 
bien que le changement de identité offre une sécurité accrue , il comporte de les difficultés et de les épreuves . 
les personnes visées doivent quitter leur milieu de vie familier et rompre les liens que ils entretenaient avec leur famille et amis . 
et quand il y a de les enfants , bien sûr , les choses sont encore infiniment plus compliquées . 
néanmoins , bien que il se agisse de un dernier recours , le établissement de une nouvelle identité est parfois nécessaire . 
depuis 1992 , le initiative commune de les autorités fédérales , de les provinces et de les territoires a aidé quelque 203 victimes et se est avéré une méthode très utile pour garantir un peu plus de sécurité à les personnes les plus menacées . 
À un certain nombre de égards , le Canada est à le avant - garde de tous les pays pour les mesures prises contre la violence conjugale . 
la nouvelle initiative sur le changement de identité est le un de les éléments pour lesquels nous sommes à la fine pointe . 
cela dit , nous pouvons , et nous devons , faire davantage . 
le éducation , le counselling , la prévention et les autres mesures de service social sont essentiels pour juguler la violence familiale . 
lorsque tout cela échoue , il faut prendre de les mesures contre les conjoints violents . 
les partenaires fédéraux , provinciaux et territoriaux ont déjà travaillé à le développement de un processus de Nouvelles identités . 
il est toujours possible de le perfectionner , mais il ne se agirait pas de réinventer la roue . 
nous devons travailler à partir de bases établies . 
effectivement , il reste encore un certain nombre de questions à régler . 
il faut notamment coordonner de façon plus formelle le processus de Nouvelles identités , un processus auquel , je le rappelle , participent les gouvernements fédéral , provinciaux et territoriaux . 
il faut également examiner les critères de admissibilité et les questions liées à la mobilité , ainsi que se pencher sur le financement de un programme pleinement mis en oeuvre . 
toutes ces questions exigent consultation et coordination . 
les gouvernements de tous les niveaux doivent participer à la lutte contre la violence conjugale . 
je suis heureux de signaler que les fonctionnaires étudient déjà de les améliorations que on pourrait apporter à le initiative de changement de identité . 
ce mois - ci , un groupe de travail fédéral - provincial - territorial a entamé de les consultations à le sujet de les questions que je viens de soulever . 
cela me amène à la question dont est saisie la Chambre , ce est - à - dire le projet de loi C - 223 qui vise à modifier la Loi sur le programme de protection de les témoins de façon à ce que les situations de violence familiale entrent dans son champ de application . 
le Programme de protection de les témoins , dirigé par la GRC , sert à protéger certaines personnes qui apportent leur aide à de les interventions de application de la loi . 
un parfait exemple , le cas de témoins qui sont appelés à témoigner dans de les cas de procès touchant le crime organisé . 
alors , bien sûr , ce programme touche de les situations particulières liées spécifiquement à le application de la loi . 
le processus de Nouvelles identités constitue un processus distinct , mené conjointement par les gouvernements . 
il concerne de les situations et de les personnes différentes de celles principalement visées par le Programme de protection de les témoins . 
essentiellement , le projet de loi C - 223 aurait pour effet de intégrer les mesures de sécurité offertes dans Nouvelles identités à la Loi sur le programme de protection de les témoins . 
il ne se agit pas maintenant de veiller à ce que les mesures de protection et de sécurité pour les victimes de violence conjugale soient les meilleures possibles . 
de même , il ne se agit pas de savoir si nous devrions capitaliser sur les processus existants comme les nouvelles identités . 
