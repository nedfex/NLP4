la Commission canadienne de le tourisme a été établie par décret en 1994 , sous la direction de le ministère de le Industrie . 
son mandat consiste à planifier , mettre en oeuvre et gérer de les programmes qui favorisent la croissance de le industrie touristique à le Canada . 
