Mme Michelle Dockrill ( Bras de Or - Cape Breton , NPD ) : 
monsieur le Président , je remercie la Chambre de le occasion que il me offre de prendre la parole en remplacement de mon collègue , le député de Regina - Lumsden - Lake Centre . 
mon collègue et moi sommes tous deux favorables à le projet de loi C - 5 , Loi constituant la Commission canadienne de le Tourisme . 
comme beaucoup de députés le savent déjà , la Commission canadienne de le tourisme a été créée en réalité en 1992 , à la suite de vastes consultations avec les divers protagonistes de le industrie touristique de un bout à le autre de le pays . 
À le époque , à le moment où le gouvernement précédent tâchait de mettre rapidement la commission sur pied et en activité , le gouvernement et le industrie avaient convenu de constituer la CCT en organisme de service spécial plutôt que en société de État . 
un organisme de service spécial , cela veut dire essentiellement que il a toutes les responsabilités et aucun pouvoir . 
la CCT était chargée de exécuter le programme , mais ce est le sous - ministre de le tourisme qui se occupait de le administration . 
À cause de la rapidité avec laquelle la CCT a été créée , son organisation a été privée de tout un ensemble de éléments importants , et ses activités étaient souvent paralysées à cause de le cauchemar bureaucratique qui avait été créé . 
certaines opérations de commercialisation piétinaient durant de les mois parce que elles devaient passer par le processus bureaucratique de approbation qui exigeait 13 signatures . 
quand on finissait par obtenir toutes les signatures , les circonstances avaient presque toujours changé . 
on pouvait détecter le risque de problèmes à un mille . 
les processus de adjudication de les contrats de le gouvernement étaient tout simplement trop lents pour suivre les changements rapides qui se produisent dans le industrie touristique . 
le entreprise ne a pas besoin que on lui permette de agir beaucoup plus rapidement , que ce soit dans le domaine privé ou le domaine public . 
mes collègues néo - démocrates et moi appuyons la transformation de le organisme de service spécial en société de État , pourvu que le gouvernement fournisse à la commission de le tourisme le soutien dont elle aura besoin pour remplir ses objectifs . 
la mise en oeuvre et les réalisations de la Commission canadienne de le tourisme constituent un modèle de partenariat unique entre le secteur public et le secteur privé . 
le industrie de le tourisme fournit actuellement plus de les deux tiers de le financement de les activités de la Commission . 
les relations de travail seront désormais régies par le Code canadien de le travail plutôt que par la Loi sur le emploi dans la Fonction publique et les unités de négociation seront fusionnées , mais il y aura une période de transition de un an qui donnera droit de admissibilité à les concours de la Fonction publique et à les procédures de grief . 
le industrie de le tourisme appuie ce changement , les provinces appuient ce changement et le personnel appuie ce changement . 
je crois que nous devrions être en mesure de nous entendre ici aujourd' hui et de faire adopter le projet de loi le plus rapidement possible . 
toutes les politiques gouvernementales relatives à le tourisme devraient être aussi bénéfiques que celle - ci pour le industrie . 
je profite de le occasion pour rappeler à la Chambre les changement subits et brutaux que nos deux compagnies aériennes ont effectués à le chapitre de les commissions que elles tirent da la vente de les billets à le échelle internationale . 
À lui seul , ce changement menace quelque 7 500 emplois dans les petites communautés de le pays . 
fondamentalement , les deux sociétés aériennes se sont unies pour établir la nouvelles structure de prélèvement de les commissions et elles en ont informé les agents de voyage . 
je répète que les deux sociétés aériennes se sont unies pour établir la nouvelle structure de prix . 
puis , lorsque le association de les agents de voyage a voulu rencontrer les sociétés aériennes pour discuter de les taux , ces mêmes sociétés le ont menacée de le accuser de pratiques monopolistiques en vertu de la Loi sur la concurrence . 
les libéraux sont vraiment passés maître dans le art de appliquer la loi de les deux poids deux mesures . 
même si je ne souhaite pas nécessairement exprimer mon désaccord avec cette déclaration , je dois quand même exprimer de les réserves . 
ce ne est pas le tourisme qui va garantir notre identité . 
ce est plutôt notre identité socio - culturelle distincte en tant que Canadiens qui va garantir le intérêt continu de les touristes pour le Canada . 
le tourisme ne sera jamais rien de plus ni rien de moins que une partie intégrante de notre économie . 
une économie bien équilibrée et dynamique ne peut être établie sur le tourisme seulement . 
je le signale , car il est extrêmement important pour nous à le Cap - Breton de se rappeler de cela à une époque où le gouvernement fédéral nous dit que le tourisme va assurer notre survie . 
le tourisme est sensible à les caprices de le économie et à les petites variations saisonnières . 
il est important de noter que même si nous reconnaissons que cette année , le tourisme a rapporté 230 millions de dollars comparativement à 211 millions de dollars le année précédente à le Cap - Breton , cela fait de le tourisme un de les facteurs importants de reconstruction de notre économie dévastée , mais ce ne est absolument pas la seule réponse . 
comme certains de mes collègues le savent peut - être déjà , les députés néo - démocrates de la Nouvelle - Écosse collaborent avec les exploitants de entreprises touristiques dans toutes les provinces maritimes . 
nous ferons face à une situation de monopole chez les fournisseurs de denrées si la fusion entre les groupes Sobey et Oshawa voit le jour . 
cette fusion pourrait être catastrophique pour les restaurants , les gîtes touristiques et les hôtels qui doivent accepter les prix exigés de leurs fournisseurs de denrées . 
si la fusion se matérialise , il ne y aura pratiquement aucune concurrence dans ce secteur . 
je suis persuadée que pratiquement tous les députés reconnaîtront que une situation où il ne y a ni contrôle ni concurrence pourrait être catastrophique pour les gens qui dépendent de les fournisseurs . 
même si je ai le sentiment de devoir appuyer le adoption de le projet de loi C - 5 , je ne le fais pas sans quelques réserves à le égard de les politiques de le gouvernement libéral relativement à le tourisme et à les exploitants de entreprises touristiques . 
je ne crois pas que le gouvernement libéral appuie les exploitants de entreprises touristiques , surtout ceux qui sont chefs de petites entreprises , ainsi que les employés . 
