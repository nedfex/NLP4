le honorable B . Alasdair Graham ( leader de le gouvernement ) : 
honorables sénateurs , on le sait , sir Max Aitken , devenu plus tard lord Beaverbrook , était un expatrié imaginatif et dynamique qui avait fait sa marque comme magnat britannique de la presse avant la Première Guerre mondiale . 
pendant la guerre , il a joué un rôle essentiel en tant que envoyé spécial de le Dominion de le Canada sur le front occidental et il a , à ce titre , informé les Canadiens et le reste de le monde sur cette guerre qui devait être la dernière . 
on doit à Aitken de avoir commandé le un de les plus remarquables programmes de art de tous les temps , le Canadian War Memorials Fund . 
huit de les immenses et magnifiques huiles créées dans le cadre de ce programme embellisent cette Chambre . 
nos délibérations se tiennent sous le regard attentif de les messagers de le passé . 
nos murs font écho à leurs sacrifices . 
de autres ont décrit les événements de la Grande Guerre dans de les lettres et journaux personnels . 
le un de les plus célèbres auteurs de ce type de récit est Talbot Papineau , qui a servi dans le Princess Patricia' s Canadian Light Infantry , de le mois de août 1914 jusque à sa mort tragique survenue durant le carnage de Passchendaele , à le automne de 1917 . 
petit - fils de Louis - Joseph Papineau , Talbot Papineau a incarné avec passion et intelligence le esprit même de un Canada bilingue et biculturel . 
il est devenu un héros canadien qui a captivé le coeur et le esprit de son époque . 
le compte rendu que il nous fait de le épreuve de le feu sur le front occidental , pour en avoir été témoin , décrit le horreur de les gaz toxiques et de les tranchées à le odeur fétide , le agonie de les hommes rendus fous par le artillerie lourde et décrit le courage , la valeur et les souffrances de un monde qui avait perdu la raison . 
le pays a pleuré sa mort . 
