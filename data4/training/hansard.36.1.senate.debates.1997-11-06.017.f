ils ont beaucoup à nous apprendre , surtout à ceux qui ont perdu le sens de le engagement et le esprit de tolérance que représente , et que représentera toujours le Canada , et à ceux parmi nous qui sont arrogants , indifférents ou négligents à le égard de notre pays . 
