honorables sénateurs , durant les prochaines heures et les prochains jours , nous rendrons hommage à le sacrifice de ces générations . 
nous rendrons hommage à les jeunes hommes et à les jeunes femmes qui ont combattu côte à côte pour cimenter les assises de une véritable nation canadienne . 
nous rendrons hommage à ceux qui , dans les airs , sur la mer et sur la terre , ont contribué à la défense de le Canada . 
nous nous souviendrons . 
