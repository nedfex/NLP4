ce projet donnera à RNCan le infrastructure et les outils dont il a besoin , sur le plan de la technologie de le information , pour accroître son efficacité . 
la CGC étudie actuellement de les façons de poursuivre ces activités dans les fonds documentaires et les bases de données pour le groupe de recherche sur les sites de impact météoritique par le intermédiaire de partenariats et de ententes de collaboration . 
