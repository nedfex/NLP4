dans sa forme actuelle , le projet de loi dispose que le ministre peut constituer un comité consultatif de les cultures spéciales . 
la proposition de amendement stipule que le ministre constitue un comité consultatif de les cultures spéciales , rendant obligatoire la création de cet organisme , qui est présentement facultative . 
il va sans dire que nous appuyons cette motion . 
le gouvernement entend toutefois se opposer à les autres propositions de amendement . 
je vais prendre trois ou quatre minutes pour en expliquer les raisons . 
