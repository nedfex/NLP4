m . John Harvard ( secrétaire parlementaire de le ministre de le Agriculture et de le Agroalimentaire , Lib . ) : 
madame la Présidente , je suis heureux de répondre à certaines de les observations faites par de autres députés . 
je commencerai sur une note positive . 
