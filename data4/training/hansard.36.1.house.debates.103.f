la question sera examinée plus en détail pendant le étude de le second groupe de motions . 
nous appuyons le approche axée sur un régime de cotisations obligatoires remboursables . 
les organisations de cultures de légumineuses à grain procèdent déjà de la sorte en Alberta et en Saskatchewan . Elles ont fait savoir que cette approche fonctionne et nous sommes convaincus que il en ira de même dans le cas de ce régime de assurance . 
ce est pourquoi nous le proposons . 
quelque un a dit que , en définitive , le projet de loi créerait un fonds . 
rien ne est plus contraire à la vérité . 
il se agit de un régime de assurance , ni plus ni moins . 
il ne se agit pas de un fonds . 
lorsque viendra le temps de trouver de les candidats à les postes à pourvoir à le sein de le comité consultatif de les cultures spéciales , le ministre procédera à de les consultations poussées . 
les groupements de producteurs spécialisés auront toute liberté de soumettre leurs recommandations , pour que le ministre comprenne pleinement ce que veulent les producteurs et les négociants . 
je voudrais parler de le fond de ces motions , qui proposent un conseil élu par opposition à un comité consultatif de les cultures spéciales dont les membres seraient nommés . 
nous avons tenu de les consultations généralisées et nous avons constaté que le industrie ne appuie pas cette approche . 
de après nos consultations , le secteur souhaite un organisme dont les membres sont nommés par le ministre . 
le porte - parole de le opposition officielle a essayé de établir une analogie avec le projet de loi C - 4 , concernant la Commission canadienne de le blé . 
le analogie ne tient pas . 
dans le cas de le secteur de les cultures spéciales , si nous créions un conseil de administration dont les membres sont élus , le coût de les élections serait prohibitif . 
ce est ce que nous ont dit un certain nombre de porte - parole de le secteur . 
ce serait là un coût inutile . 
voilà pourquoi nous nous opposons à un processus électoral complexe qui nécessiterait un système électoral compliqué et un mécanisme compliqué pour choisir les neuf administrateurs . 
madame la Présidente , vous savez aussi bien que tous les Canadiens que si nous adoptons un système où il nous faudra élire les administrateurs à le lieu de nommer les membres de un comité consultatif , nous devrons déterminer qui sera éligible . 
il faudra également établir de les circonscriptions si nous devons diviser le territoire en zones représentées chacune par un administrateur . 
ce serait très difficile jusque à un certain point . 
