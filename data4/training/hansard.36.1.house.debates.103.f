malgré les appels que les députés de le opposition ont , à le étape de la deuxième lecture le 27 mars , lancés à le gouvernement afin que il écoute les producteurs , les agriculteurs , le groupe de producteurs de cultures spéciales lors de le étude de ce projet de loi en comité , le gouvernement ne a pas tenu compte de les amendements que les producteurs réclamaient presque à le unanimité . 
je veux parler principalement de la question de le adhésion par défaut . 
À les termes de le projet de loi , dans sa forme actuelle , la contribution sous forme de prélèvement à la source sera obligatoire en dépit de ce que dit le gouvernement et en dépit de ce que demandaient les producteurs qui voulaient que elle soit volontaire dans le cas de les cultures spéciales . 
en de autres termes , les producteurs doivent , à le début de la campagne agricole , déclarer que ils ne veulent pas que leurs contributions soient versées dans le fonds commun de assurance de les acheteurs et doivent ensuite voir combien on prélève sur leurs récoltes durant le année et soumettre les reçus pertinents à la fin de le année . 
le gouvernement a présenté un amendement à le étape de le rapport et le secrétaire parlementaire a laissé entendre que les producteurs auraient à présenter une seule demande durant le année pour se retirer de le régime . 
ce ne est pas ce que laisse entendre le amendement sous sa forme actuelle . 
ce est un petit pas dans la bonne direction , car il est précisé que la Commission canadienne de les grains , en tant que agent pour ce fonds , doit rembourser les producteurs qui ne souhaitent pas adhérer à le régime de assurance . 
le Comité de le agriculture , dominé par les libéraux , a fait tomber le inclusion possible , à le avenir , de six cultures courantes , soit le blé , le orge , le avoine , le seigle , le lin et le colza canola . 
dans le ouest de le pays , le industrie a dit craindre que à un moment donné , à le avenir , la contribution sur les cultures spéciales puisse se étendre à les cultures courantes . 
heureusement , le gouvernement a jugé bon de clarifier cela et de exclure ces cultures . 
malheureusement , le gouvernement ne a pas fait preuve de la même sagesse lorsque nous discutions de le projet de loi C - 4 , les modifications à la Loi sur la Commission canadienne de le blé . 
il aurait dû faire cela pour exclure tous les grains autres que le orge et le blé qui sont , à le heure actuelle , sous la responsabilité de la Commission canadienne de le blé . 
que fait le projet de loi C - 26 ? 
il établit un régime de délivrance de licences et de assurance pour les acheteurs de cultures spéciales et les négociants en cultures spéciales . 
il remplace le système actuel dans le cadre duquel les acheteurs et les négociants doivent verser une caution pour faire face à le éventualité regrettable que ils soient acculés à la faillite ou mis sous séquestre , afin de protéger ainsi les producteurs . 
À le lieu de prévoir le versement de cautions , on établit un régime de délivrance de permis et de assurance . 
de plus , le projet de loi C - 26 apporte de les modifications à la Loi sur les sanctions administratives pécuniaires en matière de agriculture et de agroalimentaire . 
les présents mécanismes de application de la loi sont de portée beaucoup trop limitée . 
la plupart de entre eux sont beaucoup trop stricts et coûteux à faire appliquer . 
dans bien de les cas , pour de les infractions mineures , il y a une limite à ce que la Commission canadienne de le blé peut faire . 
je pense que ce est un pas dans la bonne direction et certainement quelque chose que nous appuyons . 
le projet de loi C - 26 abroge aussi la Loi sur les marchés de grain à terme , vieille de 59 ans , ce qui laisse à la Commission de les valeurs mobilières de le Manitoba le champ libre pour réglementer la bourse de les denrées de Winnipeg . 
je pense que cela aussi est un pas dans la bonne direction , et nous le appuyons . 
comme de habitude , il y a dans ce projet de loi un certain nombre de choses avec lesquelles le opposition est de accord . 
À titre de critique de le agriculture pour le opposition officielle , je dois dire que , même si mes collègues réformistes et moi avons appuyé le projet de loi en deuxième lecture , attendant le étape de le comité pour nous prononcer définitivement , nous retirerons cet appui et voterons contre le projet de loi , à moins que nos amendements de les groupes 1 et 2 ne soient adoptés . 
quelles seraient les conséquences de nos motions de le groupe 1 ? 
nous sommes de avis que il faut un conseil de administration constitué principalement de producteurs , pas simplement un comité consultatif comme le propose le projet de loi . 
tel que le projet de loi est rédigé , le ministre peut nommer un comité consultatif pour le aider à gérer le fonds de les contributions et le assurance qui y est associée . 
ce que nous avons dit , ce que les producteurs ont dit et ce que les témoins qui sont venus devant le comité ont dit , ce est que tout le monde voulait que ce soient les producteurs qui soient en charge de le fonds . 
ceux - ci ne veulent pas que il soit gérés par les administrateurs et les bureaucrates de la Commission canadienne de le blé . 
ils ne veulent pas que il soit contrôlé par de les gens nommés par le ministre de le Agriculture et de le Agroalimentaire pour leur dévouement à le parti . 
comme il se agit de le argent de les producteurs , de le argent qui est soustrait de leurs chèques et versé à le fonds , les producteurs veulent le contrôler eux - mêmes . 
nous avons présenté un groupe de motions . 
deuxièmement , ces producteurs seraient choisis parmi de les noms présentés par les groupes de producteurs de cultures spéciales . 
en de autres termes , les agriculteurs dresseraient une liste et la présenteraient , et le ministre ferait son choix dans la liste . 
nous avons déjà vu la nomination de un ancien député libéral , Ron Fewchuk , et nous ne voulons pas de ce type de nomination politique à le conseil . 
ce que le premier groupe de motions permet de constater , ce est que le comité consultatif ne aura pas vraiment beaucoup de pouvoirs . 
ce est ce que nous avons pu voir à la Commission canadienne de le blé . 
même si le ministre nomme ce comité consultatif formé de neuf membres , dont la majorité doivent être de les producteurs , comme il est dit dans la loi actuelle , les producteurs nous ont dit que ils sont inquiets parce que ils ne auront pas beaucoup de pouvoir . 
ils seront réduits à un rôle consultatif . 
nous avons vu ce qui se était passé à la Commission canadienne de le blé . 
par suite de les pressions intenses exercées par les producteurs , cette commission a évolué pour se doter de un conseil de administration composé avant tout de producteurs , de agriculteurs . 
ce est le même objectif qui est visé par ce groupe de amendements . 
nous voulons que le comité consultatif devienne un conseil de administration qui aura un vrai pouvoir pour veiller sur le argent prélevé de les paiements à les agriculteurs pour assurer les acheteurs et les négociants de grain . 
ce est le argent de les agriculteurs . 
pourquoi est - ce que ils ne en assumeraient pas la gestion à le lieu de la laisser à de les bureaucrates ou à de les personnes nommées par le gouvernement ? 
ce est le grand objectif de les motions réformistes de le premier groupe . 
je exhorte tous les députés à étudier sérieusement ces amendements . 
ces amendements ne sont pas de le seul cru de le opposition officielle . 
