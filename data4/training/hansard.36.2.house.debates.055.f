la JOURNÉE INTERNATIONALE DE LA FEMME 
Mme Raymonde Folco ( Laval - Ouest , Lib . ) : 
monsieur le Président , cette année , le thème de la Journée internationale de la femme sera « Les Canadiennes , initiatrices de changements ! » 
la Journée internationale de la femme sera le occasion de réfléchir à les progrès réalisés en vue de promouvoir le égalité de les femmes et de évaluer les défis à venir auxquels sont confrontées les femmes dans la société moderne . 
le thème de cette année a été choisi afin de souligner les initiatives et les réalisations de les groupes de femmes pour contrer la violence et la pauvreté à le Canada , deux enjeux prioritaires pour Condition féminine Canada . 
le gouvernement rappelle , avec raison , que nous avons toutes et tous un rôle à jouer pour atteindre cet objectif de société et faire en sorte que les femmes puissent bénéficier de une qualité de vie partout à le Canada . 
