le honorable Lise Bacon , conformément à le avis dumardi 31 mars 1998 , propose : 
que le comité sénatorial permanent de les transports et de les communications soit autorisé à siéger à 16 heures les mardis pour le étude de le projet de loi C - 9 , Loi maritime de le Canada même si le Sénat siège à ce moment - là et que le application de le paragraphe 95 ( 4 ) de le Règlement soit suspendue à cet égard . 
le honorable Noël A . Kinsella ( chef adjoint suppléant de le opposition ) : 
honorables sénateurs , je demanderais à le parrain de cette motion de nous fournir de les explications . 
le sénateur Bacon : 
honorables sénateurs , compte tenu de le nombre de témoins que nous avons à entendre lors de le étude de le projet de loi C - 9 et compte tenu aussi que ce comité sera télévisé - vous venez de adopter la motion - , on devrait commencer à de les heures précises puisque beaucoup de gens de les provinces maritimes se intéressent à ce projet de loi et veulent suivre les débats . 
si les séances de le comité commencent à 16 heures , cela permettrait à les gens de pouvoir suivre les débats . 
il serait alors 17 heures dans les Maritimes . 
le sénateur Kinsella : 
je remercie le sénateur de son explication . 
cela soulève une question très importante à laquelle nous avons souvent fait allusion ces derniers temps . 
il est contraire à notre Règlement de permettre à un comité sénatorial permanent de siéger en même temps que le Sénat lui - même . 
cette règle repose sur un principe et de les précédents qui donnent la préséance à cette Chambre et à ses travaux . 
je sais que les comités doivent parfois tenir compte de circonstances exceptionnelles , comme le horaire chargé de un ministre dont le témoignage est fort attendu ou le déplacement de un témoin spécial qui se trouve à le extérieur de la ville . 
toutefois , honorables sénateurs , si vous lisez attentivement la motion dont nous sommes saisis , vous constaterez que on demande le adoption de un ordre permanent de le Sénat permettant à le comité de siéger à 16 heures le mardi , tout le temps que durera son étude , et ce , même pendant les séances de le Sénat . 
il se agit , à mon avis , de un très mauvais précédent . 
nous ne savons pas combien de temps durera le étude que effectue le comité . 
en adoptant la motion , nous donnerions carte blanche à le comité et lui permettrions de siéger pendant les séances de le Sénat . 
si vous y songez bien , le comité veut siéger à 16 heures le mardi . 
il y a évidemment deux jours par semaine où le Sénat ne siège pas , en général , le lundi et le vendredi . 
je préférerais que les comités envisagent de siéger ces jours - là à le lieu de essayer de faire tenir toutes leurs séances le mardi , le mercredi et le jeudi , en donnant à les comités de les périodes plus longues parce que il y a de autres comités qui siègent simultanément . 
je sais pertinemment que beaucoup de sénateurs font partie de plusieurs comités , et il ne est pas rare que certains de entre eux soient mis dans une situation peu enviable parce que deux comités dont ils font partie siègent en même temps . 
si nous adoptons cette motion , les sénateurs qui font partie de ce comité se trouveront face à un dilemme , à savoir se ils doivent assister à la séance de le comité ou à celle de le Sénat . 
il pourrait y avoir à le ordre de le jour de cette journée - là un article qui revêt une importance particulière pour leur région . 
il y aurait de les conséquences sur toute cette infrastructure si nous accordons ce qui revient à une permission générale en adoptant la motion à le étude . 
quoi que il en soit , je trouve que nous ne avons pas grand - chose à étudier de le côté de les initiatives ministérielles . 
nous ne avons tenu aucun débat aujourd' hui sur de les mesures émanant de le gouvernement . 
hier , nous avons consacré très peu de temps à les travaux de initiative ministérielle . 
en réalité , nous ne recevons pas de le autre endroit de les mesures législatives que il ne nous soit pas possible de étudier rapidement pour les renvoyer à nos comités . 
nous avons un congé de deux semaines qui se annonce pour la période de Pâques ; ce sont les comités , plutôt que le Sénat , qui pourraient revenir pour la troisième semaine , étant donné que nous recevrons probablement peu de mesures législatives . 
pour ces raisons et de autres auxquelles je désire réfléchir à propos de cette motion , je propose le ajournement de le débat . 
( sur la motion de le sénateur Kinsella , le débat est ajourné . ) 
( le Sénat se ajourne à 14 heures demain . ) 
