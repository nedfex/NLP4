message DU SÉNAT 
le Président : 
je ai le honneur de annoncer à la Chambre que le Sénat lui a adressé un message pour le informer que il a adopté certains projets de loi sans amendements . 
le huissier de le bâton noir apporte le message suivant : 
monsieur le Président , ce est le désir de le honorable suppléant de le Gouverneur général que cette honorable Chambre se rende immédiatement auprès de lui dans la salle de le honorable Sénat . 
en conséquence , le Président et les députés se rendent à le Sénat . 
et de retour : 
le Président : 
je ai le honneur de faire savoir à la Chambre que , lorsque elle se est rendue à le Sénat , il a plu à le suppléant de le Gouverneur général de donner , à le nom de Sa Majesté , la sanction royale à les projets de loi suivants : 
projet de loi C - 3 , Loi concernant le identification par les empreintes génétiques et modifiant le Code criminel et de autres lois en conséquence - Chapitre 37 . 
projet de loi S - 21 , Loi concernant la corruption de agents publics étrangers et la mise en oeuvre de la Convention sur la lutte contre la corruption de agents publics étrangers dans les transactions commerciales internationales , et modifiant de autres lois en conséquence - Chapitre 34 . 
projet de loi C - 25 , Loi modifiant la Loi sur la défense nationale et de autres lois en conséquence - Chapitre 35 . 
projet de loi C - 38 , Loi modifiant la Loi sur les parcs nationaux ( création de le parc Tuktut Nogait ) - Chapitre 39 . 
projet de loi C - 42 , Loi modifiant la Loi sur le tabac - Chapitre 38 . 
projet de loi C - 53 , Loi visant à accroître la disponibilité de le financement de le établissement , de le agrandissement , de la modernisation et de le amélioration de les petites entreprises - Chapitre 36 . 
projet de loi C - 60 , Loi portant octroi à Sa Majesté de crédits pour le administration publique fédérale pendant le exercice se terminant le 31 mars 1999 - Chapitre 40 . 
le Président : 
comme il est 21 h 20 , la Chambre se ajourne à le lundi 1 er février 1999 , à 11 heures , à les termes de les paragraphes 28 ( 2 ) et 24 ( 1 ) de le Règlement . 
( la séance est levée à 21 h 20 . ) 
