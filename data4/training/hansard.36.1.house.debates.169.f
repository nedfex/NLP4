KINGSTON ( ONTARIO ) 
m . Larry McCormick ( Hastings - Frontenac - Lennox and Addington , Lib . ) : 
monsieur le Président , deux citoyens de la circonscription voisine de la mienne , Kingston et les Îles , M. Ian Milne et Mme Margaret Angus , ont lancé une initiative , intitulée « Kingston - Première capitale » , en vue de faire reconnaître officiellement leur ville comme première capitale de le Canada . 
Kingston , située à le confluent de le Saint - Laurent et de le lac Ontario , et porte de entrée de le canal Rideau , a servi de capitale à le Canada - Uni de 1841 à 1843 . 
souvent appelée la Ville de calcaire , Kingston a été le hôte de la première , de la deuxième et de la troisième sessions de la première législature de la Province de le Canada jusque à ce que la capitale fût déplacée à Montréal vers la fin de 1843 . 
de les divers édifices qui ont accueilli le Assemblée législative , deux seulement , le hôpital de Kingston et le marché Bonsecours de Montréal , existent encore . 
il est vraiment approprié que Kingston soit reconnue officiellement comme la première capitale de notre grand pays . 
je visite régulièrement cette ville historique , et je admire les traditions et le respect de le histoire que nourrit cette collectivité . 
je applaudis à les efforts deM. Milne et de Mme Angus de même que de ceux qui les aident , et je les félicite de leur dévouement et de leur engagement envers ce projet valable . 
