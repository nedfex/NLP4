la SEXUALITÉ ET LA SANTÉ GÉNÉSIQUE 
Mme Beth Phinney ( Hamilton Mountain , Lib . ) : 
monsieur le Président , la majorité de la population mondiale est constituée de jeunes qui , à le cours de le prochain millénaire , grandiront avec peu , voire pas de information sur leur santé génésique . 
chaque année , deux millions de jeunes entre cinq et quinze ans sont lancés sur le marché mondial de le commerce sexuel , la majorité étant de les adolescentes . 
on estime que , chaque année , un adolescent sur vingt contracte une maladie transmissible sexuellement . 
le Fonds de les Nations Unies pour la population , dans son rapport sur le état de le monde , ainsi que la Conférence internationale sur la population et le développement estiment tous deux que les pays développés et en développement doivent supprimer les obstacles juridiques , réglementaires et sociaux à la santé génésique et relative à la sexualité , de manière à ce que les adolescents puissent avoir un meilleur accès à de le information et à de les services à le sujet de leur santé . 
puisque le 11 juillet est la Journée mondiale de la population , je invite le ACDI et les députés de les deux côtés de la Chambre à profiter de cette occasion pour appuyer les initiatives qui font la promotion de les besoins de les jeunes en matière de santé génésique et relative à la sexualité . 
