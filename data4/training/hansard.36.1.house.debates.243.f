les MALADIES THYROÏDIENNES 
m . Bernard Patry ( Pierrefonds - Dollard , Lib . ) : 
monsieur le Président , je suis heureux de informer la Chambre que le mois de juin est le Mois de les maladies thyroïdiennes à le Canada . 
plus de un million de Canadiens et de Canadiennes de tous les âges souffrent de une forme quelconque de maladie thyroïdienne et cette maladie frappe cinq à dix fois plus de femmes que de hommes . 
lorsque les troubles thyroïdiens ne sont ni décelés , ni traités , ils entraînent souvent de graves problèmes physiques ou affectifs . 
À travers ses 22 sections , la Fondation canadienne de la thyroïde se emploie à sensibiliser la population à les affections thyroïdiennes à le moyen de publications et de réunions de information à le échelle communautaire à les quatre coins de le Canada . 
je profite de le occasion pour souligner le travail remarquable accompli par la Fondation canadienne de la thyroïde et ses nombreux bénévoles dévoués , et leur souhaiter tout le succès possible pendant le Mois de les maladies thyroïdiennes . 
