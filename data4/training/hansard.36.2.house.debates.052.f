loi SUR LA CITOYENNETÉ AU CANADA 
m . Andrew Telegdi ( secrétaire parlementaire de la ministre de la Citoyenneté et de le Immigration , Lib . ) : 
madame la Présidente , je suis heureux de avoir le honneur de ouvrir ce débat sur le projet de loi C - 16 concernant la citoyenneté canadienne , à le nom de la ministre de la Citoyenneté et de le Immigration . 
À mon avis , comme à celui de bon nombre de Canadiens , la citoyenneté canadienne représente le appartenance à notre société . 
la citoyenneté est donc beaucoup plus que un simple document , plus que une case à cocher sur un formulaire , plus que un document qui se avère commode quand on voyage à le étranger . 
ce est pourquoi la loi sur la citoyenneté doit donc être considérée comme le une de nos plus importantes lois . 
notre loi sur la citoyenneté établit les règles fondamentales qui doivent régir ceux qui peuvent se considérer comme de les Canadiens à part entière . 
elle reflète ce que les Canadiens retirent vraiment de leur citoyenneté . 
en 1947 , le gouvernement libéral a consacré le statut de citoyen canadien dans la loi pour la première fois . 
cette loi de 1947 a marqué le début de une ère de grands changements dans la société canadienne , ère qui se est poursuivie sur 30 ans . 
en 1977 , alors queM. Robert Andras était ministre de la Main - de oeuvre et de le Immigration , le gouvernement libéral a adopté une nouvelle loi sur la citoyenneté . 
cette nouvelle loi se appuyait sur le expérience tirée de la première loi canadienne sur la citoyenneté . 
cette deuxième loi nous a été très utile à le cours de les vingt dernières années , qui ont été marquées par une évolution encore plus rapide . 
toutefois , le temps est venu de la moderniser . 
le temps est venu de adopter une loi qui reconnaisse pleinement les répercussions de la Charte canadienne de les droits et libertés . 
le temps est venu de adopter une loi qui traite de les nombreuses questions juridiques et administratives qui ont surgi avec le expérience à le cours de les années . 
le temps est venu de adopter une loi qui rehausse la valeur de la citoyenneté canadienne . 
ce est ce que fait le projet de loi C - 16 . 
permettez - moi de souligner de abord que nos principes de base concernant la citoyenneté ont résisté à le épreuve de le temps . 
la signification que les Canadiens donnaient à la citoyenneté en 1947 est essentiellement la même que celle que ils lui donnent encore aujourd' hui . 
les enfants nés à le Canada seront encore de office de les citoyens canadiens . 
les enfants nés dans de autres pays , de un parent né à le Canada , ont toujours droit à la citoyenneté canadienne . 
ceux qui viennent à le Canada plus tard doivent être résidents permanents avant de pouvoir demander la citoyenneté . 
ils doivent encore prouver leur attachement à le Canada . 
ils doivent prouver que ils connaissent notre société et nos valeurs . 
ils doivent prouver que ils connaissent à le moins le une de nos deux langues officielles . 
je tiens à rappeler à les députés de cette Chambre que le ancien ministre de la Citoyenneté et de le Immigration a déposé le projet de loi C - 63 lors de la dernier session parlementaire . 
ce projet de loi se voulait à la fois une tentative de moderniser la Loi sur la citoyenneté et de renforcer la valeur de la citoyenneté canadienne . 
le projet de loi est mort à le Feuilleton lors de la prorogation de la Chambre , mais pas avant de avoir été le objet de nombreuses discussions en cette Chambre ainsi que en comité . 
ce projet de loi renferme essentiellement le même objectif et les mêmes dispositions que le projet de loi C - 63 . 
il reflète le large consensus qui était manifeste lors de les débats précédents . 
mais ce projet de loi ne est pas identique à celui que nous avons examiné lors de la dernière session . 
nous ne avons pas tout simplement apposé un nouveau numéro sur un ancien projet de loi . 
permettez - moi de aborder quelques - unes de les modifications majeures . 
par exemple , nous comptons toujours renforcer les critères de résidence , mais nous avons prévu une modification sur la façon de répondre à ces critères . 
mais pour accumuler le temps de présence physique requis , les demandeurs disposeront maintenant de six ans à le lieu de cinq comme le prévoyait le projet de loi C - 63 . 
nous avons aussi décidé de recommencer à créditer le temps de présence physique que a accumulé une personne qui possède un statut juridique à le Canada autre que celui de résident permanent . 
par exemple , la moitié de le temps de présence accumulé par quelque un qui est arrivé à le Canada à titre de réfugié à le sens de la convention , de visiteur ou de détenteur de un permis ministériel pourrait compter , jusque à concurrence de un an , dans les trois ans exigés pour le obtention de la résidence permanente . 
nous avons déjà dit clairement que nous voulons que les enfants adoptés à le étranger obtiennent beaucoup plus simplement la citoyenneté canadienne . 
mais nous voulons aussi répondre à les préoccupations que ont suscitées les propositions que nous avons déjà présentées . 
par exemple , dans les cas de adoption , seuls les mineurs obtiendront automatiquement la citoyenneté . 
les nouvelles règles seront rétroactives à 1977 . 
il devra également être prouvé que le adoption est dans le intérêt supérieur de le enfant . 
permettez - moi de parler de quelques autres changements . 
nous proposons toujours un nouveau serment de citoyenneté , un peu modifié par rapport à le ancien . 
ils continueront de prêter allégeance à le Canada et à Sa Majesté la reine . 
nous modifierons les pouvoirs de annulation et de révocation proposés . 
nous voulons également que il soit clair que le on ne peut révoquer la citoyenneté que dans le cas où la personne a cherché sciemment à nous tromper . 
qui plus est , on ne prendra aucune mesure pour révoquer la citoyenneté de les enfants . 
en ce qui a trait à tous ces changements importants , il faut souligner que les grandes orientations en matière de citoyenneté que notre gouvernement a énoncées en 1998 restent en vigueur . 
elles sont inscrites dans ce projet de loi . 
nous voulons également limiter le nombre de générations de les personnes nées à le extérieur de le Canada qui peuvent prétendre à la citoyenneté canadiens et clarifier les conditions à respecter pour conserver la citoyenneté canadienne . 
nous continuons de croire que la décision de enlever à le processus de attribution ses aspects quasi - judiciaires pour le rendre purement administratif est pleine de bon sens . 
pour ce faire , nous nous proposons de recourir à de les critères et à de les tests qui soient clairs et cohérents . 
nous avons le intention de nous en remettre , pour une large part , à les agents de la citoyenneté dans ce domaine . 
nous maintenons le rôle de les juges de la citoyenneté , ces hommes et ces femmes que ce projet de loi désigne sous le nom de « commissaires à la citoyenneté » . 
ils auront de nouvelles attributions en matière de promotion de la citoyenneté active à le sein de la collectivité . 
ils conseilleront le ministre dans ce domaine et continueront de présider les cérémonies de la citoyenneté . 
ils seront les ambassadeurs de la citoyenneté dans leurs collectivités . 
le projet de loi vise à simplifier le processus de examen à le égard de les personnes qui souhaitent contester les décisions rendues . 
une personne qui estime que il y a eu erreur de fait pourra demander à le ministre de la Citoyenneté et de le Immigration que il soit procédé à un examen de les faits à le origine de cette décision . 
si la personne pense que il y a eu une erreur de interprétation de la loi , elle pourra se adresser à la Cour fédérale pour obtenir un contrôle judiciaire . 
À les termes de le nouveau système , un juge de la Cour fédérale peut exiger que la cause soit réexaminée . 
toutes ces propositions figurent encore dans ce projet de loi , car elles sont judicieuses et appropriées . 
permettez - moi de terminer mon intervention de aujourd' hui avec quelques observations . 
le histoire de la citoyenneté à le Canada est marquée , de une part , par le évolution et , de autre part , par de les principes durables . 
la loi de 1947 était fidèle à son temps . 
elle reflétait la situation relative de les hommes et de les femmes à le époque . 
elle reflétait un monde dans lequel nous entretenions de les liens plus étroits avec de autres pays dont les citoyens étaient de les sujets britanniques . 
en 1977 , le évolution de la vie dans la société canadienne a exigé une modernisation . 
la loi de 1977 supprimait de nombreuses iniquités dans le ancienne loi . 
elle apportait de les critères plus cohérents et égalitaires en matière de citoyenneté . 
elle reconnaissait que les femmes méritaient de être traitées de la même façon que les hommes et de avoir les mêmes droits afférents à la citoyenneté . 
en ce nouveau millénaire , nous avons besoin de une loi encore plus moderne , plus simple , plus claire et plus cohérente . 
ce projet de loi satisfait à ces exigences . 
la nouvelle Loi sur la citoyenneté de le Canada , qui fait aujourd' hui le objet de un débat , va renforcer la valeur de la citoyenneté canadienne . 
il va intégrer le évolution dont notre société a été témoin , ainsi que notre cadre législatif moderne et nos processus gouvernementaux . 
elle continuera de respecter les éléments fondamentaux de ce que la citoyenneté signifie pour nous . 
les principes à le coeur de notre loi sur la citoyenneté ont résisté à le épreuve de le temps depuis le époque où le honorable Paul Martin père a donné à le Canada sa première Loi sur la citoyenneté , en 1946 , il y a plus de un demi - siècle . 
sous la direction de le très honorable William Lyon Mackenzie King , le Canada venait de prouver que il était un pays égal à tout autre sur la planète . 
les Canadiens avaient combattu en tant que Canadiens pour libérer le Europe de la terrible agression nazie . 
de autres Canadiens avaient combattu en Asie et dans le Pacifique pour défendre nos valeurs et nos intérêts . 
ici , à le Canada , les Canadiens avaient contribué de bien de les façons à le effort de guerre . 
la création de une citoyenneté unique bien à nous était une façon tout à fait appropriée de souligner ce que notre pays et nos citoyens venaient de réaliser . 
ce était une bonne façon pour le Canada de donner à ses citoyens un sentiment de appartenance enraciné dans notre pays et ses habitants . 
les Canadiens de naissance et les Canadiens naturalisés ont tous fait leur part pour nous mériter notre place sur la scène mondiale à le époque . 
ils sont tous devenus de les citoyens de ce grand pays en 1947 pour la première fois dans notre histoire . 
les Canadiens de naissance et les Canadiens naturalisés en font autant aujourd' hui . 
elle nous rassemblera tous , que nous soyons Canadiens de naissance ou Canadiens naturalisés . 
À tous les ans , naissent environ 350 000 bébés à le pays , et environ 160 000 personnes font une demande de citoyenneté canadienne . 
notre Loi sur la citoyenneté permet à plus de un demi - million de personnes de se joindre à la grande famille canadienne en tant que égaux , en tant que Canadiens . 
une nouvelle loi sur la citoyenneté sera une façon appropriée de aider à célébrer un nouveau millénaire . 
nous comptons sur le appui de la Chambre à le projet de loi C - 16 . 
