la LOI SUR LA CONCURRENCE 
m . Julian Reed ( secrétaire parlementaire de le ministre de les Affaires étrangères , Lib . ) : 
À ce que je comprends , le projet de loi C - 235 vise à empêcher les fournisseurs verticalement intégrés de resserrer le marges de leurs clients non intégrés qui sont aussi leurs concurrents , de telle sorte que ils menacent la viabilité de ces entreprises non intégrées , généralement appelées de les indépendants dans le industrie pétrolière . 
je félicite aussi mon collègue , le député de Pickering - Ajax - Uxbridge , qui a rédigé ce projet de loi et se est efforcé de trouver de les solutions à les problèmes que éprouvent de nos jours les entreprises indépendantes de le industrie pétrolière canadienne . 
les efforts de le député sont bien intentionnés . 
je doute fort que à le étape de rédaction de le projet de loi , mon collègue et de autres aient envisagé les effets négatifs que je vais essayer de expliquer . 
le projet de loi C - 235 propose deux modifications à la Loi sur la concurrence . 
je vais faire très attention , parce que la question est compliquée . 
le projet de loi interdirait et rendrait passible de une nouvelle infraction à le criminel tout fournisseur verticalement intégré qui fabrique un produit et le vend à le détail à un détaillant qui lui fait concurrence , à un prix supérieur à le prix de détail fixé par le fournisseur verticalement intégré , moins ses coûts de mise en marché et un rendement raisonnable . 
ce changement a pour but de empêcher toute pratique qui aurait pour effet de réduire ou de éliminer la marge de profit de un détaillant non intégré . 
deuxièmement , le projet de loi introduit dans la Loi sur la concurrence une mesure qui se ajoute à les autres dispositions touchant le abus de position dominante , en interdisant à un fournisseur verticalement intégré de obliger ou de essayer de obliger un client qui lui fait concurrence à le niveau de la vente à le détail sur un marché donné à accepter les prix que il lui dicte à le lieu de le laisser libre de fixer son propre prix à le détail . 
avant de examiner les moyens proposés dans ce projet de loi pour atteindre ces objectifs tout à fait louables , je attire le attention de mes collègues sur le fait que de les dispositions visant à interdire de les pratiques potentiellement anti - concurrentielles existent déjà dans la Loi sur la concurrence et que le Bureau de la concurrence veille à ce que elles soient rigoureusement observées . 
la Couronne a eu gain de cause dans de nombreux cas de fixation de les prix de revente , comme on les appelle , portés devant les tribunaux et faisant appel à le type de pratique visée par ce projet de loi . 
de les centaines de milliers de dollars de amendes ont ainsi été imposés et cela a servi à empêcher de autres recours à de telles pratiques . 
de même , le Tribunal de la concurrence a rendu de les ordonnances interdisant à certaines firmes qui avaient abusé de leur position dominante sur un marché de recommencer à recourir à de telles pratiques , ce qui a permis de restaurer la concurrence sur le marché en question . 
ces dispositions existent déjà et sont appliquées avec rigueur . 
pourquoi , dans ce cas , examiner une mesure législative qui est redondante ? 
afin de veiller à ce que les fournisseurs verticalement intégrés ne réduisent la marge bénéficiaire de les détaillants qui leur font concurrence , ce projet de loi propose que le gouvernement ou les tribunaux établissent ce qui à leurs yeux constitue de les frais de marketing et un taux de rendement raisonnables pour le ensemble de les fabricants à le Canada . 
en outre , à chaque changement de prix , peu importe la fréquence , le fabricant serait tenu de se assurer que cela ne affecte pas la marge bénéficiaire de les détaillants qui lui font concurrence . 
sur beaucoup de marchés à le Canada , les prix changent quotidiennement , voire plusieurs fois par jour selon le niveau de concurrence et la nature de le produit en question . 
je rappelle aussi à mes collègues que ce projet de loi , tel que il est libellé , ne se limite pas à le secteur pétrolier pour lequel il a été expressément conçu , mais que il propose de les amendements à une loi qui touche le ensemble de le économie canadienne . 
je crains que bon nombre de nos sociétés , plutôt que de ployer sous le fardeau de cette réglementation , préféreront adopter une politique à long terme de maintien de les prix sans proposer les diverses formes de réductions auxquels on se attend habituellement . 
cette rigidité de les prix aura un effet nocif sur la capacité de les entreprises canadiennes de rivaliser avec leurs concurrents étrangers , dissuadera les investisseurs étrangers de se implanter à le Canada et influera considérablement sur le prix à les utilisateurs en aval de le produit , y compris les consommateurs bien entendu . 
