m . Pat Martin ( Winnipeg - Centre , NPD ) : 
madame la Présidente , comme je ne ai pu terminer mon exposé hier , je suis heureux de prendre à nouveau la parole pour préciser la position de le NPD sur le projet de loi C - 40 qui modifie la Loi sur le extradition . 
comme je le ai souligné hier , le caucus néo - démocrate a quelques réserves et préoccupations à le égard de le projet de loi C - 40 , mais il appuie de façon générale le esprit de cette mesure législative . 
je aimerais récapituler certaines de nos préoccupations et réserves . 
tout de abord , nous craignons que les modifications apportées à les règles de la preuve ne soient pas suffisantes ou assez complètes pour satisfaire notre porte - parole en matière de justice . 
ce est là notre principale réserve à le égard de le projet de loi C - 40 . 
nous aimerions que le libellé soit plus clair et plus complet sur cette question hautement controversée . 
nous aimons croire que nous avons dépassé ce stade . 
notre position devrait donc être très très claire sur la question de le extradition de criminels vers un pays où la peine de mort est toujours appliquée ; nous ne voulons pas jouer un rôle dans un tel processus . 
la formulation à ce sujet préoccupe le caucus néo - démocrate . 
je ne pense pas que il soit même nécessaire de revenir sur tous les cas où de les Canadiens ont été accusés de crimes graves et , par la suite , innocentés . 
si notre pays imposait la peine capitale , il pourrait arriver que de les personnes soient exécutées et que , par la suite , leur innocence soit établie . 
les Canadiens blâment sévèrement les pays où de tels cas se sont produits . 
son innocence a par la suite été catégoriquement établie . 
cet homme a donc été électrocuté pour rien . 
de plus , en cas de extradition de un criminel , il faudrait que le crime en question soit reconnu à le même degré dans le pays visé que à le Canada . 
la question est très simple . 
si le Canada renvoie un criminel dans un autre pays , ce dernier devrait appliquer une sévérité égale à celle qui est exercée à le Canada . 
la même chose ne devrait - elle pas se appliquer à les méthodes de châtiment ? 
nous ne dénonçons pas seulement la peine capitale , mais également les pays qui infligent , en guise de peines , de les châtiments cruels et inhumains comme la torture ou la mutilation , entre autres . 
si la règle est stricte concernant le extradition vers de les pays qui appliquent la peine capitale , nous aimerions aussi savoir quelle souplesse il pourrait y avoir . 
si nous avions un accord écrit de le pays signataire avec nous de un traité de extradition , selon lequel le détenu ne serait pas exécuté , serions - nous plus à le aise pour extrader le détenu ? 
le argument invoqué est que cette personne ne devrait pas aller subir son procès à les États - Unis parce que la Floride , où le incident a eu lieu , applique la peine capitale . 
le projet de loi C - 40 nous donne le occasion de examiner de les situations comme celles - là ; nous pourrons donc y faire face le cas échéant . 
je reviendrai tout à le heure sur ce cas si le temps le permet . 
il crève les yeux que nous devions revoir notre façon de faire . 
la ministre de la Justice me a récemment écrit une lettre à le sujet de le cas dont je parlais à le instant . 
de après le ton et le contenu de sa lettre , elle même ne cache pas que le processus de extradition est lent , lourd et assommant . 
À la première étape , souvent qualifiée de judiciaire , un juge doit établir , sur la foi de la preuve par affidavit qui lui est présentée par le État demandant le extradition , si les actes commis dans cet État étranger constitueraient une infraction à le Canada . 
voilà la dualité dont je parlais tout à le heure . 
nous devons tout de abord acquérir la conviction que les actes en cause constitueraient un crime aussi important chez nous que dans le État qui demande le extradition . 
cette condition doit être remplie à le étape judiciaire . 
ce est également à cette étape que le État requérant doit prouver que il possède suffisamment de preuves . 
dans le cas que je évoquais , si le grand jury fédéral de les États - Unis porte une accusation , il est clair de après les éléments de preuve que la personne visée devrait à tout le moins comparaître devant les tribunaux et subir un procès . 
il y a ensuite le étape dite de le exécution , généralement la plus longue de tout le processus de extradition . 
ce est là que le ministre de la Justice intervient . 
en effet , le ministre de la Justice doit décider si , oui ou non , il y a lieu de extrader le fugitif vers le État requérant , conformément à le traité de extradition pertinent . 
ce est là que le processus peut être paralysé , et ce , pendant de les années . 
dans le dernier paragraphe de la lettre que elle me a adressée , la ministre de la Justice essaie de apporter quelques motifs de réconfort . 
elle fait valoir que le processus est beaucoup plus rapide depuis les modifications qui ont été introduites en 1992 à la loi sur le extradition . 
elle prétend que cela a permis de abréger considérablement le processus de appel . 
or , retarder la justice , ce est nier la justice . 
certes , je dois reconnaître que la loi sur le extradition est de les plus complexes . 
plusieurs intervenants sont en cause . 
il nous faut composer avec la situation politique internationale , le droit pénal , le droit international , de les lois contradictoires , les tribunaux , le ministre fédéral de la Justice et le gouverneur général . 
ce est de cette manière que certains ont exploité la situation actuelle en matière de extradition . 
nous ne devrions jamais accepter ni mettre en place un système dans le cadre duquel il faut cinq , sept et même parfois douze ans avant de traduire un criminel en justice , à plus forte raison se il y a amplement de preuves pour démontrer que la personne visée devrait à tout le moins subir un procès . 
il faut aussi se souvenir que la situation est encore plus complexe lorsque la Loi sur le immigration entre en ligne de compte et lorsque les personnes visées font face à la fois à le expulsion et à le extradition . 
ce est une autre complication qui vient se ajouter très tôt à une demande de extradition . 
souvent , il est possible de expulser quelque un se il se agit de un étranger qui a , à le intérieur de nos frontières , commis une infraction à notre Code criminel . 
de toute évidence , dans ce cas - là , les services de immigration interviennent et expulsent la personne , de sorte que il ne y a pas de extradition . 
