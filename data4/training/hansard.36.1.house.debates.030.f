naturellement , ils avaient tort . 
passons rapidement de 1982 à 1989 , année où le gouvernement de le Québec a invoqué la clause de dérogation . 
le Québec a pris cette initiative à la suite de une décision de la Cour suprême de le Canada , qui a déclaré que la loi linguistique de le Québec , la loi 178 , était anticonstitutionnelle et contraire à la Charte de les droits et libertés . 
je ne tiens pas tant à analyser les raisons pour lesquelles la clause de dérogation a été utilisée . 
il est beaucoup plus intéressant de examiner les parallèles entre les dangers potentiels que posait la clause de dérogation et les risques inhérents à une disposition reconnaissant une société distincte . 
les politiques de le époque ne avaient pas complètement tort à le sujet de la réaction de le opinion publique à le recours , par le Québec , à la disposition dérogatoire . 
le problème , ce est que le tribunal de le opinion publique ne a rien changé et que son plein impact ne se est fait sentir que longtemps après coup . 
néanmoins , il y a eu pour le gouvernement de le Québec de les retombées négatives . 
car , si conforme à la Constitution que ait été son recours à la disposition dérogatoire , il ne était guère acceptable pour ceux qui , tant à le Québec que à le extérieur , dénonçaient le fait que le gouvernement provincial piétinait les droits de les Québécois anglophones . 
