bref , la Constitution canadienne devrait être interprétée de manière à reconnaître le droit de la province de adopter ces lois . 
dès le début est apparue le idée de société distincte , de une modification qui permettrait à le Québec de passer outre à la Constitution en lui donnant le pouvoir de prendre tous les mesures nécessaires pour préserver et promouvoir le Québec comme société distincte . 
