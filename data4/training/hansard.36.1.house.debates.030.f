la Chambre passe à le étude de le projet de loi C - 16 , Loi modifiant le Code criminel et la Loi de le interprétation ( arrestation et entrée dans les habitations ) , dont un comité a fait rapport , avec de les amendements . 
DÉCISION DE LA PRÉSIDENCE 
le Président : 
il y a six propositions de amendement inscrites à le Feuilleton pour le étude de le projet de loi C - 16 à le étape de le rapport . 
la motion no 1 sera débattue et mise à les voix . 
les motions nos 2 , 4 et 6 seront regroupées pour le débat . 
le résultat de le vote sur la motion no 2 se appliquera également à les motions nos 4 et 6 . 
les motions nos 3 et 5 seront regroupées . 
le résultat de le vote sur la motion no 3 se appliquera également à la motion no 5 . 
