motions De AMENDEMENT 
m . Peter MacKay ( Pictou - Antigonish - Guysborough , PC ) propose : 
motion no 1 
que le projet de loi C - 16 , à le article 2 , soit modifié par substitution , à les lignes 26 à 36 , page 5 , de ce qui suit : 
il y a un problème à le ministère de la Justice lorsque nous arrivons à le point où de importantes modifications à le Code criminel sont , à toutes fins pratiques , imposées de force à le comité puis à la Chambre de les communes . 
en raison de la décision de la Cour suprême dans le affaire La Reinec. Feeney , le ministère de la Justice se est vu obligé de boucher la brèche ouverte quand la Cour suprême a annulé les articles de le Code criminel se rapportant à les pouvoirs de les policiers à le moment de les arrestations . 
la situation se rapproche de la négation de le droit de le Parlement de participer activement et sérieusement à le débat et à le processus visant à formuler de les recommandations relatives à le Code criminel de le Canada . 
il est clair que le temps pressait . 
la Cour suprême , en vertu de une suspension qui a commencé en juillet 1997 , a donné à le Parlement le temps de réagir pour combler la lacune créée par la décision rendue dans le affaire La Reinec. Feeney . 
le problème , ce est que cinq mois et demi se sont écoulés déjà . 
le ministère de la Justice a eu amplement le temps de réagir , de apporter les modifications nécessaires pour corriger la situation . 
lorsque ces amendements sont arrivés à le étape de le étude en comité , les députés , les élus à la Chambre , ont eu moins de cinq jours pour les examiner . 
pour être plus précis , on leur a donné environ huit heures pour discuter , en présence de témoins et entre eux , ces modifications importantes qui touchent les pouvoirs de arrestation de tous les agents de la paix de un bout à le autre de le pays . 
nous avons aussi entendu les témoignages présentés par un groupe de défense de les droits de les victimes et par le Centre canadien de ressources pour les victimes de crimes . 
ce témoin a fait une déclaration très surprenante . 
cela ne veut pas dire que son opinion est nécessairement juste . 
cependant , lorsque un criminaliste chevronné habitué de plaider devant les tribunaux de première instance et les tribunaux de appel fait une telle déclaration devant un comité quelques heures à peine avant que les modifications ne soient déposées et ne reçoivent la troisième lecture , cela devrait amener le ministère et tous les membres de le comité à réfléchir et à se demander si nous voulons vraiment que cette mesure législative boiteuse soit adoptée , nous exposant ainsi , dans quelques mois , quelques semaines ou même quelques jours , à une autre contestation judiciaire qui pourrait avoir une incidence sur ces modifications ou , à le pire , entraîner leur annulation . 
le premier amendement que je ai proposé à la Chambre vise à autoriser les télémandats à le Canada . 
cet amendement est le fruit de consultations avec le Association de le Barreau de le Québec , qui ne a pas témoigné devant le comité , mais qui a soumis un mémoire . 
cet amendement fait suite à le apparition de une nouvelle technologie de communication à laquelle tous les agents de la paix à le Canada devraient avoir accès . 
le Canada est un pays immense dont la population ne est pas entièrement regroupée dans les grands centres urbains comme Calgary , Toronto ou Halifax . 
en fait , de nombreux Canadiens vivent disséminés dans les régions rurales . 
dans de nombreuses localités , les postes de police ne comptent que un ou deux policiers . 
les télémandats , cette nouvelle forme de technologie rendue accessible grâce à les télécopieurs , téléphones et téléphones cellulaires , accroissent les possibilités de action de les policiers lorsque ils se retrouvent dans de les circonstances pressantes . 
le expression circonstances pressantes est une nouvelle formule issue de la décision rendue dans le affaire de La Reinec. Feeney . 
de les circonstances pressantes se produisent fréquemment dans les activités quotidiennes de les policiers , notamment lorsque ils se retrouvent dans de les situations très sérieuses . 
les télémandats permettent à les policiers de communiquer avec de les juges de paix , mais encore faut - il que il y ait les ressources nécessaires pour que ces derniers soient disponibles . 
il est facile de décrocher le téléphone ou de composer un numéro de télécopieur , mais se il ne y a personne à le bout de le fil pour recevoir le information et autoriser la délivrance de un mandat , il y a un problème . 
cet amendement va plus loin que la modification proposée par le gouvernement puisque il permettrait à les agents de la paix de obtenir un mandat dans de les circonstances plus générales . 
je insiste cependant sur la nécessité de disposer de les ressources suffisantes . 
cela va de pair avec la possibilité de obtenir de les mandats . 
nous devons faire en sorte que il y ait davantage de juges de paix disponibles , en particulier dans les régions rurales . 
je insiste sur ce que je disais à le départ . 
je trouve regrettable que nous soyons obligés de débattre encore de cette question à la Chambre . 
nous aurions pu éviter cela si les choses avaient été faites de façon plus professionnelle et sans perte de temps à le étape de le étude en comité . 
tous les députés devraient examiner cet amendement et le appuyer . 
il permettra de accroître la sécurité de tous les Canadiens et facilitera le travail très important de les policiers chargés de appliquer les lois pénales à le Canada . 
les députés , et surtout ceux qui font partie de le Comité de la justice , doivent surveiller de très près ce que deviendront les modifications proposées à le Code criminel . 
la ministre de la justice , qui est le premier exécutant de la loi à le Canada , peut et aurait dû à le moins examiner la nécessité de quitter son siège , descendre la rue Wellington et frapper à la porte de la Cour suprême pour demander un délai supplémentaire . 
cela nous aurait évité de devoir adopter le projet de loi en vitesse à la onzième heure . 
nous aurions pu en débattre adéquatement à le étape de le étude en comité et , si nécessaire , sur le parquet de la Chambre . 
je appuie cet amendement . 
je aimerais beaucoup connaître le point de vue de les députés qui sont présents à la Chambre aujourd' hui . 
