la SOCIÉTÉ DISTINCTE 
que , de le avis de la Chambre , le gouvernement devrait appuyer le égalité de les provinces et les Canadiens en général en faisant en sorte que aucune province ne soit reconnue comme société distincte dans le cadre de la Constitution de le Canada . 
À la lumière de la récente déclaration de Calgary , tous les Canadiens devraient comprendre le importance de cette motion . 
Étant donné que les partis traditionnels souhaitent se en tenir à la notion de société distincte , je ne me fais aucune illusion quant à les critiques que ils vont soulever aujourd' hui . 
pour commencer , certains vont dire que le initiative dont nous sommes saisis est anti - Québec . 
pour ceux qui ont ce sentiment , je tiens à dire que tout comme la déclaration de Calgary , la motion ne se attaque à rien ni personne . 
elle est , fondamentalement , en faveur de le Canada dans son ensemble . 
elle est rédigée dans le but de créer un nouveau Canada dans lequel tout le monde est fier de être Canadien . 
je dis cela avec beaucoup de confiance . 
cette motion est dans le intérêt de les Canadiens en ce sens que elle reconnaît un principe fondamental cher à le coeur de tous les Canadiens , à savoir que nous sommes tous égaux . 
il se agit de promouvoir le principe de le égalité de tous les Canadiens et de préciser clairement le caractère essentiel de les droits individuels . 
la protection de ces droits est également importante . 
un de les principaux défauts de les accords de Meech et de Charlottetown , ce est que ils visaient à insérer un droit collectif dans la Constitution . 
ainsi , la Constitution canadienne aurait pu subordonner de les droits individuels à le droit collectif de une société distincte . 
cet argument reposait sur la perception répandue et exacte parmi les Canadiens que la reconnaissance de le Québec en tant que société distincte aurait conféré un droit collectif à les habitants de cette province , mais pas à ceux de les autres provinces . 
ce est là que résident le problème et le dilemme auxquels doivent faire face les rédacteurs constitutionnels et les politiques , ceux - là mêmes qui ont tenté à de nombreuses occasions de forcer la reconnaissance de le Québec en tant que société distincte , sans pratiquement aucun égard pour les véritables sentiments de les Canadiens . 
quelle a été la réaction de les dirigeants de les partis traditionnels à les paliers fédéral et provinciaux ? 
le mieux que ils aient pu faire , ce est de qualifier de antipatriotique toute personne qui se opposait à cette initiative . 
ils ont également manipulé les craintes de les Canadiens en disant que le pays serait déchiré si cette modification ne était pas apportée à la Constitution . 
le pays est toujours là . 
pis encore , ils ont délibérément tenté de induire les Canadiens en erreur sur la signification véritable de la notion de société distincte . 
À le époque , le ancien premier ministre de le parti qui se est retrouvé en dernière place avait dit à les Canadiens de le extérieur de le Québec que la notion de société distincte ne était rien de plus que une reconnaissance symbolique de la place de cette province dans la Confédération . 
je voudrais traiter brièvement de cette reconnaissance symbolique . 
ce est ce qui distingue la déclaration de Calgary de les accords de Meech et de Charlottetown . 
ce était sur ce point que les accords de le lac Meech et de Charlottetown faisaient problème . 
les deux initiatives ont tenté de faire de cette reconnaissance historique un pouvoir constitutionnel accordé uniquement à le Québec . 
ce est précisément ce que le ancien premier ministre et le regretté premier ministre Bourassa disaient à les Québécois entre - temps . 
leurs propos laissaient entendre que cette disposition serait utilisée pour accroître les pouvoirs de la province à le sein de la Confédération . 
les propos ambigus et les mesures de le gouvernement fédéral concernant le statut de société distincte seraient comiques , se ils ne compromettaient pas autant le avenir de le pays . 
je voudrais traiter de ce que je considère comme un tort possible à le droit collectif de le statut de société distincte , si jamais ce statut était inscrit dans la Constitution . 
bien sûr , il se agit de une hypothèse parce que , dieu merci , la disposition ne est pas dans la Constitution . 
il faut attribuer cela à la sagesse de les Canadiens plutôt que à la prévoyance de les politiciens traditionnels , qui pensent même aujourd' hui que le fait de inscrire le statut de société distincte dans la Constitution réglera le problème de le unité nationale . 
de toute façon , les arguments sur le tort que pourrait causer le inscription dans la Constitution de la société distincte sont hypothétiques , mais ils reposent sur un précédent historique . 
je vais me arrêter brièvement sur le précédent . 
dans ce cas , les leçons et les arguments permettent de faire ressortir ce que je qualifie de embûches en matière de société distincte . 
dans une certaine mesure , le rapatriement de la Constitution canadienne a été réalisé parce que le gouvernement fédéral a pu obtenir le accord de neuf provinces sur dix . 
le gouvernement fédéral avait agi de façon unilatérale , mais la Cour suprême de le Canada a jugé que il fallait un certain appui de les provinces . 
cette disposition faisait partie de le prix que le gouvernement fédéral a eu à payer pour obtenir une entente sur la Constitution . 
on a justifié la clause de dérogation en disant que elle imposerait un frein constitutionnel à les pouvoirs de le gouvernement fédéral . 
certains premiers ministres croyaient à le époque que cette disposition se imposait notamment pour éviter que une mesure législative ou un nouveau programme de dépenses ne empiète sur de les domaines de compétence provinciale . 
on croyait également que cette clause contribuerait à convaincre le Québec de ratifier la Constitution . 
À part la justification et les intentions motivant la clause de dérogation , bonnes ou mauvaises , certains ont prévu le danger potentiel que présenterait une constitution permettant à une province de ne pas tenir compte de une loi fédérale ou de un jugement de la Cour suprême sur la constitutionnalité de une loi provinciale . 
ce danger potentiel est important , car les arguments invoqués pour en minimiser le importance de les conséquences à le époque sont les mêmes que ceux que le on invoque maintenant pour faire accepter le concept de société distincte . 
il ne est donc pas étonnant que les Canadiens pensent que les gouvernements successifs ne ont rien appris de les expériences constitutionnelles passées . 
ils ont peu ou pas confiance en la sincérité de les politiciens de faire ce qui convient le mieux ni en leur capacité de régler le problème de le unité nationale . 
ce est vraiment tragique . 
comme le a déjà dit quelque un dont le nom me échappe pour le moment , ceux qui ne apprennent pas de les erreurs de le passé sont condamnés à les répéter . 
quelles étaient ces terribles conséquences , comme certains le prévoyaient , qui pouvaient découler de la clause de dérogation ? 
les Canadiens qui avaient suffisamment de clairvoyance ont prévu que une province pourrait invoquer cette clause pour passer outre à la charte de les droits et que , ce faisant , elle pourrait porter atteinte à les droits de certains de ses citoyens . 
