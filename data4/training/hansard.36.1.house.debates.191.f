de tous les périodiques vendus à le Canada , la proportion de périodiques canadiens est passée de 20p. 100 en 1956 à 65p. 100 en 1997 . 
dans un pays aussi vaste que le nôtre et compte tenu de notre riche diversité culturelle , les périodiques canadiens sont un puissant véhicule pour exprimer et échanger de les histoires , de le information et de les perspectives . 
