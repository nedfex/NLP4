les recettes provenant de la prestation de services publicitaires sont la principale source de revenus pour les éditeurs de périodiques . 
elles servent à financer la production de le contenu rédactionnel , le achat de photographies et bien de autres choses encore . 
sans ces recettes , il ne y aurait pas de contenu de origine canadienne . 
