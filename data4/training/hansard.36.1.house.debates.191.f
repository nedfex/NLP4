nous savons tous que cet argument se est révélé spécieux . 
le plus important moteur de la croissance économique de le Canada à le cours de la dernière décennie a été le commerce bilatéral avec les États - Unis , ce pays avec lequel nous effectuons 80p. 100 de nos échanges commerciaux , soit environ un milliard de dollars par jour . 
le projet de loi C - 55 vise une toute petite partie de les échanges annuels de 350 milliards de dollars de plus entre nos deux pays , soit le marché de la publicité de 400 millions de dollars de les magazines . 
pas un seul Canadien ordinaire , à le extérieur de le milieu politique étrange et tordu que constituent Ottawa et le caucus libéral , ne voulait que nous ramenions le économie de les décennies en arrière en consacrant le protectionnisme dans ce projet de loi , ce est - à - dire en imposant de les restrictions à la liberté de expression , en pénalisant les publications américaines qui contiennent de la publicité canadienne . 
