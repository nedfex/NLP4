DÉCLARATIONS DE SÉNATEURS 
le Nouvel An chinois 
le honorable Vivienne Poy : 
la famille et les amis se réunissent pour partager de la nourriture , rendre hommage à les ancêtres et attendre le début de le printemps animés de un nouvel espoir . 
la tradition voulant que les familles se réunissent à la veille de le Nouvel An tire son origine de une ancienne légende concernant une bête appelée « nian » . 
une fois le an , la bête surgissait de la mer et dévorait les gens et leurs animaux . 
les familles se réunissaient à domicile la veille de le jour de le An pour fêter , boire et prier pour que la bête ne les attrape pas . 
le lendemain matin , les gens sortaient de chez eux pour se féliciter mutuellement de être sortis indemnes de la nuit grâce à les mots « gong hey » . 
de le fait de ce lien , le mot chinois désignant la bête , « nian » a fini par signifier « an » . 
la date de le Nouvel An est fixée selon le calendrier lunaire chinois . 
le année traditionnelle chinoise compte 354 jours et12 mois lunaires de 29 ou 30 jours chacun . 
tous les deux ou trois ans , un treizième mois se ajoute pour que le calendrier corresponde à les mouvements de la Terre et de le Soleil . 
le Nouvel An chinois correspond chaque année à une date différente de le calendrier grégorien et se situe entre le 21 janvier et le 19 février . 
chaque année de le calendrier lunaire porte le nom de le un des12 animaux de le zodiaque chinois . 
nous entamons aujourd' hui le année de le lapin . 
la fête de le Nouvel An chinois et les journées qui suivent sont une période de célébration . 
les écoliers jouissent de un congé de dix jours et , dans les universités , les étudiants entament leurs vacances de le printemps qui peuvent durer jusque à six semaines . 
de les expositions , de les foires florales , de les danses de lion , de les feux de artifice et de les parades marquent le début de la nouvelle année . 
les enfants et amis célibataires reçoivent de les « lai xi » de les personnes mariées . 
ces petites enveloppes rouges contiennent de le argent , en guise de bonne fortune . 
de nombreux aliments de le Nouvel An chinois traditionnel sont choisis parce que leur nom est phonétiquement semblable à de les souhaits de bonne chance . 
les personnes qui les consomment verront leurs voeux se réaliser . 
les Chinois décorent traditionnellement leurs maisons et entreprises de fleurs en pots , qui sont un important symbole de croissance et de prospérité . 
comme le arbre de Noël dans les pays occidentaux , de les pêchers et cerisiers sont coupés et vendus sur les marchés à le occasion de le Nouvel An et placés dans de grands vases . 
se ils fleurissent le premier de le an , ce est signe de bonne fortune pour la nouvelle année . 
